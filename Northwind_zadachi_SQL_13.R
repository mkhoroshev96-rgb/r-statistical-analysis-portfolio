WITH cte as(SELECT order_id
            ,order_date
            ,shipped_date
            ,freight
            ,quantity
            ,discount
            ,unit_price
            FROM orders
            JOIN order_details USING(order_id)),
agg as(SELECT order_id
       ,Extract(month from order_date) as month_year
       ,ROUND(AVG(shipped_date - order_date)::numeric,0) as delivery_date
       ,ROUND(AVG(freight)::numeric,2) as avg_freight
       ,ROUND(SUM(quantity)::numeric,2) as sum_quantity
       ,ROUND(AVG(discount)::numeric,4) as avg_discount
       ,ROUND(AVG(unit_price)::numeric,2) as avg_price
       ,ROUND(SUM(quantity * unit_price * (1-discount))::numeric,2) as sum_chek
       ,ROUND(SUM((unit_price * quantity * (1 - discount)) - (unit_price * quantity * 0.6))::numeric,2) as sum_profit
       FROM cte
       GROUP By order_id, order_date),
case_w as(SELECT *
            ,case 
          when month_year <= 6 Then 'First_half'
          when month_year > 6 Then 'Second_half'
          END as years_group
          FROM agg),
final as(SELECT order_id
         ,years_group
         ,AVG(delivery_date) filter(where years_group = 'First_half') as avg_del_fir_h
         ,AVG(delivery_date) filter(where years_group = 'Second_half')  as avg_del_sec_h
         ,AVG(avg_freight) filter(where years_group = 'First_half') as avg_fr_fir_h
         ,AVG(avg_freight) filter(where years_group = 'Second_half')  as avg_fr_sec_h
         ,AVG(sum_quantity) filter(where years_group = 'First_half') as avg_qnt_fir_h
         ,AVG(sum_quantity) filter(where years_group = 'Second_half') as avg_qnt_sec_h
         ,AVG(avg_discount) filter(where years_group = 'First_half') as avg_disc_fir_h
         ,AVG(avg_discount) filter(where years_group = 'Second_half') as avg_disc_sec_h
         ,AVG(avg_price) filter(where years_group = 'First_half') as avg_pri_fir_h
         ,AVG(avg_price) filter(where years_group = 'Second_half') as avg_pri_sec_h
         ,AVG(sum_chek) filter(where years_group = 'First_half') as avg_chek_fir_h
         ,AVG(sum_chek) filter(where years_group = 'Second_half') as avg_chek_sec_h
         ,AVG(sum_profit) filter(where years_group = 'First_half') as avg_prof_fir_h
         ,AVG(sum_profit) filter(where years_group = 'Second_half') as avg_prof_sec_h
         FROM case_w
         GROUP BY order_id, years_group)
