по замовленнях в першому та другому півріччі.

Нульова гіпотеза: середній профіт по замовленям ніяк не змінилася в залежності
від півріччя

Нульова гіпотеза: середній профіт по замовленям суттєво змінилася в залежності
від півріччя

d_51$avg_profit_per_half_years <- as.numeric(as.character(d_51$avg_profit_per_half_years))

first <- d_51 %>% filter(years_group == "First_half") %>% pull(avg_profit_per_half_years)

second <- d_51 %>% filter(years_group == "Second_half") %>% pull(avg_profit_per_half_years)

t.test(avg_profit_per_half_years~years_group, data = d_51)

Отже, й по данному показнику ми приймаємо нашу нульову гіпотезу, p-value = 0.2, 
а це значить, що середній профіт по замовленям ніяк не змінилася в залежності
від півріччя. Детальніше розглянемо ситуацію на боксплоті

boxplot(avg_profit_per_half_years~years_group, data = d_51)

Боксплот по данному показнику ідентичний тому,що у нас був в випадку з середнім 
чеком клієнтів. Навіть центри приблизно так само відрізняються. Тому пропоную 
перейти до аналізу конкретно самих знасень середніх обох груп. Так от, середній
профіт по групі першого півріччя становить 571,94 долари, в той час як цей же показник
в групі другого півріччя дорівнює 513,56 доларів. Таким чином, абсолютне відхилення
становить 58,38 доларів. Відносна різниця дорівнює 11,37%. Тобто, абсолютне відхилення
досить таки не значне, а ось відносне досить таки суттєве. Але, все це в межах
нульової гіпотези, бо p-value = 0.2, тобто воно більше ніж 0,05, тому умовно 
середній профіт ніяк не змінився не зважаючи на півріччя.

Таким чином, проаналізувавши всі показники по групам першого та другого півріччя
можемо сказати, що суттєвих різниць між данними групами немає. Так, є дещо більші
відхилення, є дещо менші відхилення, але в цілому ситуація досить таки рівномірна,
в межах нульових гіпотез. Тому, виходить, що ситуація +/- стабільна і в цілому
клієнти компанії ведуть себе досить однаково незалежно від півріччя.

