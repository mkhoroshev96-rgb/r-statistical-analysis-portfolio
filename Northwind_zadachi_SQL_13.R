Варіант 2
WITH cte as(SELECT order_id
            ,order_date
            ,shipped_date
            ,freight
            ,quantity
            ,discount
            ,unit_price
            FROM orders
            JOIN order_details USING(order_id)),
agg as(SELECT order_id
       ,ROUND(AVG(shipped_date - order_date)::numeric,0) as delivery_date
       ,ROUND(SUM(quantity * unit_price * (1-discount))::numeric,2) as sum_chek
       ,ROUND(SUM((unit_price * quantity * (1 - discount)) - (unit_price * quantity * 0.6))::numeric,2) as sum_profit
       FROM cte
       GROUp by order_id),
median as(SELECT *
            ,(SELECT percentile_cont(0.5) within group (order by delivery_date) FROM agg)as median_delivery_days
          FROM agg),
del_gr as(SELECT *
            ,case
          when delivery_date >= median_delivery_days THEN 'no_speed_delivery'
          when delivery_date < median_delivery_days THEN 'speed_delivery'
          END as delivery_group
          FROM median),
long as(SELECT order_id
        ,delivery_group
        ,ROUND(AVG(sum_chek) filter(where delivery_group = 'no_speed_delivery')::numeric,2) as avg_chek_no_speed
        ,ROUND(AVG(sum_chek) filter(where delivery_group = 'speed_delivery')::numeric,2) as avg_chek_speed
        ,ROUND(AVG(sum_profit) filter(where delivery_group = 'no_speed_delivery')::numeric,2) as avg_profit_no_speed
        ,ROUND(AVG(sum_profit) filter(where delivery_group = 'speed_delivery')::numeric,2) as avg_profit_speed
        FROM del_gr
        where delivery_group is not null
        GROUP by order_id, delivery_group)
SELECT order_id
,delivery_group
,avg_chek_no_speed as avg_chek
,avg_profit_no_speed as avg_profit
FROM long
where delivery_group = 'no_speed_delivery'
Union ALL
SELECT order_id
,delivery_group
,avg_chek_speed as avg_chek
,avg_profit_speed as avg_profit
FROM long
where delivery_group = 'speed_delivery'

