Задача 90

🚚 Що впливає на ймовірність того, що доставка буде повільнішою, ніж у середньому?
  
  Тип: логістична регресія (GLM + SQL)
SQL-таблиці: orders, order_details, products, shippers

📌 Суть:
  Замість фіксованих 3 днів ми аналізуємо затримки доставки відносно медіанної 
тривалості доставки у всій базі.
Мета — визначити, які фактори підвищують або знижують ймовірність, що доставка 
буде повільнішою за медіану.

✅ Ознака затримки:
Розраховуємо тривалість доставки: shipped_date - order_date
Визначаємо медіану по всіх замовленнях
Створюємо бінарну змінну:
  
1 — якщо доставка > медіани (запізнення)
0 — якщо ≤ медіани (вчасно/швидко)
  
