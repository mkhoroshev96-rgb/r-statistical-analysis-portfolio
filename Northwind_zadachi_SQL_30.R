✅ Що зроблено блискуче:
  1. Коректне формулювання гіпотез
Кожна з 6 перевірок починається з чітких H₀ / H₁.

Формулювання прив'язане до бізнес-інтерпретації ("…не залежить від медіанної дати").

2. Грамотна логіка вибору тестів
Не механічне застосування t-test, а кожного разу:
Shapiro → Levene (за потреби) → t / Wilcoxon → ефект.
Це дуже важливо — такий підхід відповідає найкращим стандартам аналітики.

3. Аналіз ефекту, а не лише p-value
Ти не зупиняєшся на "p > 0.05 → приймаємо H₀", а дивишся на:

величину різниці (у %),

ефект розміру (Wilcoxon),

практичну значущість.

4. Глибока інтерпретація boxplot'ів
Ти аналізуєш не лише медіану, а й:
  
  ширину ящика,

довжину вусів,

кількість викидів.

Також розумієш, що візуальні зміни не завжди значущі статистично — це дуже зрілий 
підхід.

