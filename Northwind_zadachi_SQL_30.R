Задача 139
A/B тест: порівняння структури замовлень у топ-5 найактивніших клієнтів до та 
після медіанної дати

База даних:
  Northwind — таблиці orders, order_details, пов’язані через order_id.

Мета дослідження:
  Перевірити, чи змінилася структура замовлень у топ-5 найактивніших клієнтів 
після медіанної дати кожного з них. Аналіз проводиться окремо для кожного клієнта,
порівнюючи два періоди: до і після медіани дати оформлення замовлень.

Алгоритм:
  
Для кожного замовлення обчислити:
  
avg_price — середня ціна товарів у замовленні;
avg_quantity — середня кількість одиниць товару;
avg_discount — середній розмір знижки;
avg_freight — середня вартість доставки;
avg_delivery_date — середній час доставки (дата відвантаження мінус дата замовлення);
avg_chek — середній чек (сума з урахуванням знижки).

Визначити топ-5 клієнтів за кількістю замовлень (dense_rank() по customer_id).

Для кожного з топ-5 клієнтів поділити замовлення на дві групи за датою оформлення 
(order_date) за допомогою NTILE(2):
  
before_group — замовлення в першій половині;
after_group — замовлення в другій половині.

Одиниця аналізу:
Одне замовлення (order_id).

Подальші дії:
Для кожного клієнта провести порівняльний аналіз між групами before_group і 
after_group за ключовими метриками. Рекомендовано використати:
  
візуалізацію (boxplot),
перевірку нормальності (Shapiro-Wilk),
t-тест або Wilcoxon, залежно від розподілу.

Рішення

WITH cte as(SELECT order_id
            ,customer_id
            ,order_date
            ,shipped_date
            ,ROUND(AVG(unit_price)::numeric,2) as avg_price
            ,ROUND(AVG(quantity)::numeric,2) as avg_quantity
            ,ROUND(AVG(discount)::numeric,2) as avg_discount
            ,ROUND(AVG(freight)::numeric,2) as avg_freight
            ,ROUND(AVG(shipped_date - order_date)::numeric,0) as avg_delivery_date
            ,ROUND(AVG(quantity * unit_price * (1-discount))::numeric,2) as avg_chek
            ,count(*) over(partition by customer_id) as count_orders_customers 
            FROM orders
            JOIN order_details USING(order_id)
            GROUP BY order_id,customer_id),
dense_r as(SELECT *
             ,dense_rank() over(order by count_orders_customers DESC) as dense
           FROM cte),
median as(SELECT *
            ,ntile(2) over(partition by customer_id order by order_date) as median
          FROM dense_r
          where dense <= 5)
SELECT *
  ,case
when median = 1 then 'before_group'
else 'after_group'
END as median_group
FROM median

