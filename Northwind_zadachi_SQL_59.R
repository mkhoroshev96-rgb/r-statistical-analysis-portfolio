Перевірка статистичних припущень
Для кожної метрики застосовано Shapiro-Wilk тест, є візуальний аналіз гістограм.
Всі метрики розподілені не нормально – коректно відмовився від t‑тесту й використав
Wilcoxon/Mann-Whitney.
Вибір тестів
Використання wilcox_test() логічне для двох незалежних груп з ненормальним 
розподілом.
Не проводив перевірку рівності дисперсій (правильно, бо Wilcoxon її не потребує).
Структура аналізу по кожній метриці
Є послідовність: гіпотеза → Shapiro → гістограма → тест → boxplot → середні/медіани
→ бізнес-висновок.
Описані абсолютні та відносні різниці у середніх/медіанах.
Висновки за p-value
Усі p > 0.05 → правильно прийнята H₀: немає статистично значущих відмінностей.
Не було помилкових трактувань p-value.
Фінальна бізнес-інтерпретація
Логічно вказано зв’язок між обсягом продажів → чеками → вартістю доставки.
Є спроба пояснити можливу сезонність/маркетингові причини падіння обсягів.
❌ Що потребує виправлень / покращень
Непослідовність бізнес-логіки по кількості vs знижки
Ти кажеш, що знижки були вищі після медіани, але обсяги продажів зменшилися – це 
дійсно парадокс, але аналіз не копнув глибше.
Можливо, попит нееластичний або сезонність переважила знижки – треба чіткіше 
пояснити, чому маркетингова політика не спрацювала.
Відсутня оцінка розміру ефекту
Для Wilcoxon треба було дати wilcox_effsize() → навіть якщо p>0.05, корисно знати, 
чи є слабкий тренд (r<0.3).
У задачі 201 ти це робив → тут бракує.
Деякі бізнес-висновки повторюються шаблонно
Наприклад, для вартості доставки ти знову пишеш:
  “…бо ці метрики сильно корелюють”
Але тут можна було підкріпити кореляційним аналізом (Spearman).
Мала увага до коефіцієнтних метрик
freight_ratio та daily_freight_per_units описані поверхнево, без додаткових 
інсайтів (наприклад, чи змінилася логістична ефективність).
Можна було додати бізнес-інтерпретацію: логістика стала дорожчою/дешевшою відносно
замовлень?
