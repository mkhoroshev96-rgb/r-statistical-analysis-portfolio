Перевірка нормальності і Шапіро-Вілк
Для великих вибірок Шапіро дуже чутливий → він майже завжди дає «не нормально». 
Це ок, але варто було б додати QQ-плот або перевірити лог-трансформацію метрик 
(особливо для сильно правозміщених).
Іноді праве зміщення можна було нормалізувати log() або sqrt() → це дозволило б 
застосувати параметричний тест для порівняння середніх, а не лише рангових медіан.
Інтерпретація p-value ~0.08 для кількості
Було сказано «пильнувати, бо скоро може стати значущим» — це трохи некоректно. 
Статистично p=0.08 не стає «майже значущим», це просто немає доказів відмінності.
Правильніше: «тенденція, яка може бути цікавою, але не підтверджена статистично».
Логіка доставки й вартості перевезень
Є гіпотеза, що «менше транспорту → дорожче і повільніше».
Але одночасно:
післямедіанний період мав швидшу доставку і дорожчу добову доставку → це виглядає 
як дві різні причини.
Якщо транспорт був у дефіциті в домедіанному, мала б зрости вартість доставки тоді,
а не після.
Потрібно чіткіше розділити гіпотези:
Домедіанний: дефіцит транспорту → повільно, але ще не підняли тарифи.
Післямедіанний: транспорт нормалізували, але ввели новий тариф.
Зараз це змішано.
freight_ratio vs чек
Було сказано, що freight_ratio вище домедіанно, але кількість проданих товарів 
післямедіанно більша → логічно, що при більших чеках частка доставки відносно 
зменшується. Це правильна бізнес-логіка, але пояснення в тексті занадто ускладнене
й трохи заплутане.
Добова вартість доставки
Є твердження «добова вартість зросла, бо підняли тарифи». Це гіпотеза, але не 
доведено. Можливо, зросла через менший обсяг у партії (доставка стала частішою з
меншими замовленнями).
Треба було перевірити кореляцію між daily_freight_per_units і кількістю/чеком.
Висновки про дисконтну політику
Стверджується, що «компанія ввела дисконтну політику», але ж p-value для discount
= 0.46, а медіани = 0%.
Тобто реально знижки не змінились, і ця гіпотеза не підкріплена.
Тут логіка трохи натягнута: зростання кількості могло бути не через знижки, а 
через зростання довіри/попиту.
Ефект розміру
Ефект size лише для quantity. Було б доречно оцінити розмір ефекту для чеків 
(бо там зміна 44% і 56% — навіть без значущості це бізнесово цікаво).
Фінальний висновок занадто довгий і змішує кілька різних причин
В одному абзаці одразу про транспорт, знижки, пізнє спрацювання політики, дефіцит
транспорту, тарифи… → трохи хаотично.
Краще розділити висновки по бізнес-блоках: ціни, обсяги, логістика.
❌ Які помилки або неточності
Манна-Вітні → p-value не означає "дані можуть скоро стати значущими". Це не 
динамічна метрика. Це просто «не значущо».
Суперечність у гіпотезі про дисконти: вони не змінились, але висновок будується 
на припущенні про «грамотну дисконтну політику».
Немає контролю на мультиплікацію тестів: 8 метрик × 1 перевірка = 8 тестів → 
імовірність помилкових висновків збільшується. Можна було додати поправку 
Bonferroni.
Частково змішано причинно-наслідкові зв’язки: транспорт, дисконт, швидкість 
доставки трактуються одночасно без чіткого поділу.
