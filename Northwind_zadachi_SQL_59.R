🎯 Мета дослідження:
Проаналізувати зміни ключових бізнес‑ та логістичних метрик для категорії
Confections (кондитерські вироби) у 1998 році. Замовлення розділені на домедіанний
та післямедіанний періоди за датою оформлення (order_date).

📌 Метрики (8):
unit_price — ціна одиниці товару
quantity — кількість проданих одиниць
discount — знижка
chek — вартість позиції замовлення (з урахуванням знижки)
freight — вартість доставки
freight_ratio — частка витрат на доставку у загальній вартості позиції
delivery_date — час доставки в днях
daily_freight_per_units — добова вартість доставки на одиницю товару

🛠 Алгоритм:
Відібрати всі позиції категорії Confections за 1998 рік.
Розділити їх на 2 групи (ntile(2)): домедіанний і післямедіанний періоди.
Для кожної метрики:
перевірити нормальність (Shapiro-Wilk + гістограма),
застосувати відповідний тест (t-test або Wilcoxon),
побудувати boxplot,
порахувати середні, медіани, абсолютну й відносну різницю,
зробити бізнес‑висновки щодо змін у категорії.

