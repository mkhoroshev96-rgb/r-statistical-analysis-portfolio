Задача 169

Порівняльний аналіз доставок між двома найбільш активними постачальниками
📦 База даних: Northwind
📊 Таблиці: orders, order_details, products, suppliers
🔗 Об’єднання: через order_id, product_id, supplier_id

🎯 Мета дослідження:
Проаналізувати відмінності у структурі доставлених замовлень між двома фірмами-
виробниками з найбільшою кількістю поставок — supplier_id = 12 і supplier_id = 28.

📌 Вибірка:
У вибірку включено всі товарні позиції (одиниці товару) з замовлень, де 
постачальником є один із двох вказаних (supplier_id IN ('12','28')).

🧮 Метрики (на рівні товарної позиції):
unit_price — ціна за одиницю товару
quantity — кількість одиниць товару в замовленні
discount — знижка, застосована до позиції
freight — загальна вартість доставки для всього замовлення
chek — вартість позиції з урахуванням знижки
= unit_price * quantity * (1 - discount)
freight_ratio — співвідношення витрат на доставку до вартості позиції
= freight / chek
delivery_date — тривалість доставки в днях
= shipped_date - order_date
daily_freight_per_unit — вартість доставки однієї одиниці товару на добу
= freight / (delivery_date * quantity)

🧠 Напрямок аналізу:
Порівняй обидві групи (supplier 12 vs 28) за кожною метрикою:
Базові: unit_price, quantity, discount, freight
Похідні: chek, freight_ratio, delivery_date, daily_freight_per_unit

Перевір статистично:
  
Чи є різниця у структурі замовлень між постачальниками?
Чи хтось із них пропонує вигідніші ціни, ефективніше доставляє або надає більші 
знижки?
  
  Методи:
  
Перевірка нормальності (Shapiro-Wilk)
Порівняння середніх (t-тест або тест Манна-Вітні)
Boxplot для кожної метрики

⚠️ Уточнення:
freight — на рівні всього замовлення, а не окремого товару. Це аналітичне припущення,
що дозволяє ділити її на кожну позицію.
Постачальник (supplier) — не той, хто доставляє, а той, хто продав товар.
Якщо хочеш досліджувати логістів, треба аналізувати ship_via.

Рішення

SELECT supplier_id
,company_name
,p.unit_price
,quantity
,discount
,freight
,ROUND((freight/(p.unit_price * quantity *(1-discount)))::numeric,2) as freight_ratio
,ROUND((p.unit_price * quantity *(1-discount))::numeric,2) as chek
,(shipped_date - order_date) as delivery_date
,ROUND((freight / (shipped_date - order_date) / quantity)::numeric,2) as daily_freight_per_unit
FROM orders
JOIN order_details USING(order_id)
JOIN products p USING(product_id)
