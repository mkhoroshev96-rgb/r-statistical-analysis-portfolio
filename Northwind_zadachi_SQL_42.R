📊 Метрики для аналізу:
unit_price — ціна одиниці товару
quantity — кількість одиниць товару в позиції
discount	Знижка, що застосована
freight — витрати на доставку (на рівні замовлення)
freight_ratio — частка доставки у сумарній вартості товарної позиції
chek — підсумкова сума товарної позиції з урахуванням знижки
delivery_date — кількість днів від оформлення до доставки (shipped_date - order_date)

🧪 Методи аналізу:
Shapiro-Wilk test — перевірка нормальності розподілу по кожній метриці
Levene’s test — перевірка гомогенності дисперсій (якщо дані нормальні)
t-test або Mann–Whitney U test — залежно від результатів перевірок
Bootstrapping — додатковий метод оцінки довірчих інтервалів, якщо потрібно

📐 Оцінка ефекту:
Cohen’s d — для параметричних тестів
Rank-biserial correlation — для непараметричних
Boxplot — візуальне порівняння кожної метрики

🔎 Дослідницькі питання:
Чи існують значущі відмінності у середній вартості, знижках, кількості, доставці
