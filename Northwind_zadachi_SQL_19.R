Задача 107

Порівняльний аналіз купівельної поведінки у 1996 та 1997 роках

📦 Контекст:
Компанія хоче проаналізувати ключові відмінності у купівельній поведінці клієнтів
між 1996 та 1997 роками. Це дозволить виявити зміни у бізнес-метриках на ранніх 
етапах роботи, оцінити динаміку, адаптацію клієнтів до нового сервісу, а також 
вплив зовнішніх чинників на структуру замовлень і доходів.

🎯 Ціль:
Для кожного з двох років — 1996 та 1997 — порівняти такі показники:

Середню ціну товару
Середню знижку
Середню кількість товарів у замовленні
Середній чек (unit_price * quantity)
Середній прибуток (unit_price * quantity * (1 - discount - 0.4))
Середню вартість доставки (orders.freight)
Середню швидкість доставки (shipped_date - order_date)

📌 Методологія:
  
Витягнути рік з orders.order_date (через EXTRACT(YEAR FROM order_date))
Об’єднати таблиці: orders, order_details, products, customers
Відібрати лише замовлення з 1996 та 1997 років
Згрупувати дані по кожному замовленню
Провести t-тести для кожного з показників між роками
Побудувати boxplot-и для кожної метрики
Інтерпретувати результати з точки зору бізнесу (наприклад, адаптація сервісу, 
зміни цінової політики, зміни в логістиці)

Рішення
А тепер проаналізуємо інші роки. Думаю також буде цікаво побачити, як змінилася
картина.

WITH cte as(SELECT order_id
            ,order_date
            ,shipped_date
            ,unit_price
            ,quantity
            ,discount
            ,freight
            FROM order_details
            Join orders Using(order_id)
            Join customers Using(customer_id)),
agg as(SELECT order_id
       ,EXTRACT(year from order_date) as year
       ,ROUND(AVG(shipped_date - order_date)::numeric,0) as delivery_date
       ,ROUND(AVG(unit_price)::numeric,2) as avg_price
       ,ROUND(SUM(quantity)::numeric,2) as sum_quantity
       ,ROUND(AVG(discount)::numeric,4) as avg_discount
       ,ROUND(AVG(freight)::numeric,2) as avg_freight
       ,ROUND(SUM(quantity * unit_price * (1-discount))::numeric,2) as sum_chek
       ,ROUND(sum((unit_price * quantity * (1 - discount)) - (unit_price * quantity * 0.6))::numeric,2) as sum_profit
       FROM cte
       GROUP by order_id,EXTRACT(year from order_date)),
ca_we as(SELECT *
           ,case
         when year = 1996 Then '1996_year'
         when year = 1997 Then '1997_year'
         END as year_group
         FROM agg)
SELECT *
  FROM ca_we
where year_group is not null

