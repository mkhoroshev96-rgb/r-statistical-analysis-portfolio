Оцінка від чатіка

Як тренувальний кейс для портфоліо під офер дата-аналітика — він виглядає впевнено
і показує багато того, що хоче бачити роботодавець.
Що добре
Чітка структура аналізу
Є послідовність: гіпотеза → перевірка нормальності → вибір тесту → розрахунок → 
візуалізація → опис результатів → бізнес-висновок.
Це демонструє, що ти володієш повним циклом перевірки гіпотез.
Повнота метрик
Враховано ціни, кількість, знижки, чеки, логістичні показники та похідні коефіцієнти
— це набір, що показує різнобічність аналітики.
Є формули, а значить, ти розумієш, звідки беруться показники.
Правильний вибір тестів
Використання Манна–Вітні при ненормальному розподілі без спроби “протиснути” 
t-тест — показник методологічної грамотності.
Використання розміру ефекту
Це величезний плюс. Багато хто з початківців обмежується тільки p-value. Тут же 
видно, що ти розумієш різницю між статистичною та практичною значущістю.
Бізнес-інтерпретація
Є переклад статистичних результатів у бізнес-мову (“не впливає суттєво”, 
“стабільні характеристики”).
Що можна покращити
Стислість
Є багато повторів фраз (“дані розподілені не нормально → не можемо використовувати
t-тест → Манна–Вітні”). Це можна прописати один раз у вступі, а далі скорочувати.
Це зробить текст більш динамічним і легшим для читання.
Підсумкова таблиця результатів
Зібрати всі метрики, p-value, ефект, відносні різниці у одній таблиці. Це дуже 
підвищує презентабельність кейсу та економить час читача.
Візуалізації
Замість окремого boxplot для кожної метрики можна зробити кілька комбінованих 
графіків (наприклад, фасетинг у ggplot2). Це виглядатиме більш професійно.
Якісні акценти
Можна додати блок “методологія” на початку (у 3–5 пунктах), щоб рекрутер одразу
розумів, як ти будеш підходити до аналізу в реальній роботі.
