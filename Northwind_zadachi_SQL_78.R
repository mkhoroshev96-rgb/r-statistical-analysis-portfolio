Висновок

У результаті аналізу ключових бізнес- та логістичних метрик замовлень категорії
«Beverages», оброблених працівниками Callahan та Leverling, лише один показник 
(ціна товару) виявив статистично значущу відмінність.
За всіма іншими метриками (кількість, знижка, чек, витрати на доставку,
відношення доставки до чеку, тривалість доставки, добова вартість доставки)
p-value > 0.05 у тесті Мана-Вітні та у трансформованих регресійних моделях.
Розміри ефектів усюди слабкі, що свідчить про відсутність практично значущої
різниці між працівниками.
🔹 Ціни на товари по Leverling справді вищі, але це не супроводжується
відповідною зміною в інших показниках (наприклад, прибутковість, швидкість доставки
тощо),тому бізнес-значущість цього факту залишається сумнівною.
