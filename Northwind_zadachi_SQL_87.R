🔗 Зв’язки: orders↔order_details↔products↔categories↔suppliers
🎯 Вибірка: category_name = Dairy Products; постачальники з країн France та Norway.
🧩 Одиниця аналізу: товарна позиція (рядок замовлення).

📌 Метрики (на рівні позиції):
unit_price — ціна товару;
quantity — кількість;
discount — знижка (0–1);
chek = unit_price × quantity × (1 − discount) — вартість позиції із знижкою;
freight_ratio = freight / chek — частка доставки у чеку;
delivery_date = shipped_date − order_date (днів) — тривалість доставки;
daily_freight_per_unit = freight / (delivery_date × quantity) — добові витрати 
доставки на одиницю.
🔍 Завдання: провести порівняльний аналіз зазначених метрик між постачальниками 
з France та Norway для категорії Dairy Products (виявити наявність/відсутність 
статистично значущих відмінностей і їхню практичну інтерпретацію).

Рішення

SELECT category_name
,s.country
,p.unit_price
,quantity
,discount
,ROUND((p.unit_price * quantity * (1-discount))::numeric,2) as chek
,ROUND((freight/(p.unit_price * quantity * (1-discount)))::numeric,4) as freight_ratio
,(shipped_date - order_date) as delivery_date
,ROUND((freight/(shipped_date - order_date)/quantity)::numeric,4) as daily_freight_per_unit
FROM orders
JOIn order_details USING(order_id)
JOIN products p USING(product_id)
JOIN categories USING(category_id)
JOIN suppliers s USING(supplier_id)
WHERE category_name = 'Dairy Products' AND s.country IN ('France', 'Norway')

