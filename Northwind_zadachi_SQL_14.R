WITH cte AS(SELECT ship_country      
            ,order_id 
            ,order_date
            ,shipped_date
            ,customer_id
            ,quantity
            ,unit_price
            ,discount
            ,freight
            FROM orders
            JOIN order_details USING(order_id)
            JOIN customers USING(customer_id)),
agg AS(SELECT ship_country      
       ,order_id 
       ,ROUND(AVG(shipped_date - order_date)::numeric,2) as delivery_date
       ,ROUND(sum(quantity)::numeric,2) as sum_quantity
       ,ROUND(AVG(unit_price)::numeric,2) as avg_price
       ,ROUND(AVG(discount)::numeric,4) as avg_discount
       ,ROUND(AVG(freight)::numeric,2) as avg_freight
       ,ROUND(sum(quantity * unit_price * (1-discount))::numeric,2) as sum_chek
       ,ROUND(sum((unit_price * quantity * (1 - discount)) - (unit_price * quantity * 0.6))::numeric,2) as sum_profit
       FROM cte
       GROUP BY ship_country,order_id),
agg_per_country as(SELECT ship_country
                   ,ROUND(AVG(delivery_date)::numeric,2) as avg_delivery_date_per_country
                   ,ROUND(AVG(sum_quantity)::numeric,2) as avg_quantity_per_country
                   ,ROUND(AVG(avg_price)::numeric,2) as avg_price_per_country
                   ,ROUND(AVG(avg_discount)::numeric,4) as avg_discount_per_country
                   ,ROUND(avg(avg_freight)::numeric,2) as avg_freight_per_country
                   ,ROUND(avg(sum_chek)::numeric,2) as avg_chek_per_country
                   ,ROUND(AVG(sum_profit)::numeric,2) as avg_pofit_per_country
                   FROM agg
                   GROUP BY ship_country
                   Order by avg_freight_per_country),
nti as(SELECT *
         ,ntile(3) over(order by avg_freight_per_country) as tercile_freight
       FROM agg_per_country)
SELECT *
  ,case
when tercile_freight = 1 then 'low_freight'
when tercile_freight = 2 then 'medium_freight'
when tercile_freight = 3 then 'high_freight'
END as freight_group
FROM nti 

