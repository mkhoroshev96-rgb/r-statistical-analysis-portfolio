WITH cte as(SELECT order_id 
            ,customer_id
            ,quantity
            ,unit_price
            ,discount
            ,freight
            FROM orders
            JOIN order_details USING(order_id)
            JOIN customers USING(customer_id)),
agg as(SELECT customer_id
       ,ROUND(SUM(quantity)::numeric,2) as sum_quantity
       ,ROUND(AVG(unit_price)::numeric,2) as avg_price
       ,ROUND(AVG(discount)::numeric,4) as avg_discount
       ,ROUND(AVG(freight)::numeric,2) as avg_freight
       ,ROUND(sum(quantity * unit_price * (1-discount))::numeric,2) as sum_chek
       ,ROUND(SUM((unit_price * quantity * (1 - discount)) - (unit_price * quantity * 0.6))::numeric,2) as avg_profit
       FROm cte
       GROUP BY customer_id
       ORDER BY customer_id),
vol_gr as(SELECT *
            ,NTILE(3) OVER (ORDER BY sum_quantity) AS volume_group
          FROm agg)
SELECT *
  ,case 
when volume_group = 1 THEN 'low_volume'
when volume_group = 2 THEN 'medium_volume'
when volume_group = 3 THEN 'high_volume'
END as gradation
FROM vol_gr

