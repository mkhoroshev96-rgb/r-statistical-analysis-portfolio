Задача 146

Порівняльний аналіз ефективності трьох найактивніших працівників за основними 
бізнес-метриками

База даних:
  Northwind — таблиці orders, order_details, employees, з’єднані через order_id 
та employee_id.

Мета дослідження:
  Перевірити, чи існують статистично значущі відмінності між трьома працівниками,
які обробили найбільшу кількість замовлень, за ключовими метриками замовлень.

Алгоритм відбору:
  
Для кожного замовлення (order_id) розраховано:
  
avg_price — середня ціна одиниці товару;
avg_quantity — середня кількість товару;
avg_discount — середній відсоток знижки;
avg_freight — середня вартість доставки;
median_order_value — медіанна вартість позицій у замовленні;
avg_chek — середній чек;
avg_delivery_date — середня швидкість доставки (у днях).

Для кожного employee_id визначено кількість замовлень: count_orders_per_employee.
Побудовано рейтинг працівників за кількістю оброблених замовлень — DENSE_RANK().
Відібрано лише топ-3 працівників (ранги 1, 2 та 3) та класифіковано їх як:
  
First_employer — найбільше замовлень;
Second_employer — друге місце;
Third_employer — третє місце.

Одиниця аналізу:
Одне замовлення (order_id), яке було оброблене одним з трьох найактивніших працівників.

Ціль аналізу:
Порівняти три групи працівників (First_employer, Second_employer, Third_employer)
за всіма вищевказаними метриками з метою виявлення потенційних відмінностей в 
ефективності їхньої роботи.

📊 Типи статистичного тесту:
Перед тестуванням необхідно провести Shapiro-Wilk тест на нормальність для кожної
метрики в розрізі трьох працівників.

Якщо всі три групи мають нормальний розподіл і гомогенність дисперсій (перевірено
через Levene-тест) — використовуємо ANOVA.

Якщо хоча б одна умова не виконується — використовуємо тест Крускала-Уолліса 
(Kruskal-Wallis test).

