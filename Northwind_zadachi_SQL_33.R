Порівняльний аналіз поведінки клієнтів із Канади та Швеції

База даних:
Northwind — таблиці orders та order_details, пов’язані через order_id.

Мета дослідження:
Визначити, чи існують статистично значущі відмінності між клієнтами з Канади та 
Швеції за ключовими метриками, що описують структуру замовлень.

Одиниця аналізу:
Одне замовлення (order_id).

Метрики для аналізу (розраховуються як середні по кожному замовленню):
  
avg_price — середня ціна одиниці товару;
avg_quantity — середня кількість товарних одиниць;
avg_discount — середній відсоток знижки;
avg_freight — середня вартість доставки;
median_order_value — медіанна вартість товарних позицій (з урахуванням знижки);
avg_chek — середній чек;
avg_delivery_dat — середній час доставки (у днях).

Ціль аналізу:
Порівняти поведінку клієнтів з Канади та Швеції за всіма метриками, виявити 
статистично значущі відмінності, побудувати boxplot’и та визначити розмір ефекту.

Методологія:
Для кожної метрики провести перевірку нормальності розподілу (Shapiro-Wilk);
Якщо обидві групи мають нормальний розподіл і гомогенність дисперсій (Levene test)
— застосувати t-test;
Якщо ні — застосовується тест Манна-Вітні (Wilcoxon Rank-Sum Test);
Оцінити розмір ефекту (наприклад, wilcox_effsize).

