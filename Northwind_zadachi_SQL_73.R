Одиниця аналізу: товарна позиція (order_details).
Вибірка: замовлення лише категорії "Dairy Products", оброблені працівниками Davolio
та Peacock(фільтр: category_name = 'Dairy Products' AND last_name IN ('Davolio',
'Peacock')).
Метрики та формули:
unit_price — ціна товару;
quantity — кількість одиниць;
discount — знижка (0–1);
chek = unit_price * quantity * (1 - discount) — вартість позиції з урахуванням 
знижки;
freight — вартість доставки (на рівні замовлення, без пропорційного розподілу);
freight_ratio = freight / chek — частка доставки у чеку позиції;
delivery_date = shipped_date - order_date (у днях);
daily_freight_per_unit = freight / delivery_date / quantity — добові витрати на 
доставку на одиницю товару.
Якість даних (виключення):
Відсутні дати order_date або shipped_date;
delivery_date <= 0;
quantity <= 0;
chek <= 0 (для уникнення ділення на нуль).
Мета дослідження:
Порівняти наведені метрики між Davolio та Peacock, визначити, чи існують статистично
та практично значущі відмінності.
Завдання:
Описати вибірку: кількість позицій у групах, базові статистики (mean, median, IQR).
Перевірити нормальність розподілу (Shapiro–Wilk) та гомогенність дисперсій 
(Levene, де доречно).
Для кожної метрики обрати тест:
параметричний t-тест (або Welch) при виконанні припущень;
інакше — тест Манна–Вітні.
Розрахувати для кожної метрики p-value та розмір ефекту (Cohen’s d або rank-biserial r).
Побудувати boxplot по кожній метриці (дві групи), за потреби — гістограми.
Зробити бізнес-висновки: чи є відмінності суттєвими та які можливі наслідки для 
логістики й цінової політики.
