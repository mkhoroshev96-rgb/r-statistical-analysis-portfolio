📌 Метрики для аналізу (на рівні товарної позиції):
unit_price — ціна товару;
quantity — кількість одиниць товару;
discount — розмір знижки;
chek — вартість позиції з урахуванням знижки;
freight — вартість доставки;
freight_ratio — частка доставки у чеку;
delivery_date — тривалість доставки (у днях);
daily_freight_per_unit — щоденні витрати на доставку на одиницю товару.

Рішення

SELECT category_name
,last_name
,p.unit_price
,quantity
,discount
,ROUND((p.unit_price * quantity * (1 - discount))::numeric,2) as chek
,freight
,ROUND((freight/(p.unit_price * quantity * (1 - discount)))::numeric,4) as freight_ratio
,(shipped_date - order_date) as delivery_date
,ROUND((freight/(shipped_date - order_date)/quantity)::numeric,4) as daily_freight_per_unit
FROM orders 
JOIN order_details USING (order_id)
JOIN products p USING (product_id)
JOIN categories USING (category_id)
JOIN employees USING (employee_id)
WHERE category_name = 'Dairy Products' AND last_name IN ('Peacock', 'Leverling')

