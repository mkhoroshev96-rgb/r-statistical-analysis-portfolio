Задача 104

Глибокий аналіз змін купівельної поведінки по продуктах до і після медіанної дати

📦 Контекст:
  Бізнес хоче зрозуміти, як змінилася поведінка клієнтів щодо кожного продукту з
часом. Орієнтир — медіанна дата всіх замовлень. Потрібно порівняти ключові метрики 
до та після цієї дати по кожному продукту.

🎯 Ціль:
Для кожного продукту обчислити:
  
Середню ціну товару
Середню знижку
Середню кількість товарів у замовленні
Середній прибуток (розрахунок: unit_price * quantity * (1 - discount))
Середній чек (сума по всьому замовленню, але на одного продукту — для спрощення 
можна брати як unit_price * quantity)
Середню вартість доставки (з таблиці orders.freight, розподілену пропорційно до товару)
Усе це — до і після медіанної дати замовлення, окремо для кожного продукту.

Рішення
Нарешті, маємо задачу трохи іншого плану. Досить таки цікава, і ми вважаємо, що
різниця все ж таке буде в вимірах ключових показників

with cte as(SELECT distinct order_date      
            ,product_id
            ,product_name
            ,shipped_date
            ,p.unit_price
            ,quantity
            ,discount
            ,freight
            ,ROUND((p.unit_price * quantity * (1 - discount))::numeric,2) as chek
            ,ROUND(((p.unit_price * quantity * (1 - discount)) - (p.unit_price * quantity * 0.6))::numeric,2) as profit
            FROM products p
            Join order_details Using(product_id)
            Join orders Using(order_id)),
wit_runk as(SELECT *
              ,shipped_date - order_date as delivery_date
            ,row_number() over(partition by product_id order by order_date) as within_runk
            ,COUNT(*) OVER (PARTITION BY product_id) AS total_count
            FROM cte),
final as(SELECT *
           ,case
         when within_runk <= total_count/2 Then 'before'
         else 'after'
         END as median_group
         FROM wit_runk)
SELECT product_id
,product_name
,unit_price
,quantity
,discount
,freight
,chek
,profit
,delivery_date
,median_group
FROM final

