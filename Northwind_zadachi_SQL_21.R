Задача 112

📦 Контекст:
  У компанії працює кілька менеджерів з продажу (робітників), які обробляють 
замовлення. Кожен із них відповідає за частину клієнтів та виконує замовлення 
через систему. Компанія хоче зрозуміти, чи впливає фактор конкретного працівника
на ключові метрики: час доставки, обсяг замовлення, знижки, суми чеків і прибутки.

🎯 Мета:
  Перевірити, чи є статистично значуща різниця між працівниками компанії за 
наступними метриками:
  
delivery_date — час між оформленням і відвантаженням замовлення (в днях)
unit_price — середня ціна проданих товарів
quantity — кількість проданих одиниць у замовленні
discount — середній розмір знижки
freight — витрати на доставку
chek — сумарна вартість замовлення
profit — прибуток компанії з одного замовлення

🧪 Гіпотези для кожної метрики:
  
нульова гіпотеза: Розподіли значень метрики не залежать від працівника (last_name).
альтернативна гіпотеза: Хоча б в одного працівника значення метрики відрізняється.

Рішення

SELECT last_name
,order_date
,shipped_date
,unit_price
,quantity
,discount
,freight
,ROUND((shipped_date - order_date)::numeric,0) as delivery_date
,ROUND((quantity * unit_price * (1-discount))::numeric,2) as chek
,ROUND(((unit_price * quantity * (1 - discount)) - (unit_price * quantity * 0.6))::numeric,2) as profit
FROM orders
JOIN order_details USING(order_id)
Join employees Using(employee_id)

