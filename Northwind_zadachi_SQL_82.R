🧩 Одиниця аналізу: товарна позиція (рядок замовлення)

📌 Метрики (на рівні позиції):
unit_price — ціна товару;
quantity — кількість одиниць;
discount — знижка (0–1);
chek = unit_price * quantity * (1 − discount) — вартість позиції із знижкою;
freight — вартість доставки (з рівня замовлення, розподілена на позицію як у вибірці);
freight_ratio = freight / chek — частка доставки в чеку;
delivery_date = shipped_date − order_date (днів) — тривалість доставки;
daily_freight_per_unit = freight / (delivery_date * quantity) — добові витрати на 
доставку одиниці товару.
🧼 Якість даних: виключити рядки з NULL у shipped_date або order_date, delivery_date
≤ 0, quantity ≤ 0.
🛠 Завдання:
Порівняти показники між працівниками Fuller та Leverling для категорії Beverages:
перевірити нормальність (Shapiro–Wilk, QQ) та гомоскедастичність 
(Levene/Brown–Forsythe);
обрати відповідний тест (t-test/Welch або Mann–Whitney);
навести p-value, 95% CI, розміри ефектів (Cohen’s d / Hedges’ g або Cliff’s delta);
за потреби застосувати трансформації (Box–Cox, Yeo–Johnson) і повторити ключові 
тести;
провести робастний сенситивіті-аналіз (вінзоризація / trimmed means 10–20%);
додатково дослідити взаємозв’язок discount ↔ quantity окремо по працівниках та 
перевірити різницю схилів через модель із взаємодією.

