Рішення

SELECT category_name
,last_name
,p.unit_price
,quantity
,discount
,(p.unit_price * quantity * (1-discount)) as chek
,freight
,(freight/(p.unit_price * quantity * (1-discount))) as freight_ratio
,(shipped_date - order_date) as delivery_date
,(freight/(shipped_date - order_date)/quantity) as daily_freight_per_unit
FROM orders 
JOIN order_details USING(order_id)
JOIN products USING (product_id)
JOIN categories USING(category_id)
JOIN employees USING(employee_id)
WHERE category_name = 'Beverages' AND last_name IN ('Fuller', 'Leverling')

