Італії, а саме на 74,06%. Що стосується показника р, то він дорівнює 0,01477, що
значить, що є статистично значуща різниця між даними країнами по цій метриці

shapiro.test(residuals(m8))

Показник р становить 0,08011, що значить, що наші залишки нормально розподілені
після трансформації даних. Перейдемо до візуальної оцінки розподілу залишків на
лінійній моделі

qqnorm(residuals(m8)); qqline(residuals(m8))

В цілому, залишки нормально розподілені в центрі лінійної моделі, проте в хвостах
ми бачимо доволі помітні відхилення , які не є суттєвими та в кінцевому рахунку
не ламають нашої нормальності. Перейдемо до перевірки гіпотези рівності дисперсій.

levene_test(daily_bc~country, data = d_207)

показник р становить 0,949, що вказує нам на те, що наші дисперсії є рівними.
Тобто і параметричний і непараметричний тест вказують нам на один і той же ключовий
висновок, а тому і обидва тести є рівнозначними між собою по цій метриці як і
результати по ним

ВИсновки

Загальна картина. За більшістю метрик статистично значущих відмінностей не 
виявлено:
unit_price, quantity, discount, freight, freight_ratio, delivery_date — U-тести 
p > 0.05; ефекти слабкі (Cliff’s δ ≈ 0…0.14), практичної ваги не мають.

Де різниці є (і як їх читати):
Чек позиції (chek) — вищий у Italy: після Box–Cox та з урахуванням нерівності 
дисперсій (Welch t-test) p ≈ 0.047; back-transform показує ~+34–35% до середнього
чеку (≈ 380$ vs 282$). Ефект-сайз за δ ≈ 0.14 (малий) і чутливий до методу 
(U-тест p ≈ 0.087) → інтерпретувати обережно.
Добова вартість доставки на одиницю (daily_freight_per_unit) — вища у Italy: 
  U-тест p ≈ 0.015; модель із Box–Cox підтверджує результат (p ≈ 0.0148). Різниця
за середніми ~+70–75%; δ ≈ 0.20 (малий).

Бізнес-інтерпретація. Постачання з Italy дає (ймовірно) вищий середній чек, але 
разом із вищими добовими логістичними витратами на одиницю. Решта показників між
країнами суттєво не відрізняються.
Надійність/зауваги. Результати по chek залежать від методу (нерівні дисперсії, 
помірні відхилення від нормальності); ефекти загалом малі. Варто підтвердити 
робастністю (BH/FDR для множинних тестів, стратифікація за product_id, урахування
кластерів order_id).

