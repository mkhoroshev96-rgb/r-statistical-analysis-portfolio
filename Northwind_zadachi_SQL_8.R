WITH cte AS(SELECT customer_id
            ,count(*) as orders_count
            ,ROUND(sum(unit_price * quantity *  (1-discount))::numeric,2) as total_revenue
            ,ROUND((sum(unit_price * quantity *  (1-discount))/count(*))::numeric,2) as avg_order_value
            FROM orders
            Join order_details USING(order_id)
            GROUP by customer_id
            Order by avg_order_value DESC),

final as(SELECT customer_id
         ,orders_count
         ,total_revenue
         ,avg_order_value
         ,case
         when avg_order_value >=1000 and orders_count >= 50 THEN 'A+_class'
         when avg_order_value >=1000 and orders_count < 50 THEN 'A_class'
         when avg_order_value >=700 and orders_count >= 40 THEN 'B+_class'
         when avg_order_value >=700 and orders_count < 40 THEN 'B_class'
         when avg_order_value >=400 and orders_count >= 30 THEN 'C+_class'
         when avg_order_value >=400 and orders_count < 30 THEN 'C_class'
         when avg_order_value < 400 and orders_count >= 20 THEN 'D+_class'
         when avg_order_value < 400 and orders_count < 20 THEN 'D_class'
         end as status
         FROM cte)
SELECT status
,count(*)
FROM final
GROUP by status
Order by status

