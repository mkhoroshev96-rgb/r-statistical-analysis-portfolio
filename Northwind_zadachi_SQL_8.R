Чи замовляють дорожчі товари швидше?
  
  💭 Ідея:
Можливо, чим дорожчий товар — тим менше часу минає між замовленням і відвантаженням,
бо компанія обробляє такі замовлення швидше. Або навпаки — вони обробляються повільніше,
бо потребують перевірки, пакування тощо.

🧪 Гіпотеза (кореляційний аналіз):
H₀: Ціна товару не повязана з часом доставки
H₁: Є залежність між ціною товару та часом доставки

Рішення
SQL:
  
SELECT order_id
,ROUND(AVG(shipped_date - order_date)::numeric,2) as delivery_date
,ROUND(AVG(unit_price)::numeric,2) as avg_price
FROm orders
JOIN order_details USING (order_id)
WHERE shipped_date is not null
GROUP by order_id

