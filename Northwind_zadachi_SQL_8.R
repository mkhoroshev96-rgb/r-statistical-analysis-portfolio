Моє розслідування

SELECT ship_country
,ROUND(AVG(freight)::numeric,2) as avg_freight
,ROUND(AVG(unit_price * quantity * (1-discount))::numeric,2) as avg_total_revenue
,ROUND((AVG(freight) / AVG(unit_price * quantity * (1-discount)))::numeric,2) as freight_to_profit
FROm orders
JOIN order_details USING (order_id)
GROUP BY ship_country
Order by freight_to_profit DESC

boxplot(freight_to_profit~ship_country, data = d_19)

Отже відсортувавши дані та візуально переглянувши все + боксплот, дійсно є країни 
в котрих freight_to_profit відрізняється від всіх інших. На мою думку цими країнами
є Австрія та Аргентина. У них цей коефіцієнт становить по 0,22. Але характер
цієї взаємодії різний. Бо якщо Австрія має середній чек один з найбільших, але і
затрати на перевезення до неї найбільші серед всіх країн, то Аргентина навпаки
має досить низький середній чек та порівняно з Австрією низьку середню вартість
доставки. Ось от так буває. Ще хотів би відзначити Швецію та США, у них данний
показник становить 0,19. Але тут трохи інший звязок, ніж в попередніх країн. 
Середній чек у них досить таки пристойний, але затрати на перевезення у них одні
з найбільших після Австрії. Далі у нас йдуть країни з досить посереднім показником 
freight_to_profit, від 0,14 до 0,17. А от найнижчий цей показник, він становить 
0,13 аж у 7 країн. І характер тут не однозначний. Якщо в Бельгії, Швейцарії, Данії
та Канади, країн що  характеризуються досить суттєвим середнім чеком, але в свою
чергу мають досить таки високу середню вартість доставки, то ось Іспанія та Італія
мають низький чек та одні з найнижчих середніх вартостей доставки. Окремо відзначу
Польщу, там найнижчий чек та найнижча вартість доставки. Ось так, здавалося
країни різні за характером замовлень та середнім чеком, але коефіцієнт однаковий.
