того, чи клієнт належить до групи low_median_count чи high_median_count. 

Альтернативна гіпотеза: середньої швидкості доставки куплених товарів залежить від
того, чи клієнт належить до групи low_median_count чи high_median_count. 

d_117$avg_delivery_date <- as.numeric(as.character(d_117$avg_delivery_date))

Перейдемо до перевірки гіпотези нормальності розподілу даних за Шапіро тестом

d_117 %>% group_by(median_group) %>% shapiro_test(avg_delivery_date)

Шапіро тест вказує на те, що наші дані розподілені не нормально, тому надалі 
т-тест ми використовувати не можемо. З цього випливає, що подальша перевірка
гіпотези рівності дисперсій не має сенсу - надалі використовуємо тест Мана-Вітні

wilcox_test(avg_delivery_date~median_group, data = d_117)

Показник р становить 0,361, що говорить нам про те, що ми приймаємо нашу нульову
гіпотезу - середньої швидкості доставки куплених товарів не залежить від того, чи
клієнт належить до групи low_median_count чи high_median_count. Перейдемо до 
оцінки розміру ефекту.

wilcox_effsize(avg_delivery_date~median_group, data = d_117)

Коефіцієнт становить 0,0322, що говорить нам про дуже дуже слабкий розмір ефекту.
Тобто, всього-навсього 3,22% варіацій днів доставки пояснюються різницями між
групами. Тобто, в прикладному плані дана метрика є абсолютно не значущою для
бізнесу

Отже, в ході нашого аналізу двох груп low_median_count та high_median_count ми 
виявили, що з 7 ключових показників статистично значуща різниця присутня в 5 з них.
Це середня кількість куплених товарів, середні знижки, середня вартість перевезення,
медіанне значення замовлення та середні чеки. Тобто, від того, до якої групи належить
клієнт залежать зазначені вище показники. З цього випливає, що якщо кількість
здійснених замовлень становить вищі значення ніж, медіанні, то й в цього клієнта
будуть вищі всі вище зазначені показники. ОТже, від медіанної кількості залежить
дуже багато. На нашу думку, це фурор. В прикладному сенсі з 5 вище зазначених 
показників в 3 показників середній розмір ефекту і в двох слабкий. Тобто, досить
таки важливе прикладне значення для бізнесу мають данні показники в цілому.

