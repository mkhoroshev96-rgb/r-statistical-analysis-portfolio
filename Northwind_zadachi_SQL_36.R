WITH cte as(SELECT order_id
            ,customer_id
            ,company_name
            ,ROUND(AVG(unit_price)::numeric,2) as avg_price
            ,ROUND(AVG(quantity)::numeric,2) as avg_quantity
            ,ROUND(AVG(discount)::numeric,2) as avg_discount
            ,ROUND(AVG(freight)::numeric,2) as avg_freight
            ,ROUND(PERCENTILE_CONT(0.5) WITHIN GROUP (ORDER BY quantity * unit_price * (1 - discount))::numeric,2) AS median_order_value
            ,ROUND(AVG(quantity * unit_price * (1-discount))::numeric,2) as avg_chek
            ,ROUND(AVG(shipped_date - order_date)::numeric,0) as avg_delivery_date
            ,count(*) over (partition by customer_id) as count_order_per_customer
            FROM orders
            JOIN order_details USING(order_id)
            Join customers USING(customer_id)
            GROUP BY order_id,customer_id,company_name
            order by count_order_per_customer),
median as(SELECT *
            ,(SELECT percentile_cont(0.5) within group (order by count_order_per_customer) FROM cte) as median
          FROM cte)
SELECT *
  ,case
when  count_order_per_customer <= 11 Then 'low_median_count'
when  count_order_per_customer > 11 Then 'high_median_count'
END as median_group
FROM median

