Проаналізувати, чи відбулися зміни у структурі замовлень товарів категорії 
"Seafood" (морепродукти) до та після медіанної дати оформлення замовлення.

🛠 Алгоритм формування вибірки
Вибрані усі товарні позиції з категорії "Seafood".
Для кожної позиції розраховані наступні метрики:
unit_price — ціна одиниці товару;
quantity — кількість одиниць;
discount — розмір знижки;
freight — загальна вартість доставки;
chek — сума замовлення з урахуванням знижки;
freight_ratio — частка доставки у підсумковій сумі;
delivery_date — тривалість доставки (в днях);
daily_freight_per_unit — добова вартість доставки однієї одиниці товару.

Усі позиції розділено на дві групи:
  
Домедіанна група — замовлення до медіанної дати;
Післямедіанна група — замовлення після медіанної дати.

📊 Завдання для аналізу
Для кожної з метрик (unit_price, quantity, discount, freight, chek, freight_ratio,
delivery_date, daily_freight_per_unit) потрібно:
Перевірити нормальність розподілу (тест Шапіро-Вілка).
Вибрати відповідний тест: t-тест або тест Манна-Вітні.
Побудувати boxplot по групах.
Порівняти середні, медіанні значення і % зміну.
Розрахувати розмір ефекту (wilcox_effsize або cohen_d).

