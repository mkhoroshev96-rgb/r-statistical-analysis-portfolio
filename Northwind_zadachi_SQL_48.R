🧩 Алгоритм формування вибірки
Відібрані лише ті товарні позиції, які належать до категорії "Produce".

Для кожної позиції розраховано наступні метрики:
  
unit_price — ціна одиниці товару;
quantity — кількість одиниць;
discount — знижка;
freight — загальна вартість доставки на замовлення;
chek — підсумкова сума з урахуванням знижки;
freight_ratio — частка вартості доставки у складі чеку;
delivery_date — тривалість доставки у днях;
daily_freight_per_unit — добова вартість доставки однієї одиниці товару.

Усі позиції розділено на дві групи залежно від дати замовлення:
  
Домедіанна група — оформлені до медіанної дати;
Післямедіанна група — оформлені після медіанної дати.

