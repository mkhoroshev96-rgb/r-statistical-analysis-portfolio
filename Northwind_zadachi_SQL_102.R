Що зроблено сильно
Повний пайплайн по кожній метриці: Shapiro–Wilk → Levene → вибір тесту (Welch / 
класичний t / Mann–Whitney) + оцінка ефекту (Cohen’s d / Cliff’s δ) і перевірка 
залишків після трансформацій. Це дуже добре.
Трансформації з обґрунтуванням: Box–Cox/YJ/log, з «shift» для discount — доречно.
Для chek лог-нормалізація і подальший Welch — коректно.
Бізнес-інтерпретація: чітко виділено, що реальна й стійка відмінність — у цінах
і чеку, решта — шум; рекомендації по SKU-міксу/ціновій політиці — практичні.

