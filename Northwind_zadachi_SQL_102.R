Дані та вибірка.
База: Northwind. Таблиці: orders, order_details, products, categories, suppliers.
Зв’язки: orders ↔ order_details ↔ products ↔ categories ↔ suppliers.
Вибірка: category_name = 'Confections'; постачальники з Brazil та UK.
Одиниця аналізу: рядок замовлення (товарна позиція).

Метрики (рівень позиції):
unit_price, quantity, discount;
chek = unit_price × quantity × (1 − discount);
freight;
freight_ratio = freight / chek;
delivery_date = shipped_date − order_date (у днях);
daily_freight_per_unit = freight / (delivery_date × quantity).

Завдання.

Порівняти усі метрики між Brazil та UK.
Перевірити припущення:
нормальність у групах — Shapiro–Wilk;
гомогенність дисперсій — Levene.
Вибір тесту по кожній метриці:
якщо нормальність + гомогенність: класичний t-test;
якщо нормальність, але дисперсії різні: Welch t-test;
інакше — Mann–Whitney (Wilcoxon rank-sum).
Трансформації за потреби: Box–Cox / Yeo–Johnson / log (для discount дозволяється 
невеликий shift при нулях). Після трансформацій повторити Shapiro та Levene, 
перевірити залишки моделей.

Розміри ефектів:
для t-тестів — Cohen’s d (з CI);
для непараметричних — Cliff’s δ (з CI) та, за бажанням, оцінка різниці медіан 
Hodges–Lehmann.
Візуалізація (опційно): boxplot/violin по країнах, додати точкові значення/джиттер;
для лог-/BC-масштабу — вказати це на графіку.

