Ти наводиш середні: UK вище (19.5 проти 17.4), але пишеш δ≈+0.75 «на користь
Brazil». Це несумісно водночас. Найімовірніше, справа у порядку рівнів фактора 
в cliffs_delta() (напр., порівняння відбулося як “UK > Brazil”, а інтерпретація 
