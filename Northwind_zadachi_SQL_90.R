δ ≈ 0.55 (великий) → у France чек значно вищий.
Вартість доставки (freight): U-тест p ≈ 0.033; модель після Box–Cox значуща
(p ≈ 0.034); δ ≈ 0.21 (малий) → у France доставка дорожча.

Де різниці немає/вона нестійка:
Знижка (discount): U-тест p ≈ 0.96; моделі не дають значущості → різниці немає.
Відношення доставки до чеку (freight_ratio): U-тест p ≈ 0.22; у моделі Box–Cox
p ≈ 0.034, але за Welch (нерівні дисперсії) p ≈ 0.058 → висновок нестійкий,
радше без різниці; δ ≈ −0.12 (малий).
Дні доставки (delivery_date): U-тест p ≈ 0.66; моделі незначущі → різниці немає;
δ ≈ −0.04 (незначний).
Добова доставка на одиницю (daily_freight_per_unit): U-тест p ≈ 0.30; модель
після Box–Cox p ≈ 0.42 → різниці немає; δ ≈ 0.10 (дуже малий).

Бізнес-інтерпретація.
Для «Dairy Products» постачання з France асоціюється з вищою ціною, більшими
