with cte as(SELECT order_id
            ,order_date
            ,shipped_date
            ,unit_price
            ,quantity
            ,discount
            ,freight
            FROM orders
            Join order_details USING(order_id)),
agg as(SELECT order_id
       ,extract(month from order_date) as month
       ,extract(year from order_date) as year
       ,ROUND(AVG(shipped_date - order_date)::numeric,0) as avg_delivery_date_per_order
       ,ROUND(AVG(unit_price)::numeric,2) as avg_price_per_order
       ,ROUND(SUM(quantity)::numeric,2) as sum_quantity_per_order
       ,ROUND(AVG(discount)::numeric,4) as avg_discount_per_order
       ,ROUND(AVG(freight)::numeric,2) as avg_freight_per_order
       ,ROUND(SUM(quantity * unit_price * (1-discount))::numeric,2) as sum_chek_per_order
       FROM cte
       GROUP by order_id, extract(month from order_date), extract(year from order_date))
SELECT *
  ,case 
when month <= 6 then 'first_half'
when month > 6 then 'second_half'
END as half_group
FROM agg
where year = 1997

