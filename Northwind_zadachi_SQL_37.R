Задача 158

Вплив навантаження працівника на характеристики оброблених ним замовлень
📂 База даних:
Northwind — використовуються таблиці orders, order_details, employees, об’єднані
через order_id та employee_id.

🎯 Мета дослідження:
Перевірити, чи існують статистично значущі відмінності у структурі замовлень між
двома групами працівників:
  
low_median_value — ті, хто обробив кількість замовлень ≤ медіани.
high_median_value — ті, хто обробив кількість замовлень > медіани.

Аналіз проводиться на рівні кожного замовлення (order_id), але з урахуванням 
належності працівника до групи.

🔍 Метрики для аналізу (по кожному order_id):
avg_price — середня ціна товарів у замовленні;
avg_quantity — середня кількість одиниць;
avg_discount — середня знижка;
avg_freight — середня вартість доставки;
median_order_value — медіанна вартість позицій у замовленні (з урахуванням знижки);
avg_chek — середній чек замовлення;
avg_delivery_date — середній час доставки у днях.

Рішення

WITH cte as(SELECT order_id
            ,employee_id
            ,last_name
            ,ROUND(AVG(unit_price)::numeric,2) as avg_price
            ,ROUND(AVG(quantity)::numeric,2) as avg_quantity
            ,ROUND(AVG(discount)::numeric,2) as avg_discount
            ,ROUND(AVG(freight)::numeric,2) as avg_freight
            ,ROUND(PERCENTILE_CONT(0.5) WITHIN GROUP (ORDER BY quantity * unit_price * (1 - discount))::numeric,2) AS median_order_value
            ,ROUND(AVG(quantity * unit_price * (1-discount))::numeric,2) as avg_chek
            ,ROUND(AVG(shipped_date - order_date)::numeric,0) as avg_delivery_date
            ,count(*) over(partition by employee_id) as count_orders_employee
            FROM orders
            JOIN order_details USING(order_id)
            JOIN employees USING(employee_id)
            Group by order_id,employee_id,last_name
            order by count_orders_employee),
median as(SELECT *
            ,(select percentile_cont(0.5) within group(order by count_orders_employee) FROM cte) as median
          FROM cte)
SELECT *
  ,case 
when count_orders_employee <= median Then 'low_median_value'
when count_orders_employee > median Then 'high_median_value'
END as median_group
FROM median

