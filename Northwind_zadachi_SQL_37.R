📂 База даних:
  Northwind — використовуються таблиці orders, order_details, products, suppliers,
об’єднані через order_id та product_id.

🎯 Мета дослідження:
Перевірити, чи існують статистично значущі відмінності у структурі постачань між 
двома групами постачальників:
  
low_median_supply_volume — постачальники, які здійснили менше за медіану кількості
поставок.
high_median_supply_volume — постачальники, які здійснили медіану або більше поставок.

🔍 Одиниця аналізу:
Окрема товарна позиція в замовленні (тобто один рядок з order_details), що має 
конкретного supplier_id.

📊 Метрики для аналізу (на рівні рядка поставки):
unit_price — ціна одиниці товару;
quantity — кількість поставленого товару;
discount — знижка на товар;
freight — витрати на доставку;
chek — сума з урахуванням знижки (quantity * unit_price * (1-discount));
delivery_date — час доставки в днях (shipped_date - order_date);
count_unit_suppliers — кількість позицій, поставлених кожним постачальником.

Рішення

WITH cte as(SELECT supplier_id
            ,company_name
            ,p.unit_price
            ,quantity
            ,discount
            ,freight
            ,ROUND((quantity * p.unit_price * (1-discount))::numeric,2) as chek
            ,(shipped_date - order_date) as delivery_date
            , count(*) over(partition by supplier_id) as count_unit_suppliers
            FROM orders
            JOIN order_details USING(order_id)
            JOIN products p USING(product_id)
            Join suppliers USING(supplier_id)
            ORDER BY count_unit_suppliers),
median as(SELECT *
            ,(SELECT percentile_cont(0.5) within group (order by count_unit_suppliers) from cte) as median 
          FROM cte)
SELECT *
  ,case 
when count_unit_suppliers < median THEN 'low_median_supply_volume'
when count_unit_suppliers >= median THEN 'high_median_supply_volume'
end as median_supplirs_group
FROM median

