Задача 95 

Залежність тривалості доставки від дня тижня оформлення замовлення
📦 Контекст:
Бізнес підозрює, що день тижня, коли клієнт оформлює замовлення, може впливати
на швидкість доставки.Наприклад, замовлення, оформлені у пятницю або на вихідних,
обробляються повільніше через графік роботи складів і перевізників.

🎯 Мета:
Для кожного замовлення порахувати:
delivery_time = shipped_date - order_date (кількість днів до відправки).
Для кожного замовлення визначити:
день тижня оформлення (order_date) → Monday, Tuesday, ..., Sunday.
Побудувати вибірку: delivery_time, day_of_week.
Провести ANOVA:
Чи залежить середній час доставки від дня тижня оформлення замовлення?
Якщо ANOVA покаже значущість — провести Tukey HSD.
Побудувати boxplot для візуалізації розподілу delivery_time по днях тижня.

🔥 Що буде особливо цікавого:
Це абсолютно нова площина аналізу — календарно-процесуальна поведінка.
Ти зможеш побачити, наприклад:
Чи справді замовлення в п’ятницю доставляються повільніше?
Чи немає різниці між замовленнями, оформленими в понеділок чи середу?

Рішення

Отже, чатік підігнав дуже цікаву задачу: як змінюються ключові показники в залежності
від дня тижня. Отож, проведемо наше дослідження з допомогою анова-тесту. 

WITH cte as(SELECT order_id
       ,order_date
	   ,shipped_date
	   ,quantity
	   ,unit_price
	   ,discount
	   ,freight
FROm orders
JOIN order_details USING(order_id)),
agg as(SELECT order_id
       ,Extract(dow from order_date) as week_day
	   ,ROUND(AVG(shipped_date - order_date)::numeric,0) as delivery_date
	   ,SUM(quantity) as sum_quantity
	   ,ROUND(AVG(unit_price)::numeric,2) as avg_price
	   ,ROUND(AVG(discount)::numeric,4) as avg_discount
	   ,ROUND(AVG(freight)::numeric,2) as avg_freight
	   ,ROUND(SUM(quantity * unit_price * (1-discount))::numeric,2) as sum_chek
	   ,ROUND(sum((unit_price * quantity * (1 - discount)) - (unit_price * quantity * 0.6))::numeric,2) as sum_profit
FROM cte
GROUP BY order_id, Extract(dow from order_date))
SELECT *
       ,case
	   when week_day = 1 Then 'Montag'
	   when week_day = 2 Then 'Dienstag'
	   when week_day = 3 Then 'Mittwoch'
	   when week_day = 4 Then 'Donnerstag'
	   when week_day = 5 Then 'Freitag'
	   END as week_day_group
FROM agg

