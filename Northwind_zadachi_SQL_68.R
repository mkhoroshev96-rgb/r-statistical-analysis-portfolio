WITH cte as(SELECT product_name
            ,category_name
            ,order_date
            ,p.unit_price
            ,quantity
            ,discount
            ,ROUND((p.unit_price * quantity * (1 - discount))::numeric,2) as chek
            ,freight
            ,ROUND((freight/(p.unit_price * quantity * (1 - discount)))::numeric,4) as freight_ratio
            ,(shipped_date - order_date) asdelivery_date
            ,ROUND((freight/(shipped_date - order_date)/quantity)::numeric,4) as daily_freight_per_unit
            ,count(*) over (partition by product_name) as count_orders
            FROM orders
            JOIN order_details USING(order_id)
            JOIN products p USING(product_id)
            JOIN categories USING(category_id)),
ranks as(SELECT *
           ,dense_rank() over (order by count_orders DESC) as ranks
         FROM cte
         WHERE category_name = 'Produce'),
median as(SELECT *
            ,ntile(2) over (order by order_date) as median
          FROM ranks
          WHERE ranks <= 3)
SELECT *
  ,case when median = 1 THEN 'Домедінний період'
when median = 2 THEN 'Післямедінний період'
END as periods
FROM median

