Таким чином, наш показник р становить 0,231, що вказує нам на те, що наші дисперсії
є рівними. Тобто, ми будемо використовувати т-тест, але без поправки на нерівність
дисперсій. Перейдемо до побудови моделі

m8 <- lm(daily_bc~country, data = d_219)
summary(m8)

Отже, середня Бокс-КОкс добова вартість доставки одиниці товару по Бразилії
становить -1,1205, що в свою чергу в оберненому перетворенні становить
(0.1*(-1.1205)+1)^(1/0.1)=0.305. В той же час, середня Бокс-Кокс різниця в добових
вартостях доставки одиниці товару між Бразилією та Великою Британією дорівнює
0,5906, що в зворотній трансформації становить
(0.1*(-1.1205+0.5906)+1)^(1/0.1)-(0.1*(-1.1205)+1)^(1/0.1)=0.275. Тобто, середня
нормалізована добова вартість доставки одиниці товару по Великій Британії дорівнює
0.305+0.275=0.58. В той же час, в відносних величинах дана середня нормалізована
метрика є вищою по Великій Британії, а саме на 90,16%. Показник р при цьому 
становить 0,06249, що є трохи вищим за 0,05, що свідчить нам про те, що немає
статистично значущої різниці між даними країнами по цій метриці

shapiro.test(residuals(m8))

Показник р становить 0,865, що говорить нам про те, що наші дані розподілені
нормально після трансформації. В такому випадку перейдемо до візуального аналізу
розподілу залишків на лінійній моделі

qqnorm(residuals(m8)); qqline(residuals(m8))

Як ми бачимо на графіку, наші дані розподілені досить таки нормально на даній
лінійній моделі. При цьому ми спостерігаємо деякі відхилення в нижньому хвості.
АЛе так як вони є досить таки не значними та одиничними, вони ніяк не ламають
нашої нормальності. Отже, по даній метриці ми зафіксували досить таки цікаву
картину - непараметричний тест говорить, що різниця між країнами є, а параметричний -
що її немає. Так кого слухати в даному випадку? В даному випадку більш пріорітетним
є параметричний тест, бо він банально є більш точнішим. Тому, виходить, що 
результати параметричного тесту є більш доцільнішими і точнішими по цій метриці

Висновок

Головні результати
Unit price: UK ≫ Brazil (ціни в межах країн константні; δ≈−1 → 100% пар “UK вище”). 
Це не шум, а різні прайси/SKU.
Quantity: різниці немає (Wilcoxon p≈0.43; після лог-трансформації t-test p≈0.49;
ефект дуже малий).
Discount: UK вище (p≈0.025; δ≈−0.24 → малий ефект). На практику впливає помірно.
Chek (рядковий чек): UK ≫ Brazil (Wilcoxon p≪0.001; δ≈−0.86 — дуже великий, 
стабільний; лог-модель підтверджує ~4.4×).
Freight (абсолют): UK > Brazil (Wilcoxon p≈0.016; Box–Cox t-test p≈0.011; ефект
малий, але значущий).
Freight ratio (частка в чеку): Brazil > UK (Wilcoxon p≈0.019; лог-модель p≈0.019;
ефект малий, але стійкий).
Delivery days: різниці немає (Wilcoxon p≈0.39; лог-t p≈0.73; ефект дуже малий).
Daily freight per unit: розбіжність на межі (Wilcoxon p≈0.043 vs t-test p≈0.062)
→ трактуємо як нема надійної різниці.

Бізнес-інтерпретація
UK формує значно вищі чеки не за рахунок “більше штук”, а через суттєво вищі
прайси/SKU-мікс (і трохи вищі знижки).
Абсолютний freight в UK вищий, але в частці від чеку логістика важча в Brazil
(високий freight_ratio через низькі чеки).
Терміни доставки подібні, тож сервісна різниця не пояснює розрив у виручці.

Що робити
Brazil (підняти чек, зменшити частку логістики):
– переглянути портфель: додати/пушити дорожчі SKU, більші фасування, бандли;
– MOQ/консолідація відправок, умови free-shipping для більших чеків;
– таргетовані промо на апсейл (а не лише загальні знижки).

UK (оптимізувати маржу на високих чеках):
– логістика: переговори з перевізниками/консолідація, контроль упаковки/ваги;
– промо: знижки вищі — перевірити ROI, не “розмивати” маржу там, де попит і так
високий.

Зауваги до статистики (щоб закріпити висновки)
Константні ціни в кожній країні ламали частину тестів — це ринковий факт, а не
артефакт.
Для “на межі” метрик (discount, daily) варто зробити FDR-корекцію p-value та
Hodges–Lehmann різницю медіан з CI.
Бажано within-SKU аналіз або моделі з фіксованими ефектами SKU — щоб відокремити
вплив країни від асортименту.

Підсумок: UK виграє за рахунок дорожчих SKU і, як наслідок, вищих чеків; Brazil
страждає на високу відносну вартість логістики. Основні важелі — портфель/апсейл
у Brazil та оптимізація freight і промо в UK.

