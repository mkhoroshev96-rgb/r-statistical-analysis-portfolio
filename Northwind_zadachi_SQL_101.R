Дані та вибірка.
База: Northwind. Таблиці: orders, order_details, products, categories, suppliers.
Зв’язки: orders↔order_details↔products↔categories↔suppliers.
Вибірка: category_name = 'Beverages'; постачальники з Brazil та UK.
Одиниця аналізу: рядок замовлення (товарна позиція).

Метрики (рівень позиції):
  unit_price, quantity, discount;
chek = unit_price × quantity × (1 − discount);
freight;
freight_ratio = freight / chek;
delivery_date = shipped_date − order_date (дні);
daily_freight_per_unit = freight / (delivery_date × quantity).

Завдання.
Порівняти метрики між Brazil та UK.
Перевірити нормальність (Shapiro–Wilk) та гомогенність дисперсій (Levene).
Обрати тест: Welch t-test/класичний t-test (якщо припущення виконуються) або 
Mann–Whitney (інакше).
За потреби застосувати трансформації (Box–Cox / Yeo–Johnson / log) і повторити 
перевірки.
Оцінити розміри ефектів: Cohen’s d (t-тести) або Cliff’s δ (непараметричні).
Візуалізація (опційно): boxplot/violin по країнах.
Результат.
– Зведена таблиця по кожній метриці: середні/медіани (Brazil vs UK), p-value, 
ефект, висновок.
– 2–4 абзаци бізнес-інтерпретації: що саме відрізняється (ціна/знижки/чек/логістика)
і які управлінські дії можливі.

Рішення

SELECT category_name
,s.country
,p.unit_price
,quantity
,discount
,ROUND((p.unit_price * quantity * (1-discount))::numeric,2) as chek
,freight
,ROUND((freight/(p.unit_price * quantity * (1-discount)))::numeric,4) as freight_ratio
,(shipped_date - order_date) as delivery_date
,ROUND((freight/(shipped_date - order_date)/quantity)::numeric,4) as daily_freight_per_unit
FROM orders
JOIN order_details USING(order_id)
JOIN products p USING(product_id)
JOIN categories USING(category_id)
JOIN suppliers s USING(supplier_id)
WHERE category_name = 'Beverages' and s.country IN ('Brazil', 'UK')

