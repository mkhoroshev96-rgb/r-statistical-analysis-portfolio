Отже, оптимальна лямбда дорівнює 0,182. Перейдемо до створення нової змінної

d_225$daily_bc <- ((d_225$daily_freight_per_units^lambda8)-1)/lambda8

Дана нова змінна покликана виправити асиметрію та стабілізувати дисперсію. Чи
так це на справді? На перший пункт нам дасть відповідь Шапіро-тест

shapiro.test(d_225$daily_bc)

Показник р дорівнює 0,8742, що значить, що наші дані розподілені нормально
після трансформації. Перейдемо до другого пункту - про стабілізацію дисперсій.
Тут нам допоможе перевірка гіпотези рівності дисперсій

levene_test(daily_bc~country, data = d_225)

Показник р становить 0,689, що значить, що дисперсії є рівними. Тобто, ми будемо
користуватись т-тестом, але без поправки на нерівність дисперсій. Перейдемо до
побудови моделі

m8 <- lm(daily_bc~country, data = d_225)
summary(m8)

Отже, наша нормалізована середня добова вартість доставки одиниці товару по
Данії дорівнює -0,5273, що в оберненому перетворенні становить 
(0.182*(-0.5273)+1)^(1/0.182)=0.574. В той же час, середня Бокс Кокс різниця в
добових вартостях доставки одиниці товару між данією та США дорівнює -0,3874,
що в свою чергу в зворотній трансформації дорівнює 
(0.182*(-0.5273-0.3874)+1)^(1/0.182)-(0.182*(-0.5273)+1)^(1/0.182)=-0.207. Тобто,
дана середня нормалізована метрика по США становить 0.574-0.207=0,367. В відносних
показниках дана метрика є вищою по данії на 56,04%. Показник р дорівнює 0,1123,
що значить, що немає статистично значущої різниці між цими країнами по цій метриці.
Перейдемо до розрахунку розміру ефекту

cohens_d(daily_bc~country, data = d_225)

Наш коефіцієнт дорівнює 0,31, що значить, що ефект є середнім. При цьому довірчі
інтервали включають в себе 0, що значить що ефект не є стійким. Тобто, дана
нормалізована метрика все ж в кінцевому рахунку не має практичного сенсу. перейдемо
до аналізу нормальності розподілу залишків

shapiro.test(residuals(m8))

Показник р дорівнює 0,8967, що значить - наші залишки розподілені нормально
після трансформації. Тому перейдемо до візуальної оцінки розподілу залишків на
лінійній моделі

qqnorm(residuals(m8)); qqline(residuals(m8))

Залишки розподілені нормально на цій моделі. Відхилення є але досить не суттєві
та одиничні, зосереджені в хвості моделі. Таким чином, вони не ламають нашої 
нормальності. Тобто, обидва теста вказують нам на один і той же ключовий висновок
що значить що результати обох тестів є релевантними по цій метриці

Висновок

У категорії Seafood між постачальниками з USA та Denmark статистично значущі 
відмінності виявлено лише за однією метрикою — delivery_date (тривалість доставки),
де середня кількість днів доставки вища по США (+40,9%, p = 0.018, d = –0.47, 
середній стійкий ефект).
За всіма іншими показниками — unit_price, quantity, discount, chek, freight, 
freight_ratio, daily_freight_per_units — різниці не є статистично значущими 
(p > 0.05), а ефекти мають малий або нестійкий розмір.

Узагальнюючи:
ціни, знижки, обсяги продажів, чеки та доставка (вартісно) не демонструють 
системних відмінностей між країнами;
логістичний аспект (тривалість доставки) суттєво гірший по США, що може бути 
зумовлено більшою географічною віддаленістю або складнішими транспортними 
ланцюгами.
Отже, ключова відмінність між USA і Denmark полягає у швидкості доставки, тоді як
усі інші параметри залишаються стабільними та статистично рівними.

