Одиниця аналізу — рядок замовлення (товарна позиція).

Метрики (рівень позиції):
unit_price — ціна товару;
quantity — кількість;
discount — знижка (0–1);
chek = unit_price × quantity × (1 − discount) — вартість позиції;
freight — вартість доставки (однакова для всіх позицій замовлення);
freight_ratio = freight / chek — частка доставки у чеку позиції;
delivery_date = shipped_date − order_date — тривалість доставки (у днях);
daily_freight_per_units = freight / (delivery_date × quantity) — денна доставка
на одиницю товару.

Завдання
Порівняти всі наведені метрики між постачальниками з USA та Denmark.
Перевірити припущення:
нормальність розподілу в кожній групі (Shapiro–Wilk);
гомогенність дисперсій (Levene).
Вибрати відповідний статистичний тест:
при виконанні припущень — t-тест (Student або Welch);
при порушеннях — Mann–Whitney U test.
Обчислити розмір ефекту (Cohen’s d або Cliff’s delta — залежно від тесту).
Перевірити стійкість результатів за допомогою Box–Cox трансформації для змінних 
із сильними відхиленнями від нормальності.
Побудувати боксплоти для всіх метрик, щоб візуально оцінити відмінності між USA 
і Denmark.
Зробити аналітичний висновок, у якому узагальнити:
які метрики статистично відрізняються між країнами;
наскільки великі ефекти спостерігаються;
чи можна говорити про систематичні відмінності в ціноутворенні, знижках, логістиці
та доставці у категорії Seafood.

Рішення

SELECT category_name
,s.country
,p.unit_price
,quantity
,discount
,ROUND((p.unit_price * quantity * (1-discount))::numeric,2) as chek
,freight
,ROUND((freight/(p.unit_price * quantity * (1-discount)))::numeric,4) as freight_ratio
,(shipped_date - order_date) as delivery_date
,ROUND((freight/(shipped_date - order_date)/quantity)::numeric,4) as daily_freight_per_units
FROM orders
JOIN order_details USING (order_id)
JOIN products p USING (product_id)
JOIN categories USING(category_id)
JOIN suppliers s USING(supplier_id)
WHERE category_name = 'Seafood' AND s.country IN ('Denmark','USA')

