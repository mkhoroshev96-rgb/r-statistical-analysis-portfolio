Задача 82

Чи мають продукти з великим обсягом продажу нижчу середню ціну за одиницю?
Тип: t-test
H₀: середня ціна за одиницю не залежить від того, скільки товару було продано
H₁: продукти з великим обсягом продажу мають іншу середню ціну за одиницю

