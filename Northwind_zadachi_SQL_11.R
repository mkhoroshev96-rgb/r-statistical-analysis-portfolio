Чим більше обсяг товару (в штуках) — тим дешевше він має бути (економія на масштабі)
Але іноді трапляється:
Продукт масовий, але дорогий — бренд, де попит ≠ низька ціна
Або рідкісний і дешевий — просто непопулярний мотлох
