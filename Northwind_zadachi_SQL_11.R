2. Normal Q-Q
Чи нормально розподілені залишки (а це умова класичної лінійної регресії)
Залишки як завжди в центрі розподілені досить таки рівномірно, але по краям у нас
спостерігається відхилення від центральної лінії. Але якщо внизу відхилення
відбувається постійно і все на більшу відстань, то вверху ми бачимо спочатку відхилення
від лінії, а потім поступове повернення до неї. Тобто характер відхилення по краям
досить таки різний.

✅ 3. Scale-Location
(aka Spread-Location)
🔍 Що показує:
Чи рівномірна дисперсія залишків (гомоскедастичність). В нашому випадку ми відразу
спостерігаємо велику дисперсію, але ближче до центру вона трохи збільшується,
а вже ближче до рівня 5-6 по вісі Х вона потроху зменшується. Тому ми можемо 
сказати про суттєву гетероскедастичність.


✅ 4. Residuals vs Leverage
🔍 Що показує:
Хто найбільше впливає на модель (підозрілі точки = потенційні "outliers"). Отже, 
згідно з нашого графіка, ми маємо досить багато точок, які лежать нижче лінії.
Це нормально. Декілька точок підходять до лінії Кука, але не пересікають її.
Тобто умовно викидів у нас немає. Тобто, все в границях норми.

