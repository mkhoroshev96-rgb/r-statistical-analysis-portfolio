WITH cte as(SELECT customer_id
            ,order_date
            ,ROUND(SUM(quantity * p.unit_price * (1 - discount))::numeric,2) as order_total
            ,ROUND(AVG(quantity * p.unit_price * (1 - discount))::numeric,2) as avg_order_total
            FROM orders
            JOIN order_details USING(order_id)
            Join products p USING(product_id)
            GROUP by customer_id,order_date),
prev_order_date as(SELECT *
                     ,LAG(order_date,1) over(partition by customer_id order by order_date) as prev_order_date
                   FROM cte),
between_days AS(SELECT *
                  ,(order_date -  prev_order_date) as between_days
                FROM prev_order_date),
avg_days as(SELECT customer_id
            ,ROUND(AVG(avg_order_total)::numeric,2) as avg_order_total_per_customer
            ,ROUND(AVG(between_days)::numeric,2) as avg_between_days
            FROm between_days
            GROUP by customer_id),
median_chek as(SELECT *
                 ,(SELECT percentile_cont(0.5) within group (order by avg_order_total_per_customer) FROm avg_days) as median_chek_customers
               FROM avg_days)
SELECT *
  ,case
when avg_order_total_per_customer >= median_chek_customers THEn 'high'
when avg_order_total_per_customer < median_chek_customers THEn 'low'
END as check_level
FROm median_chek

