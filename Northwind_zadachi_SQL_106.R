Перевірка припущень
Нормальність у групах: Shapiro–Wilk.
Гомогенність дисперсій: Levene (або Brown–Forsythe на медіані).
Зафіксувати p-значення та висновок для кожної метрики.
Основні тести порівняння (Germany vs Canada)
Якщо нормальність ок і дисперсії рівні → Student’s t-test.
Якщо нормальність ок, але дисперсії не рівні → Welch’s t-test.
Якщо нормальність порушено → Mann–Whitney U (Wilcoxon rank-sum).
Для кожного тесту подати: статистику, p-value, 95% ДІ, розмір ефекту:
для t-тестів: Cohen’s d (або Hedges’ g при невеликих n);
для рангового тесту: Cliff’s delta.
Інтерпретувати напрям і силу ефекту (trivial/small/medium/large).

Трансформації та робастність
Для метрик з вираженою асиметрією протестувати Box–Cox (для додатних) або 
Yeo–Johnson (для значень з нулями/від’ємними).
Повторити п.4 на трансформованих даних; порівняти висновки.
Зробити сенситивіті-аналіз: (а) без трансформацій; (б) з трансформаціями; (в) з 
вінзоризацією. Показати, чи стабільний висновок.

Додаткові перевірки
Для freight_ratio та daily_freight_per_units окремо оцінити вплив delivery_date
як потенційного драйвера через кореляцію (Spearman) у кожній країні; коротко 
прокоментувати, чи може різниця між країнами бути наслідком різної тривалості 
доставки.

Оцінка потужності (power)
Для кожної метрики за підсумковим ефектом (наприклад, Cohen’s d) оцінити пост-hoc
power або потрібний розмір вибірки для виявлення ефекту d = 0.5 при α = 0.05, 
двосторонньо. Зробити короткий висновок про достатність наявних даних.

