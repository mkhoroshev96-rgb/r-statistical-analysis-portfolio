Метрики (рівень позиції)
unit_price — ціна товару;
quantity — кількість;
discount — знижка (0–1);
chek = unit_price × quantity × (1 − discount) — вартість позиції;
freight — вартість доставки (рівень замовлення, однакова для всіх позицій в ордері);
freight_ratio = freight / chek — частка доставки в чеку позиції;
delivery_date = shipped_date − order_date (дні) — тривалість доставки;
daily_freight_per_units = freight / (delivery_date × quantity) — “щоденна” 
доставка на одиницю.
Примітка: у статистичному аналізі відкинути записи з delivery_date ≤ 0 або NULL,
а також з нульовим/від’ємним chek.

Завдання
Підготовка даних
Перевірити пропуски та некоректні значення в кожній метриці; видалити/позначити 
проблемні записи за правилами вище.
