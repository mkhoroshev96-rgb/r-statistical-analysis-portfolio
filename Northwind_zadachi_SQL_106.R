значень; за необхідності зробити вінзоризацію (напр., 1%/99%) і зафіксувати це 
для сенситивіті-аналізу.

Описова статистика
Для кожної метрики по країнах (Germany vs Canada): n, mean, sd, median, IQR, 
min/max, CV%.
Побудувати візуалізації: boxplot + jitter та Q–Q plot для оцінки нормальності; 
