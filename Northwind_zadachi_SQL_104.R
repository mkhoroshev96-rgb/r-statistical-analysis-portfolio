Показник р становить 0,739, що значить, що наші залишки нормально розподілені
після трансформації. Перейдемо до оцінки розподілу залишків на лінійній моделі

qqnorm(residuals(m8));qqline(residuals(m8))

Наші залишки розподілені нормально на цій моделі, але все ж присутні відхилення,
котрі зосереджені в хвостах. Проте так як вони є не суттєвими, то вони не ламають
нашої нормальності. Отже, обидва тести вказують нам один і той же висновок, що 
значить, що вони є рівнозначними по цій метриці

Висновок

Короткий висновок (Brazil vs Canada, Confections)
Ціна (unit_price): константна всередині країн; Канада суттєво дорожче (49.3 vs 
17.4). Ефект максимально великий (Cliff’s δ = −1).
Чек (chek): Канада значно вища (і за median, і за mean). Непараметрично: p ≈ 
2.0e−5; після Box–Cox: t-test p ≈ 5.2e−6; Cohen’s d ≈ −1.02 (великий) → сильний 
практичний ефект.
Частка доставки в чеку (freight_ratio): вища в Бразилії (median і mean). 
Непараметрично: p ≈ 0.0015; після Box–Cox: t-test p ≈ 0.0066; Cohen’s d ≈ 0.58 
(середній) → практично помітно.
Кількість (quantity): різниці нема (MW p ≈ 0.11; після трансформації t-test p ≈ 
0.19; ефект малий).
Знижки (discount): без різниці (MW p ≈ 0.20; ефект дуже малий).
Вартість доставки (freight): без різниці (MW p ≈ 0.75; після трансформації t-test
p ≈ 0.52; ефект дуже малий).
Тривалість доставки (delivery_date): без різниці (MW p ≈ 0.40; log-t-test p ≈ 
0.46; ефект малий).
Добова доставка на одиницю (daily_freight_per_unit): без різниці (MW p ≈ 0.31; 
після трансформації t-test p ≈ 0.44; ефект малий).

Бізнес-інтерпретація
Канада: вищі ціни ⇒ істотно вищий чек (ключова відмінність і головний драйвер
виручки).
Бразилія: вища частка доставки в чеку ⇒ логістика «з’їдає» більше чеку, попри 
схожі абсолютні витрати та строки.

Рекомендації
Для Канади: утримувати прайс-премію (контроль еластичності, цінові тести),
працювати з mix-ом (товари з високим margin).
Для Бразилії: знижувати freight_ratio — консолідація відвантажень, мінімальні 
пороги, перегляд перевізників/маршрутів; стимулювати більший чек (bundles, 
порогові знижки), аби розмити частку доставки.
Загалом: знижки/строки/абсолютний freight — не драйвери різниці; фокус на ціноутворенні
(CA) та логістиці як частці чеку (BR).

