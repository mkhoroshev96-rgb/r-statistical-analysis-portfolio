Дані: Northwind → orders ↔ order_details ↔ products ↔ categories ↔ suppliers.
Вибірка: category_name = 'Confections'; постачальники з Brazil та Canada.
Одиниця аналізу: рядок замовлення (товарна позиція).
Метрики (на рівні позиції):
unit_price, quantity, discount;
chek = unit_price × quantity × (1 − discount);
freight;
freight_ratio = freight / chek;
delivery_date = shipped_date − order_date (дні);
daily_freight_per_unit = freight / (delivery_date × quantity).

Завдання:
Порівняти всі метрики між Brazil та Canada.
Перевірки припущень:
У групах: Shapiro–Wilk (нормальність).
Між групами: Levene (гомогенність дисперсій).
Вибір статистичного тесту:
є нормальність і гомогенність → класичний t-test;
нормальність, але дисперсії різні → Welch t-test;
інакше → Mann–Whitney (Wilcoxon rank-sum).
Трансформації за потреби: Box–Cox / Yeo–Johnson / log
(для discount дозволений малий shift при нулях). Після трансформацій повторити 
перевірки припущень і оцінити залишки моделей.
Ефекти:
для t-тестів — Cohen’s d з CI;
для непараметричних — Cliff’s δ з CI і (за бажанням) Hodges–Lehmann (різниця 
медіан).
Вивід: зведена таблиця
метрика | Canada mean/median | Brazil mean/median | тест | p-value | ефект
короткі бізнес-висновки (ціна/знижки/чек/логістика) та рекомендації для обох країн.
Крайові випадки:
константні unit_price, нульові discount, відсутні/нульові delivery_date — обробити
або виключити з обґрунтуванням.

Рішення

SELECT category_name
,s.country
,p.unit_price
,quantity
,discount
,ROUND((p.unit_price * quantity * (1-discount))::numeric,2) as chek
,freight
,ROUND((freight/(p.unit_price * quantity * (1-discount)))::numeric,4) as freight_ratio
,(shipped_date - order_date) as delivery_date
,ROUND((freight/(shipped_date - order_date)/quantity)::numeric,4) as daily_freight_per_unit
FROM orders
JOIN order_details USING (order_id)
JOIN products p USING (product_id)
JOIN categories USING (category_id)
JOIN suppliers s USING (supplier_id)
WHERE category_name = 'Confections' AND s.country IN ('Brazil','Canada')

