Рішення
WITH cte AS(SELECT distinct order_date
            ,customer_id
            ,order_id
            ,discount
            ,quantity
            FROM orders
            JOIN order_details USING(order_id)
            JOIn customers USING(customer_id)),

agg as(SELECT customer_id
       ,order_date
       ,SUM(quantity) as sum_quantity
       ,ROUND(AVG(discount)::numeric,4) as avg_discount
       ,count(*) as count_order
       FROM cte
       GROUP BY customer_id,order_date
       Order by order_date),
row_nmb as(SELECT *
             ,row_number() over(partition by customer_id order by order_date DESC) as within_runk
           FROM agg),
days as(SELECT *
          ,case
        when within_runk = 1 THEN 'last_day'
        when within_runk > 1 THEN 'before_days'
        END AS days_group
        FROM row_nmb)
SELECT customer_id
,ROUND(AVG(sum_quantity) filter(where days_group = 'before_days')::numeric,2) as avg_quantity_before_last
,ROUND(AVG(avg_discount) filter(where days_group = 'before_days')::numeric,4) AS avg_discount_before_last
,ROUND(AVG(sum_quantity) filter(where days_group = 'last_day')::numeric,2) as avg_quantity_last_day
,ROUND(AVG(avg_discount) filter(where days_group = 'last_day')::numeric,4) AS avg_discount_last_day
FROM days
GROUP by customer_id
Order by customer_id

