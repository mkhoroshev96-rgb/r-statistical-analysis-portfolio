Висновок

У результаті аналізу ключових бізнес- та логістичних метрик замовлень категорії
«Confections», оброблених працівниками Peacock та Leverling, істотних статистично
значущих відмінностей не виявлено.
За більшістю показників (ціна, кількість, знижка, вартість доставки, відношення
доставки до чеку, тривалість доставки, добові витрати на доставку) тести Мана-Вітні
дали p-value > 0.05. Розміри ефектів були слабкими (Cohen’s d / Cliff’s delta у 
межах 0.1–0.18), що свідчить про відсутність прикладної значущості для бізнесу.
Локальні відмінності (вищі середні ціни, чеки та вартість доставки по Leverling, 
а також більші середні знижки по Peacock) підтвердилися лише номінально, але 
статистичної сили вони не мали. Цікаво, що для метрики «чек» після Box–Cox 
трансформації (λ≈0.1) класична модель виявила значущу різницю на користь Leverling
(≈+54% до середнього чеку), тоді як непараметричний тест залишився на межі
значущості (p≈0.051). Це демонструє чутливість результатів до способу обробки
асиметричних даних.
Використання трансформацій (Box–Cox, логарифмування, Yeo–Johnson) у більшості 
випадків покращувало нормальність, але не змінювало ключового висновку:
👉 ефективність обробки замовлень у категорії «Confections» працівниками Leverling 
та Peacock є статистично однаковою, а різниці між ними не мають прикладної ваги для
бізнесу.

