🧩 Одиниця аналізу: товарна позиція (рядок замовлення).
📌 Метрики (на рівні позиції):
unit_price — ціна товару;
quantity — кількість одиниць;
discount — знижка (0–1);
chek = unit_price * quantity * (1 − discount) — вартість позиції із знижкою;
freight — вартість доставки (з рівня замовлення, віднесена до позиції як у вибірці);
freight_ratio = freight / chek — частка доставки в чеку;
delivery_date = shipped_date − order_date (днів) — тривалість доставки;
daily_freight_per_unit = freight / (delivery_date * quantity) — добові витрати 
на доставку на одиницю.
🧼 Якість даних: виключити рядки з shipped_date або order_date = NULL, 
delivery_date ≤ 0, quantity ≤ 0; за потреби відфільтрувати аномальні значення 
(обґрунтувати критерій).

