0.638-0.058=0.58. В відносних величинах дана метрика є вищою по Франції, а саме
на 10%. Що стосується показника р то він становить 0,7652, що говорить нам про
те, що немає статистично значущої різниці між даними країнами по цій метриці

shapiro.test(residuals(m8))

Таким чином, наш показник р становить 0,9138, що значить, що наші залишки розподілені
нормально після трансформації. Перейдемо до візуальної оцінки розподілу залишків
на лінійній моделі

qqnorm(residuals(m8)); qqline(residuals(m8))

Як ми бачимо на графіку, наші залишки розподілені нормально на цій моделі. Що
стосується відхиленнь, то вони присутні, зосереджені в обох хвостах. Але так
як вони є не значними та одиничними - вони ніяк не ламають нашої нормальності.
Тобто, обидва тести вказують нам на один і той же ключовий висновок, а тому вони
є повністю рівнозначними по цій метриці

Висновок

Головні результати (по метриках)

Unit price (ціна за одиницю): різниці немає (Wilcoxon p≈0.31; Cliff’s δ≈−0.11, 
дуже малий, нестійкий). Середнє у FR різко вище через дорогі SKU (mean 127 vs 19),
але медіани майже рівні (18 vs 19) — ефект ск’ю/аутлаєрів, а не системної різниці
цін.
Quantity (кількість): різниці немає (Wilcoxon p≈0.99; після Box–Cox нормальність/
гомо, t-test також p≫0.05; δ≈0, дуже малий).
Discount (знижки): UK вище (Wilcoxon p≈0.041). Ефект малий (δ≈−0.23) і за довірчими
інтервалами нестійкий → статистично значущо, але практично помірно.
Chek (рядковий чек): FR суттєво вище (Wilcoxon p≈0.00095; δ≈0.39 — середній, 
стійкий). Після трансформацій і Welch t-test результат підтверджується (p≈0.0002).
Практично: FR-позиції приносять набагато вищий чек.
Freight (сума доставки): різниці немає (Wilcoxon p≈0.12; після Box–Cox Welch 
t-test p≈0.20; δ≈0.19 — малий, нестійкий). Середні вищі у FR, але статистично не
тримається.
Freight ratio (частка доставки в чеку): UK суттєво вище (Wilcoxon p≈0.0010; δ≈−0.39 
— середній). Після лог-нормалізації результат зберігається (t-model p≈0.0015). 
Практично: логістичне навантаження в чекові вище в UK.
Delivery days (термін доставки): Wilcoxon дає FR довше (p≈0.027; δ≈0.27 — малий,
але значущий), однак після лог-трансформації у t-моделі p≈0.083 (на межі). Отже,
різниця ймовірна, але не надто стабільна.
Daily freight per unit (добова доставка на одиницю): різниці немає (Wilcoxon 
p≈0.81; після Box–Cox p≈0.77; δ≈0.03 — мікроефект).

Інтерпретація
Фінансовий профіль.
FR генерує значно більші чеки на позицію (середній і медіанний розриви великі і 
стійкі), але це не через вищі прайси — базові ціни не відрізняються на рівні 
порядків; ефект іде від кошика/композиції замовлення (SKU mix, кількість позицій
у рядку, комбінація “ціна×кількість×знижка”).
UK частіше/вище дисконтує, але розмір ефекту невеликий — знижки не “ламають” 
картину по чеку проти FR.

Логістика.
Абсолютні витрати на доставку між країнами статистично схожі.
Відносно чеку логістика дорожча в UK (вища частка freight у вартості рядка), 
причому ефект середній і стабільний.
Терміни: у FR доставки ймовірно довші, але доказ слабший у параметричній моделі 
— трактуємо як тенденцію.

Що це означає для бізнесу
FR-портфель/замовлення формують дорогі рядки (високий чек) за рахунок міксу SKU/
композиції; варто:
перевірити структуру SKU (преміум-бренди, форм-фактори, фасування);
оцінити мінімальні/типові розміри рядків, політику бандлів/додаткових позицій.
UK: логістика від’їдає більшу частку від виручки (вищий freight_ratio). Для 
підвищення маржі:
оптимізувати умови відправки (консолідація, вибір перевізників/маршрутів, поріг 
безкоштовної доставки, інкотермс);
протестувати перегляд промо (знижки таргетувати так, щоб не збільшувати логістичне
навантаження на дрібних чеках).
Ціни як такі — не джерело розривів; основні важелі — склад кошика та промо/
логістична політика.

Обмеження та що перевірити додатков
SKU-ефект: узгодити аналіз в межах однакових SKU (within-SKU порівняння чи мікс
-ефекти), щоб відділити країновий вплив від продуктового.
Контроль обсягу: моделі з коваріатами (quantity, вага/об’єм якщо доступні, тип 
сервісу доставки), робастні регресії або GLM.
Стабільність результатів: перехресна перевірка по періодах/клієнтських сегментах;
winsorization або trimmed means для дуже перекошених метрик.
Юніт-економіка: перейти від ratio до внеску в маржу (freight після знижок, ціна-
собівартість).

Підсумок:
Надійно відрізняються: чек (FR вище) та частка доставки в чеку (UK вище).
Ймовірно відрізняються: терміни доставки (FR довше) — але ефект малий/на межі.
Не відрізняються статистично: базові ціни, кількості, абсолютний freight, добова
доставка на одиницю.
Головні важелі — портфель/структура замовлень у FR та ефективність логістики/промо
в UK.

Оцінка від чатіка

Оцінка кейсу (лаконічно)

Що зроблено якісно
🔎 Коректний пайплайн: нормальність (Shapiro), гомоскедастичність (Levene), 
вибір тесту (Welch t / Mann–Whitney), ефекти (Cliff’s δ / Cohen’s d), 
візуалізації.
🔁 Сенситивіті: Box–Cox / log із повторною перевіркою припущень — це плюс.
📌 Інтерпретації по суті:
  – chek вищий у FR (ефект середній, стабільний) — ключовий результат.
– freight_ratio вищий у UK (ефект середній) — важлива логістична знахідка.
– ціна/quantity/freight/daily — без різниць → правильно не «перегріваєш» висновки.

Неточності / ризики
Суперечність про довірчі інтервали Cliff’s δ (freight_ratio): ти пишеш, що CI 
включають 0, але робиш висновок «ефект стійкий». Якщо CI містить 0 — стійкість 
не підтверджена. Потрібно узгодити.
Back-transform з Box–Cox/лог: повернення з моделі lm до початкової шкали через 
просту підстановку упереджує оцінку середнього (Jensen’s inequality). Для середніх
краще: (а) порівнювати на трансформованій шкалі; (б) звітувати геометричні/медіанні;
(в) застосувати bias-correction/Дельта-метод.
Shapiro «занадто чутливий через великий n»: слушна ремарка загалом, але в Northwind
зазвичай n невеликі. Краще обґрунтувати нормальність графіками (QQ/гістограми) +
робастні тести, ніж списувати на «велике n».
Перевірка нормальності місцями несиметрична: інколи тестувався весь масив після 
трансформації, інколи — по групах. Правильно — по групах для кожної метрики 
перед вибором параметричного тесту.
Множинні порівняння (8 метрик): p-values варто коригувати (Benjamini–Hochberg).
Після FDR-корекції «на межі» ефекти (discount p≈0.041; delivery days p≈0.027 / 
0.083) можуть втратити значущість.
Складні ratio-метрики: freight_ratio та daily_freight_per_unit мають важкі 
хвости/нульові дні доставки. Ти почистив NA, але краще додатково: winsorization
або моделі з лог-зв’язком/робастні мірки.
Причинність «мікс SKU»: висновок про склад кошика коректний інтуїтивно, але його
треба підтвердити within-SKU порівнянням чи моделлю з фіксованими ефектами SKU.

Вердикт по статистиці
Сильні, надійні різниці: chek (FR > UK); ймовірно freight_ratio (UK > FR) — за 
умови, що CI для δ не містять 0 після виправлення/корекції на множинність.
Тенденція: delivery_date (FR довше) — слабша й може зникнути після FDR.
Без різниць: unit_price, quantity, freight, daily_freight_per_unit.
Що варто додати (по пріоритету)
FDR-корекція для всіх p-values (Benjamini–Hochberg) + оновити висновки по 
«прикордонних» метриках.
Ефекти й медіани: для непараметричних результатів додай Hodges–Lehmann різницю 
медіан + CI; поруч — common language effect size (перекладний для бізнесу).
Within-SKU аналіз: або парні порівняння в межах тих самих продуктів, або lm/GLM 
з фіксованими ефектами SKU (і, за можливості, контролем за quantity).
Робастність: winsorization 1–5% для chek, freight_ratio та перевірка, що висновки
зберігаються.
Моделі для ratio: GLM з лог-зв’язком чи гамма-сімейством для freight_ratio, або 
лог-лінейна модель із вагами.
Бізнес-підсумок (залишити як є)
FR: великі чеки не через базову ціну, а через композицію позицій/кошика. → 
Перевірити SKU-мікс, фасування, бандли.
UK: логістична частка у чеку вища. → Консолідація відправок/перевізники/поріг 
free shipping, перегляд промо на дрібних кошиках.
Ціни, кількість, абсолютний freight — не драйвери розриву.
Загальна оцінка: 9/10 за метод і структурність; до 10/10 бракує FDR-корекції, 
узгодженості інтервалів для δ, і підтвердження SKU-міксу робастною моделлю.

