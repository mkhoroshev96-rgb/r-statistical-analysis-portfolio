Дані та вибірка
База: Northwind
Таблиці: orders, order_details, products, categories, suppliers
Зв’язки: orders ↔ order_details ↔ products ↔ categories ↔ suppliers
Вибірка: category_name = 'Beverages'; постачальники з France та UK.
Одиниця аналізу: рядок замовлення (товарна позиція).

Метрики (рівень позиції)
unit_price, quantity, discount
chek = unit_price * quantity * (1 − discount)
freight
freight_ratio = freight / chek
delivery_date = shipped_date − order_date (дні)
daily_freight_per_unit = freight / (delivery_date * quantity)

Завдання
Порівняти наведені метрики між France та UK.
Перевірити нормальність у кожній групі (Shapiro–Wilk) та гомогенність дисперсій 
(Levene).
Обрати тест залежно від припущень:
якщо нормальність/гомогенність виконуються → Welch t-test (або класичний t-test 
за рівних дисперсій);
інакше → Mann–Whitney (Wilcoxon rank-sum).
За потреби трансформувати розподіли (Box–Cox / Yeo–Johnson / log), повторити 
перевірки та тест.
Оцінити розміри ефектів: Cohen’s d (для t-тестів) або Cliff’s δ (для непараметричних).
Візуалізація (опційно): boxplot/violin для кожної метрики з розбиттям за країною.
Коротка бізнес-інтерпретація: що саме відрізняється між France і UK (ціна, знижки,
чек, логістика), чи має різниця практичний сенс.

Результат
Таблиця/зведення по кожній метриці: середні/медіани (France vs UK), p-value, 
розмір ефекту, висновок.
2–4 абзаци бізнес-висновків: які драйвери різниць (ціна/знижки/обсяги/доставка) 
та які управлінські дії можливі.

Рішення
SELECT category_name
,s.country
,p.unit_price
,quantity
,discount
,ROUND((p.unit_price * quantity * (1-discount))::numeric,2) as chek
,freight
,ROUND((freight/(p.unit_price * quantity * (1-discount)))::numeric,4) as freight_ratio
,(shipped_date - order_date) as delivery_date
,ROUND((freight/(shipped_date - order_date)/quantity)::numeric,4) as daily_freight_per_unit
FROM orders
JOIN order_details USING(order_id)
JOIN products p USING (product_id)
JOIN categories USING(category_id)
JOIN suppliers s USING (supplier_id)
WHERE category_name = 'Beverages' AND s.country IN ('France', 'UK')

