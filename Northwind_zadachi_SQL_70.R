Порівняння структури замовлень, оброблених Leverling vs Peacock, для клієнтів з Німеччини
База: Northwind
Таблиці та зв’язки: orders + order_details (через order_id), products 
(через product_id), customers (через customer_id), employees (через employee_id).
Фільтр: customers.country = 'Germany' і employees.last_name IN ('Leverling',
'Peacock').
Одиниця аналізу: рядок позиції замовлення (order line).
Метрики (на рівні позиції):
unit_price, quantity, discount
chek = unit_price * quantity * (1 - discount)
freight (з рівня замовлення)
freight_ratio = freight / chek
delivery_date = shipped_date - order_date (у днях)
daily_freight_per_unit = freight / delivery_date / quantity
Якість даних / відсікання:
Виключити записи з shipped_date IS NULL, order_date IS NULL, delivery_date <= 0,
quantity <= 0, chek <= 0 (щоб уникнути ділення на нуль/некоректних значень).
Завдання:
Порівняти Leverling vs Peacock за кожною метрикою (розподіли + описова статистика).
Перевірити статистичні відмінності між групами по кожній метриці 
(t‑test або Mann–Whitney залежно від припущень), оцінити розмір ефекту.
Додати візуалізації (boxplot/violin + гістограми/ECDF), коротко інтерпретувати
практичну значущість.
Зробити підсумковий бізнес‑висновок: чи відрізняється структура та логістика 
замовлень у двох працівників для німецьких клієнтів і що це може означати для 
операцій.
Рішення
SELECT c.country
,e.last_name
,p.unit_price
,quantity
,discount
,ROUND((p.unit_price * quantity * (1 - discount))::numeric,2) as chek
,freight
,ROUND((freight/(p.unit_price * quantity * (1 - discount)))::numeric,4) as freight_ratio
,(shipped_date - order_date) as delivery_date
,ROUND((freight/(shipped_date - order_date)/quantity)::numeric,4) as daily_freight_per_unit
FROM orders
JOIN order_details USING (order_id)
JOIN products p USING (product_id)
JOIN customers c USING (customer_id)
JOIN employees e USING (employee_id)
WHERE c.country = 'Germany' And e.last_name in ('Leverling', 'Peacock')
