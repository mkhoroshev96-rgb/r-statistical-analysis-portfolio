Проаналізувати динаміку ключових бізнес- та логістичних метрик по топ-3 товарах 
категорії Confections (кондитерські вироби), відібраних за кількістю замовлень. 
Для кожного з трьох лідерів усі замовлення було поділено на дві групи — до та 
після медіанної дати оформлення замовлення.
📌 Метрики для аналізу:
unit_price – ціна одиниці товару
quantity – кількість одиниць у замовленні
discount – розмір знижки
chek – сума замовлення (з урахуванням знижки)
freight – вартість доставки
freight_ratio – частка доставки в сумі замовлення (freight / chek)
delivery_date – тривалість доставки в днях
daily_freight_per_unit – добова логістична вартість доставки одиниці товару
🛠 Методологія:
Замовлення для кожного з трьох найчастіше продаваних товарів категорії Confections
було відсортовано за order_date та поділено на дві рівні групи за допомогою ntile(2) 
— "Домедіанний період" та "Післямедіанний період", які збережено в змінній periods.
