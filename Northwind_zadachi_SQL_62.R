Правильний вибір статистичних тестів
Тест Шапіро-Вілка на нормальність.
Коректно вирішено НЕ використовувати t‑тест через сильне праве зміщення → обрано
тест Мана‑Вітні. Це адекватно для невеликих вибірок і асиметричних розподілів.
Розмір ефекту через wilcox_effsize() – теж плюс, бо p‑value без ефекту не дає 
повної картини.
Коректне трактування p‑value
Де p>0.05 – прийнято нульову гіпотезу.
Де p близьке до 0.05 (чеки, дні доставки) – адекватно зазначено, що варто 
перевірити на більшій вибірці, а не робити категоричних висновків.
Єдиний показник із статистично значущою різницею – знижки, і це логічно 
інтерпретовано.
