🧂 Порівнювані категорії:
Meat/Poultry — м’ясо та птиця
Dairy Products — молочна продукція

📐 Метрики для аналізу (на рівні товарної позиції в замовленні):
unit_price — ціна одиниці товару
quantity — кількість одиниць
discount — знижка
freight — вартість доставки
chek — загальна сума товару зі знижкою
freight_ratio — частка доставки в ціні
delivery_date — тривалість доставки (днів)
daily_freight_per_unit — щоденна доставка за одиницю товару

