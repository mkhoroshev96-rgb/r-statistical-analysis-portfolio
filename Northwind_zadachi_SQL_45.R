Порівняльний аналіз логістичних і цінових характеристик між категоріями 
"Meat/Poultry" та "Seafood"
📦 База даних: Northwind
📊 Таблиці: orders, order_details, products, categories
🔗 Зв’язки: через order_id, product_id, category_id

🎯 Мета дослідження
Проаналізувати, чи існують статистично значущі та прикладно важливі відмінності 
між замовленнями м’яса/птиці та морепродуктів за ключовими логістичними та 
фінансовими метриками.

🥩 Категорії для порівняння:
Meat/Poultry — м’ясо та птиця
Seafood — риба та морепродукти

📐 Метрики для аналізу (на рівні товарної позиції в замовленні):
unit_price — ціна одиниці товару
quantity — кількість одиниць товару
discount — знижка на товар
freight — вартість доставки
chek — сума товарної позиції з урахуванням знижки
freight_ratio — частка вартості доставки у чеку
delivery_date — тривалість доставки (у днях)
daily_freight_per_unit — щоденна доставка за одиницю товару:
(freight / кількість днів / кількість)

