інформації.
Варто виділяти ключові метрики (де p близько 0.05 або бізнес-різниця велика) й
детально розписувати лише їх.
Фінальна бізнес-інтерпретація трохи хаотична
Ти згадав усі фактори (ціни, знижки, логістику), але логіка сценарію вийшла 
заплутаною.
Для портфоліо краще зробити 1–2 чіткі сценарії, а не “треш атракціон” :)
Змішування статистичної та бізнес-значущості
