клієнтів з країн Америки та Європи при покупці товарів з макрокатегорії "Бакалія" 
(спеції та злаки).

Групи країн:
America: Argentina, Venezuela, USA, Mexico, Brazil, Canada
Europa: всі інші країни

Категорії товарів для аналізу:
'Condiments' — спеції, соуси, приправи
'Grains/Cereals' — крупи, зернові продукти

Метрики для кожного рядка замовлення:
delivery_date — тривалість доставки (у днях): shipped_date - order_date;
unit_price — ціна одиниці товару;
quantity — кількість одиниць товару;
discount — знижка;
freight — вартість доставки;
chek — вартість чека з урахуванням знижки:
quantity * unit_price * (1 - discount).

Рішення

WITH cte as(SELECT category_name
            ,ship_country
            ,order_date
            ,shipped_date
            ,p.unit_price
            ,quantity
            ,discount
            ,freight
            ,shipped_date - order_date as delivery_date
            ,ROUND((quantity * p.unit_price * (1-discount))::numeric,2) as chek
            FROM orders
            JOIN order_details USING(order_id)
            Join products p USING(product_id)
            JOIN categories USING(category_id)
            where category_name in ('Condiments', 'Grains/Cereals'))
SELECT *
  ,case
when ship_country in ('Argentina','Venezuela','USA','Mexico', 'Brazil', 'Canada') Then 'America'
else 'Europa'
END as county_group
FROM cte

