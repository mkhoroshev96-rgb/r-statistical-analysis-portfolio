Задача 127

Аналіз поведінки покупців морепродуктів у країнах Америки та Європи
Вам надано базу даних Northwind, що містить інформацію про замовлення (orders), 
деталі замовлень (order_details), товари (products) та категорії товарів (categories).
Мета: проаналізувати, чи існують статистично значущі відмінності у купівельній 
поведінці клієнтів із країн Америки та Європи, які замовляють товари категорії 
"Seafood" (морепродукти).

Групи країн для аналізу:
America: Argentina, Venezuela, USA, Mexico, Brazil, Canada
Europa: усі інші країни

Метрики, що аналізуються:
Для кожного рядка замовлення з категорії "Seafood" потрібно обчислити:

тривалість доставки: delivery_date = shipped_date - order_date;
кількість одиниць товару (quantity);
ціну одиниці товару (unit_price);
знижку (discount);
вартість доставки (freight);
суму чека з урахуванням знижки: quantity * unit_price * (1 - discount) → chek.

Рішення

WITH cte as(SELECT category_name
            ,ship_country
            ,order_date
            ,shipped_date
            ,p.unit_price
            ,quantity
            ,discount
            ,freight
            ,shipped_date - order_date as delivery_date
            ,ROUND((quantity * p.unit_price * (1-discount))::numeric,2) as chek
            FROM orders
            JOIN order_details USING(order_id)
            Join products p USING(product_id)
            JOIN categories USING(category_id)
            where category_name = 'Seafood')
SELECT *
  ,case
when ship_country in ('Argentina','Venezuela','USA','Mexico', 'Brazil', 'Canada') Then 'America'
else 'Europa'
END as county_group
FROM cte

