різниця в unit price не транслюється у вищий чек.
Логістика: змінювати умови/перевізників нема підстав — суттєвих відмін немає.
Промо та обсяги: тестувати промо-механіки для Germany, щоб компенсувати вищий
прайс і підтягнути кількість/чек (A/B по рівнях знижок або bundle-стратегії).
Ризики й контроль якості аналізу
Константні ціни в одній зі сторін зірвали Shapiro — зроблено коректний перехід
на непараметрику; трансформації (Box–Cox/Yeo–Johnson/log) застосовано та 
перевірено.
Продуктовий мікс: країна постачальника може бути змішана з різними SKU у межах 
Confections. Це маскує «чистий» ефект країни.

Bottom line: на ваших даних премія ціни в Germany — реальна і статистично 
підтверджена, але не конвертується у виграш по чеку або логістиці. Для економного 
портфеля Confections логічно зміщувати закупівлі в Brazil, залишаючи Germany під 
чітко обґрунтовані “преміальні” позиції або нефінансові цінності.

