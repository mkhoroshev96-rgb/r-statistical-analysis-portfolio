дорівнює (0.182*(-0.7184-0.1083)+1)^(1/0.182)-(0.182*(-0.7184)+1)^(1/0.182)= -0.055.
Таким чином,дана середня нормалізована метрика по Німеччині становить 0.463-0.055=
0.408. В відносних показниках цей середній нормалізований показник є вищим по
Бразилії на 13,48%. Показник р становить 0,6746, що говорить про те, що немає
статистично значущої різниці між цими країнами по даній метриці. Перейдемо до
розрахунку ефекту

cohens_d(daily_bc~country, data = d_221)

Коефіцієнт становить 0,09 - наш розмір ефект є малим. Довірчі інтервали включають
в себе 0, що значить, що розмір ефекту є не стійким. Тобто, дана нормалізована
метрика між даними країнами не має практичного сенсу

