Висновок

Єдина стабільно значуща різниця — ціна за одиницю: у Німеччини вища (Mann–Whitney
p≈0.00038; Cliff’s δ ≈ −0.39 — середній ефект). Усі інші метрики (quantity, 
discount, чек, freight, freight_ratio, delivery_date, daily_freight_per_unit) 
статистично не відрізняються між країнами (p>0.05; ефекти малі/нестійкі). Тобто
логістика та “вартість доставки відносно чеку” не дають переваги жодній стороні,
а підвищена ціна в Німеччини не конвертується у значуще вищий чек.

Коротко по метриках
Unit price — вище у Germany; різниця значуща, ефект середній (δ≈−0.39).
Quantity — різниці нема (непарам. p≈0.216; після Box–Cox нормалізації теж ns; 
d≈0.19, CI перетинає 0).
Discount — різниці нема (непарам. p≈0.286; δ≈0.11 — дуже малий, нестійкий).
Chek — різниці нема (парам. на BC-шкалі: Welch t ns; d≈−0.28, CI перетинає 0).
Freight (доставка) — різниці нема (непарам. p≈0.74; після BC — t ns; d≈0.03).
Freight_ratio — тенденція до більшого в Brazil (середнє/медіана вищі), але ns 
(непарам. p≈0.085; після BC — t ns; d≈0.26).
Delivery_date — трохи довше в Germany (~+17.6% на лог-шкалі), ns (p≈0.27; d≈−0.23).
Daily_freight_per_unit — ns до і після трансформацій (p≈0.85 / 0.67; d≈0.09).

Бізнес-інтерпретація
Німеччина дорожча по прайсу, але ні чек, ні доставка, ні швидкість не стали 
кращими — отже, премія в ціні не підкріплена логістичними/економічними вигодами
на рівні позиції.
Бразилія має нижчу ціну і не гірші логістичні показники → краще для цінових SKU 
або промо-асортименту.
Вищий freight_ratio у Brazil (медіанно) — лише тенденція, без статистичної 
підтвердженості; у практичному сенсі різниця невелика.

Рекомендації
Сорсинг: для категорії Confections віддавати пріоритет Brazil для SKU, де важлива
ціна/еластичний попит. Germany залишити під «преміальні» SKU лише за наявності 
нефінансових аргументів (якість/бренд/умови постачання), бо самі метрики цього
