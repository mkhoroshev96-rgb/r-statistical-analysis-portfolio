База: Northwind
Таблиці: orders, order_details, products, categories, suppliers
Зв’язки: orders↔order_details за order_id; order_details↔products за product_id;
products↔categories за category_id; products↔suppliers за supplier_id.

Вибірка: category_name = 'Beverages'; постачальники з країн Brazil та France.
Одиниця аналізу: товарна позиція (рядок замовлення).
Метрики (на рівні позиції):
unit_price — ціна товару;
quantity — кількість;
discount — знижка (0–1);
chek = unit_price * quantity * (1 − discount) — вартість позиції із знижкою;
freight — вартість доставки (з рівня замовлення);
freight_ratio = freight / chek — частка доставки у чеку;
delivery_date = shipped_date − order_date (дні) — тривалість доставки;
daily_freight_per_unit = freight / (delivery_date * quantity) — добові витрати 
доставки на одиницю.

Завдання:
Провести порівняльний аналіз вказаних метрик між Brazil та France: описова 
статистика (mean, median, IQR), графіки розподілів (box/violin/ECDF).
Перевірити гіпотези різниці між країнами: обрати параметричні або непараметричні
тести залежно від нормальності/гомоскедастичності; надати p-value та довірчі
інтервали; застосувати корекцію за множинні порівняння (Holm/BH).
Подати розміри ефектів (для непараметричних — Cliff’s delta або Common Language
Effect Size; для лог-порівнянь — ratio of geometric means).
Робастність: для перекошених метрик застосувати лог/Box–Cox трансформації; 
перевірити залишки моделей; за потреби використати Welch t-test/робастні SE.
Рівень даних: метрики з рівня замовлення (freight, delivery_date, та похідні) 
проаналізувати на рівні order_id (уникнути дублювання позиціями) або агрегувати 
до замовлення перед тестами.
Контроль продуктового міксу: або обмежити аналіз товарами, що присутні в обох 
країнах, або оцінити модель з fixed effects по product_id (ефект країни при 
фіксованому продукті), з інтерпретацією в бізнес-термінах.
Сформулювати бізнес-висновки: де різниці суттєві для ціноутворення/маржинальності
/логістики, а де — ні; надати практичні рекомендації для закупівель та ціноутворення
в категорії Beverages.

Рішення

SELECT category_name
,s.country
,p.unit_price
,quantity
,discount
,ROUND((p.unit_price * quantity * (1-discount))::numeric,2) as chek
,freight
,ROUND((freight/(p.unit_price * quantity * (1-discount)))::numeric,4) as freight_ratio
,(shipped_date - order_date) as delivery_date
,ROUND((freight/(shipped_date - order_date)/quantity)::numeric,4) as daily_freight_per_unit
FROm orders
JOIn order_details USING(order_id)
JOIN products p USING(product_id)
JOIN categories USING(category_id)
JOIN suppliers s USING(supplier_id)
WHERE category_name = 'Beverages' AND country in ('Brazil', 'France')

