Вплив націнки на прибутковість замовлення (t-тест)
📦 Контекст:
Бізнес хоче перевірити таку гіпотезу:
"Замовлення з високою базовою націнкою товарів приносять компанії більше прибутку,
ніж замовлення з низькою націнкою."

Підозра бізнесу:
  
Якщо товари продаються з високою націнкою (приблизно 40% і більше), то й загальний
прибуток по замовленню в середньому більший.
Якщо націнка невелика (до 40%), замовлення менш вигідні.

🎯 Мета:
Для кожного замовлення обчислити:
markup = середня націнка товару в замовленні.
sum_profit = сумарний прибуток із замовлення.

Розбити замовлення на дві групи:
Low markup (markup < 0.4)
High markup (markup ≥ 0.4)

Рішення
Досить таки цікаву задачу сформулював чатік. Цікаво проаналізувати метрики, згідно
такого розбиття груп

WITH cte AS(SELECT order_id
            ,order_date
            ,shipped_date
            ,unit_price
            ,quantity
            ,discount
            ,freight
            FROM orders
            Join order_details Using(order_id)),
agg AS(SELECT order_id
       ,ROUND(AVG(((unit_price * (1 - discount)) - (unit_price * 0.6)) / (unit_price * 0.6))::numeric,4) AS markup
       ,ROUND(AVG(shipped_date - order_date)::numeric,0) as delivery_date
       ,ROUND(AVG(unit_price)::numeric,2) as avg_price
       ,ROUND(SUM(quantity)::numeric,2) as sum_quantity
       ,ROUND(AVG(discount)::numeric,4) as avg_discount
       ,ROUND(AVG(freight)::numeric,2) as avg_freight
       ,ROUND(SUM(quantity * unit_price * (1-discount))::numeric,2) as sum_chek
       ,ROUND(sum((unit_price * quantity * (1 - discount)) - (unit_price * quantity * 0.6))::numeric,2) as sum_profit
       FROM cte
       GROUP BY order_id),
med_mark AS(SELECT *
              ,(SELECT percentile_cont(0.5) within group (order by markup) FROM agg) as median_markup
            FROM agg)
SELECT *
  ,case
when markup >= median_markup Then 'High_markup'
when markup < median_markup Then 'Low_markup'
END as markup_group
FROM med_mark

