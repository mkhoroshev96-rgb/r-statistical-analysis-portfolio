в основному від 30 до 40. Тобто саме основна маса зіграла роль, бо по середнім 
показникам різниця незначна і на користь напоїв - вони в середньому дорожчі. Чеки
це похідний показник, в якому велику роль зіграли ціни. Тому й медіанні чеки в
цілому вищі по молочці, ніж по напоям по причині того, що основна маса чеків
на товари даної категорії(молочки) були в границях 400-500 доларів в той час як
по напоям від 15 до 15000. Якраз середні чеки трохи вищі по напоям - специфіка
розрахунку між методами медіанні та середні. В прикладному ж плані розмір ефекту
є середнім по цінам, а по чекам слабким. Проте, є ще один прикордонний показник -
швидкість доставки куплених товарів. Р = 0,0545, що говорить що в майбутньому між
даними групами буде статистично значуща різниця. Проте наразі він є не статистично
значущим, а в прикладжному плані має слабкий розмір ефекту, що є незначушим
показником для бізнесу.

