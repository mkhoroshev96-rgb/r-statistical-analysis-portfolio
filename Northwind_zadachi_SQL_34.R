Задача 149

Порівняльний аналіз ефективності двох працівників із найменшою кількістю оброблених
замовлень

База даних:
Northwind — використовуються таблиці orders, order_details та employees, об’єднані
через order_id та employee_id.

Мета дослідження:
Перевірити, чи існують статистично значущі відмінності у структурі замовлень між
двома працівниками, які обробили найменшу кількість замовлень.

Алгоритм вибірки:
  
За допомогою CTE обчислюється кількість замовлень, оброблених кожним працівником.
Обираються двоє працівників з найменшою кількістю оброблених замовлень (DENSE_RANK()).
Для кожного замовлення, яке вони обробили, розраховуються ключові метрики.

Одиниця аналізу:
Одне замовлення (order_id).

Метрики для порівняння (розраховані на рівні одного замовлення):
  
avg_price — середня ціна одиниці товару
avg_quantity — середня кількість товару
avg_discount — середній розмір знижки
avg_freight — середня вартість доставки
median_order_value — медіанна вартість товарних позицій
avg_chek — середній чек (з урахуванням знижки)
avg_delivery_date — середня тривалість доставки (у днях)

Рішення

WITH cte as(SELECT  order_id
            ,employee_id
            ,last_name
            ,ROUND(AVG(unit_price)::numeric,2) as avg_price
            ,ROUND(AVG(quantity)::numeric,2) as avg_quantity
            ,ROUND(AVG(discount)::numeric,2) as avg_discount
            ,ROUND(AVG(freight)::numeric,2) as avg_freight
            ,ROUND(PERCENTILE_CONT(0.5) WITHIN GROUP (ORDER BY quantity * unit_price * (1 - discount))::numeric,2) AS median_order_value
            ,ROUND(AVG(quantity * unit_price * (1-discount))::numeric,2) as avg_chek
            ,ROUND(AVG(shipped_date - order_date)::numeric,0) as avg_delivery_date
            ,count(*) over (partition by employee_id) as count_order_per_employee
            FROM orders
            JOIN order_details USING(order_id)
            JOIN employees USING(employee_id)
            GROUP BY order_id,employee_id,last_name
            ORDER BY count_order_per_employee),
wit_ran as(SELECT *
             ,dense_rank () over(order by count_order_per_employee) as within_rank
           FROM cte)
SELECT *
  FROM wit_ran
where within_rank <=2

