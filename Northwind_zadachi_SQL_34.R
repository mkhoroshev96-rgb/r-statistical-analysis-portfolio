📊 Метрики для порівняння (по кожному придбанню):
unit_price — ціна одиниці товару;
quantity — кількість придбаного товару;
discount — знижка на товар;
freight — доставка (загальна на рівні замовлення, але аналізується по товару);
avg_chek — вартість товарної позиції (з урахуванням знижки);
avg_delivery_date — тривалість доставки (в днях), спадок на рівень товару.

