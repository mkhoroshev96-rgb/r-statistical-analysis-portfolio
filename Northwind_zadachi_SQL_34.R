Задача 150

Порівняльний аналіз купівельної поведінки клієнтів із Мексики та Італії

База даних:
Northwind — використовуються таблиці orders та order_details, об’єднані через 
order_id.

Мета дослідження:
Перевірити, чи існують статистично значущі відмінності в структурі замовлень 
між клієнтами з Мексики та Італії.

Одиниця аналізу:
  Одне замовлення (order_id).

Критерій відбору:
До аналізу включаються лише замовлення, доставлені до Mexico або Italy 
(ship_country IN ('Mexico', 'Italy')).

Метрики для порівняння (обчислені на рівні одного замовлення):
  
avg_price — середня ціна одиниці товару;
avg_quantity — середня кількість одиниць товару;
avg_discount — середній розмір знижки;
avg_freight — середня вартість доставки;
median_order_value — медіанна вартість товарних позицій (з урахуванням знижки);
avg_chek — середній чек (сума з урахуванням знижки);
avg_delivery_date — середня тривалість доставки (у днях).

Рішення

SELECT  order_id
,ship_country
,ROUND(AVG(unit_price)::numeric,2) as avg_price
,ROUND(AVG(quantity)::numeric,2) as avg_quantity
,ROUND(AVG(discount)::numeric,2) as avg_discount
,ROUND(AVG(freight)::numeric,2) as avg_freight
,ROUND(PERCENTILE_CONT(0.5) WITHIN GROUP (ORDER BY quantity * unit_price * (1 - discount))::numeric,2) AS median_order_value
,ROUND(AVG(quantity * unit_price * (1-discount))::numeric,2) as avg_chek
,ROUND(AVG(shipped_date - order_date)::numeric,0) as avg_delivery_date
FROM orders
JOIN order_details USING(order_id)
where ship_country in ('Mexico', 'Italy')
GROUP BY order_id,ship_country

