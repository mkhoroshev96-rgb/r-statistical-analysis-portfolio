✅ Сильні сторони роботи:
  
Чітка структура
– кожна метрика розглядається окремо;
– спочатку гіпотези, потім тест на нормальність, далі — вибір тесту, візуалізація,
опис середніх/медіан, розмір ефекту, трансформації.
Це дуже правильний і професійний підхід.
Коректний вибір тестів
– скрізь ти перевіряєш нормальність (Shapiro), і залежно від результатів переходиш 
або до Манна–Вітні, або до t-аналогів після трансформацій.
– перевірка залишків після трансформацій — це взагалі top-level, не всі про це
думають.
Використання трансформацій
– Box-Cox, логарифмування, Yeo-Johnson застосовані правильно й доречно.
– правильно пояснено, що вони не змінили загальний висновок, а лише допомогли 
перевірити стійкість результатів (sensitivity analysis).
Оцінка розміру ефекту (effect size)
– ти не обмежився p-value, а ще й інтерпретував ефект.
– дуже правильний акцент: навіть якщо різниця між середніми виглядає великою 
(наприклад, ціна чи чек), але effect size слабкий → бізнес-сенсу немає.
Візуалізації та опис
– boxplot-и проаналізовані грамотно (медіана, розмах, екстремальні значення).
– це дає більш «живе» пояснення, ніж просто цифри.
Висновок узагальнений правильно
– ти не піддався на спокусу перебільшити, а чітко написав: статистично значущих 
відмінностей нема.
– відзначив, що локальні відмінності у середніх/медіанах не підтверджуються 
статистикою → це дуже важливо.

⚠️ Моменти, де можна докрутити:
  Shapiro-Wilk на великих вибірках
– ти використовуєш його коректно, але пам’ятай: при великому n він майже завжди
відхиляє нормальність (надчутливий).
– добре, що ти дивився ще й на гістограми + QQ-plot → це рятує.
Інтерпретація Box-Cox назад
– у тебе є місця, де обернене перетворення ти робиш «вручну» і воно виглядає 
трохи кострубато (наприклад, при λ = -0.25).
– технічно все правильно, але можна було використати готові функції (наприклад, 
car::bcPower) і уникнути зайвої математики руками.
– це не помилка, а радше питання зручності.
Відсоткові різниці
– ти часто розраховуєш відносні різниці між середніми/медіанами. Це ок, але важливо
пам’ятати: вони виглядають «великими» (наприклад, +150%), хоча статистично не 
підтверджені.
– ти це підкреслюєш («немає практичного значення»), тому тут все чисто, але для 
