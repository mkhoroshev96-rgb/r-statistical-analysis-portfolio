Вибірка: category_name = 'Meat/Poultry', постачальники з Canada та Australia.
Одиниця аналізу: рядок замовлення (товарна позиція).

Метрики (на рівні позиції):
unit_price — ціна товару;
quantity — кількість;
discount — знижка (0–1);
chek = unit_price × quantity × (1 − discount) — вартість позиції зі знижкою;
freight — вартість доставки (рівень замовлення);
freight_ratio = freight / (unit_price × quantity × (1 − discount)) — частка 
доставки в чеку;
delivery_date = shipped_date − order_date (дні) — тривалість доставки;
daily_freight_per_unit = freight / (delivery_date × quantity) — добові витрати 
доставки на одиницю.

Завдання:
Порівняти перелічені метрики між Canada і Australia у категорії Meat/Poultry; 
перевірити нормальність (Shapiro–Wilk) та застосувати відповідні тести (t-test 
або Mann–Whitney; за потреби — трансформації Box–Cox/Yeo–Johnson/лог). Надати 
коротку бізнес-інтерпретацію та розміри ефектів (Cliff’s δ / Cohen’s d).

Рішення

SELECT category_name
,s.country
,p.unit_price
,quantity
,discount
,ROUND((p.unit_price * quantity *(1-discount))::numeric,2) as chek
,freight
,ROUND((freight/(p.unit_price * quantity *(1-discount)))::numeric,4) as freight_ratio
,(shipped_date - order_date) as delivery_date
,ROUND((freight/(shipped_date - order_date)/quantity)::numeric,4) as daily_freight_per_unit
FROM orders
JOIN order_details USING(order_id)
JOIN products p USING(product_id)
JOIN categories USING (category_id)
JOIN suppliers s USING(supplier_id)
WHERE category_name = 'Meat/Poultry' and s.country IN ('Canada', 'Australia')

