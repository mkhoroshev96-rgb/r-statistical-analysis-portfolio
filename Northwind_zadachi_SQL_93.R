говорить про те, що немає статистично значущої різниці між даними країнами по
цій метриці

shapiro.test(residuals(m8))

показник р становить 0,9269, що вказує нам на те, що наші залишки розподілені 
нормально після трансформації. Тому перейдемо до візуальної оцінки розподілу
даної метрики на лінійній моделі

qqnorm(residuals(m8));qqline(residuals(m8))

Отже, наші залишки розподілені нормально по всій довжині лінії. Відхилення є в
хвостах, проте не значні та не ламають нашої нормальності. Таким чином, обидва
тести і параметричний і непараметричний вказують на один і той же висновок, тому
обидва ці тести є релевантними по цій метриці як і їхні результати.

Висновок

Окей, підсумую кейс Meat/Poultry: Australia vs Canada так само стисло й по-діловому.

Висновок

Суттєві відмінності:
Ціна (unit_price) — вища в Australia. Mann–Whitney: p ≪ 0.001; Cliff’s δ = 1.00 
(повна роздільність, надзвичайно сильний ефект).
Чек (chek) — вищий в Australia. Mann–Whitney: p ≪ 0.001; Cliff’s δ = 0.58 
(великий ефект). Лог-тест (Welch) на нормалізованих даних підтвердив: p ≪ 0.001.
Частка доставки в чеку (freight_ratio) — вища в Canada. Після Box–Cox і t-тесту: 
p = 0.002; Cliff’s δ = −0.28 (малий, але стійкий ефект).

Без статистично значущих відмінностей:
Кількість (quantity) — p ≈ 0.53; δ = 0.06 (дуже малий ефект).
Знижка (discount) — p ≈ 0.14; δ = −0.13 (дуже малий ефект).
Вартість доставки, $ (freight) — розбіжність між тестами: Mann–Whitney p = 0.041
(так), але після нормалізації t-тест p = 0.080 (ні). За коректними припущеннями 
довіряємо t-тесту → різниці немає; δ = 0.20 (малий).
Тривалість доставки (delivery_date) — p ≈ 0.39; δ = −0.09 (дуже малий). У 
середньому ~5.8 дн. (AU) vs ~6.4 дн. (CA) — різниця незначуща.
Добова доставка на одиницю (daily_freight_per_unit) — p ≈ 0.20; δ = 0.15 (малий).

Бізнес-інтерпретація.
Постачальники з Australia працюють із вищою ціною, що тягне за собою вищі чеки,
хоча швидкість та абсолютна вартість доставки істотно не відрізняються. Водночас 
у Canada частка доставки в чеку вища, тобто відносний “тягар” логістики більший
(або через менші суми позицій, або через специфіку тарифів) — ефект малий, але
стабільний. Знижки та обсяги продажів між країнами — практично однакові.

Надійність результатів.
Висновки підтверджені перехресно: непараметричні тести + нормалізації (Box–Cox /
log) з перевіркою нормальності залишків; ключові сигнали (ціна, чек, freight_ratio)
зберігаються після трансформацій.

