Потенційні методологічні ризики

Залежність спостережень. freight — на рівні замовлення, а аналіз — на рівні 
рядка; кілька рядків з одного order_id корелюють. Це занижує p-значення. Краще:
або агрегувати на рівень замовлення (розподіляти freight пропорційно, як ти й 
зробив, але тестувати на рівні order_id),
або використовувати кластеризовані SE за order_id / робастні методи.
Порівняння без контролю product-mix. Australia/Canada можуть мати різний склад
товарів у Meat/Poultry. Різниця в цінах/чеках може бути ефектом іншої номенклатури,
а не «країни». Для перевірки:
фіксовані ефекти product_id (модель: метрика ~ country + factor(product_id)),
або парні порівняння всередині однакових продуктів.
Back-transform коефіцієнтів Box–Cox. Ти інтерпретуєш коефіцієнт лінійної моделі
на трансформованій шкалі через прямий зворотний перерахунок. Це не еквівалентно 
різниці середніх на оригінальній шкалі (нелінійність). Краще:
порівнюй групові середні/медіани після зворотної трансформації прогнозів або
подавай ефект на трансформованій шкалі + аппроксимацію (delta-method/бутстреп) 
для оригінальної.
Cliff’s δ = 1 для unit_price виглядає як повне розділення без жодного перекриття.
Це можливо, але варто подвійно перевірити:
чи правильно викликано функцію (effectsize::cliff_delta(x ~ g, data=...) або 
cliff.delta(x[g==A], x[g==B])),
чи немає округлень/фільтрів, що штучно «розвели» розподіли.
Стабільність freight_ratio. Це відношення чутливе до малих чеків (шум у знаменнику).
Добре було б:
зробити відсікання/вінзоризацію малих чеків,
повторити тест робастно (наприклад, з trimmed-means або медіанами).
Zeros у лог-трансформаціях. Для delivery_date і компонентів у daily_freight_per_unit
потрібна гарантія відсутності нулів у знаменнику. Ти частково чистиш NA, але 
варто явно показати фільтр delivery_date > 0 & quantity > 0.
Єдність критерію цілі. Деколи інтерпретується різниця «середніх» і «медіан» як 
взаємозамінні. Краще заздалегідь фіксувати, що саме тестуєш: локацію медіани 
(MW) чи середнє (Welch), і послідовно цього притримуватись.

