Вибірка: category_name = "Beverages"; постачальники з France та Germany.
Одиниця аналізу: рядок замовлення (товарна позиція).

Метрики (рівень позиції):
unit_price; quantity; discount; chek = unit_price×quantity×(1−discount); freight;
freight_ratio = freight/chek; delivery_date = shipped_date−order_date (дні);
daily_freight_per_unit = freight/(delivery_date×quantity).

Завдання:
Порівняти метрики між France і Germany; перевірити нормальність (Shapiro–Wilk)
і гомогенність дисперсій (Levene); застосувати відповідний тест (Welch t-test 
або Mann–Whitney). За потреби — Box–Cox / Yeo–Johnson / log. Подати розміри ефектів
(Cohen’s d / Cliff’s δ) та коротку бізнес-інтерпретацію.

Рішення

SELECT category_name
,s.country
,p.unit_price
,quantity
,discount
,ROUND((p.unit_price * quantity *(1-discount))::numeric,2) as chek
,freight
,ROUND((freight/(p.unit_price * quantity *(1-discount)))::numeric,4) as freight_ratio
,(shipped_date - order_date) as delivery_date
,ROUND((freight/(shipped_date - order_date)/quantity)::numeric,4) as daily_freight_per_unit
FROM orders 
JOIN order_details USING(order_id)
JOIN products p USING(product_id)
JOIN categories USING(category_id)
JOIN suppliers s USING(supplier_id)
WHERE category_name = 'Beverages' AND s.country IN ('France', 'Germany')

