Залишки після трансформацій
Ти показав перевірку на нормальність залишків, але в деяких місцях вони неідеальні
(p < 0.05). Ти правильно коментував, що «це не критично», але для академічної 
строгості можна було б розглянути робастні моделі чи bootstrap.

