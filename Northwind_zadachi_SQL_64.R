Розділити період їхніх продажів на дві рівні частини за медіанною датою замовлень
(домедіанний період і післямедіанний період).
Порівняти динаміку обраних 8 метрик між цими двома часовими періодами.
Виявити, чи змінилася структура цін, обсягів замовлень, знижок та логістики для 
цих сирів, та зробити аналітичні висновки.

Примітка: надалі в нашому дослідженні ми будемо називати дані товари як Горгондзола
та камамбер для зменшення текстового навантаження

Рішення

With cte as(SELECT product_name
            ,order_date
            ,p.unit_price
            ,quantity
            ,discount
            ,ROUND((p.unit_price * quantity * (1 - discount))::numeric,2) as chek
            ,freight
            ,ROUND((freight/(p.unit_price * quantity * (1 - discount)))::numeric,4) as freight_ratio
            ,(shipped_date - order_date) as delivery_date
            ,ROUND((freight/(shipped_date - order_date)/quantity)::numeric,4) as daily_freight_per_unit
            FROM orders 
            JOIN order_details USING(order_id)
            JOIN products p USing (product_id)
            JOIN categories USING(category_id)),
names as(SELECT *
           FROm cte
         WHERE product_name IN ('Gorgonzola Telino', 'Camembert Pierrot')),
median as(SELECT *
            ,ntile(2) over(order by order_date) as median
          FROM names)
SELECT *
  ,case when median = 1 THEN 'Домедіанний період'
when median = 2 THEN 'Післямедіанний період'
END as periods
FROm median

