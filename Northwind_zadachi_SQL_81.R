🔗 Зв’язки: через order_id, product_id, category_id, employee_id
🎯 Вибірка: category_name = "Beverages"; last_name IN ("Peacock","Fuller")
🧩 Одиниця аналізу: товарна позиція (рядок замовлення).
📌 Метрики (на рівні позиції):
unit_price — ціна товару;
quantity — кількість одиниць;
discount — знижка (0–1);
chek = unit_price * quantity * (1 − discount) — вартість позиції із знижкою;
freight — вартість доставки (з рівня замовлення, віднесена до позиції як у вибірці);
freight_ratio = freight / chek — частка доставки в чеку;
delivery_date = shipped_date − order_date (днів) — тривалість доставки;
daily_freight_per_unit = freight / (delivery_date * quantity) — добові витрати 
на доставку на одиницю.
🧼 Якість даних:
Виключити рядки з NULL у shipped_date або order_date, delivery_date ≤ 0, quantity
≤ 0. За потреби — обробити аномалії (вінзоризація 1–2% або відсікання верхніх 1–2% 
для показників із «хвостами»).
🛠 Завдання:
Порівняння Peacock vs Fuller для кожної метрики:
перевірити нормальність (Shapiro–Wilk, QQ) та гомоскедастичність 
(Levene/Brown–Forsythe);
застосувати відповідний тест: t-test/Welch або Mann–Whitney;
навести p-value, 95% CI, розміри ефектів (Cohen’s d або Hedges’ g; для непараметрики
— Cliff’s delta) та коротку інтерпретацію сили ефекту.
Трансформації: оцінити потребу Box–Cox (тільки для >0) або Yeo–Johnson (коли можливі
нулі/від’ємні). Якщо λ≈0 — логарифмувати. Повторити ключові тести для unit_price, 
freight_ratio, daily_freight_per_unit після трансформацій.
Перевірка стійкості: провести робастний сенситивіті-аналіз (вінзоризація/trimmed
means 10–20%, перетестувати гіпотези) і зафіксувати, чи змінюються висновки.
Додатково (+): дослідити взаємозв’язок discount ↔ quantity окремо по працівниках; 
протестувати різницю схилів через модель із взаємодією.
Бізнес-висновок: хто (якщо є різниця) продає дорожче/дешевше, дає більші знижки, 
має вищі/нижчі логістичні витрати, швидше доставляє; оцінити практичну значущість
і запропонувати рекомендації (ціноутворення, політика знижок, логістика).
📤 Очікуваний результат:
Короткий звіт: таблиця з метриками → тест → p-value → ефект → інтерпретація; кілька
графіків (boxplot/violin, QQ), підсумковий бізнес-висновок і 2–3 практичні 
рекомендації (де оптимізувати доставку/знижки/ассортимент для «Beverages»).

Рішення

SELECT category_name
,last_name
,p.unit_price
,quantity
,discount
,ROUND((p.unit_price * quantity * (1-discount))::numeric,2) as chek
,freight
,ROUND((freight/(p.unit_price * quantity * (1-discount)))::numeric,4) as freight_ratio
,(shipped_date - order_date) as delivery_date
,ROUND((freight/(shipped_date - order_date)/quantity)::numeric,4) as daily_freight_per_unit
FROM orders 
JOIN order_details USING(order_id)
JOIN products p USING(product_id)
JOIN categories USING(category_id)
JOIN employees USING (employee_id)
WHERE category_name = 'Beverages' AND last_name IN ('Peacock', 'Fuller')

