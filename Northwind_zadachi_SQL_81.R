Ефект-сайзи для Wilcoxon
rstatix::wilcox_effsize() повертає ефект r, це не “% варіації”, і його не можна
трактувати як “7.63% варіацій пояснюється…”. Коректні інтерпретації для r: ~0.1 
(малий), ~0.3 (середній), ~0.5 (великий).
Для непараметрики краще Cliff’s delta (+ CI): rstatix::cliff_delta().
Box–Cox / Yeo–Johnson: зворотнє перетворення та упередження
Ти береш середнє на трансформованій шкалі та просто інвертуєш: це дає занижену 
оцінку на оригінальній шкалі (через нелінійність). Коректніше:
або прогнозувати на вихідній шкалі через моделювання та інверсію на рівні предиктів
(із “smearing” Дуанa),
або чітко підписати: «на трансформованій шкалі різниця незначуща; значення у 
вихідних одиницях наведені лише як приблизна ілюстрація».
