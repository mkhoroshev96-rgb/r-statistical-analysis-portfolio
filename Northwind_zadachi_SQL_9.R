Задача 65

Чи впливає частота замовлень клієнта на середній чек?
  
  💭 Ідея:
Припустимо, є клієнти, які замовляють часто, але роблять невеликі замовлення — 
наприклад, «дрібні оптовики». А є ті, хто замовляє рідко, але кожного разу купує
багато.Хочемо перевірити: чи існує зв’язок між тим, як часто клієнт робить 
замовлення, і розміром середнього чеку.

🧪 Гіпотеза (кореляційний аналіз + ANOVA):
H₀: Кількість замовлень клієнта не впливає на середній чек
H₁: Чим більше замовлень у клієнта, тим вища або нижча середня сума замовлення

