Задача 61

Чи продаються дорогі категорії швидше?
💭 Ідея:
Припустимо, що дорогі товари доставляються клієнтам швидше, бо вони більш 
пріоритетні або коштують дорожче, отже компанія зацікавлена швидше доставити й 
не втратити прибуток.

🧪 Гіпотеза (ANOVA):
H₀: Середній час доставки (shipped_date - order_date) не залежить від категорії товару.
H₁: Хоча б одна категорія має інший середній час доставки.

