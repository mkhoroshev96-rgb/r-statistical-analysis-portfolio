🛠 Алгоритм вибірки
Відібрані всі товарні позиції з категорії "Seafood", оформлені у 1997 році.
Для кожної позиції розраховано:
unit_price — ціна одиниці товару
quantity — кількість
discount — знижка
chek — сума позиції з урахуванням знижки
freight — витрати на доставку замовлення
freight_ratio — частка доставки в структурі чека (freight / chek)
delivery_date — кількість днів між оформленням і доставкою
daily_freight_per_units — добова вартість доставки одиниці товару 
(freight / delivery_days / quantity)
