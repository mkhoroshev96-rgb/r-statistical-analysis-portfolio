🎯 Мета:
Порівняти дві групи замовлень на основі медіанного значення diff_delivery_date =
  required_date - shipped_date:
  
Before_group — замовлення з меншою або рівною кількістю днів між required_date 
та shipped_date (тобто короткий плановий інтервал)

After_group — замовлення з більшою кількістю днів між required_date та shipped_date
(тобто довший плановий інтервал)

Аналіз проводиться за такими метриками:
  
avg_price_per_order — середня ціна товару
avg_discount_per_order — середня знижка
sum_quantity_per_order — кількість товару в замовленні
sum_chek_per_order — чек без доставки
avg_freight_per_order — вартість доставки
avg_delivery_date_per_order — фактична швидкість доставки

Рішення

WITH cte as(SELECT o.order_id
            ,order_date
            ,required_date
            ,shipped_date
            ,unit_price
            ,quantity
            ,discount
            ,freight
            FROM orders o
            JOIN order_details od ON o.order_id = od.order_id),
agg as(SELECT order_id
       ,ROUND(AVG(required_date - shipped_date)::numeric,0) as diff_delivery_date
       ,ROUND(AVG(shipped_date - order_date)::numeric,0) as avg_delivery_date_per_order
       ,ROUND(AVG(unit_price)::numeric,2) as avg_price_per_order
       ,ROUND(SUM(quantity)::numeric,2) as sum_quantity_per_order
       ,ROUND(AVG(discount)::numeric,4) as avg_discount_per_order
       ,ROUND(AVG(freight)::numeric,2) as avg_freight_per_order
       ,ROUND(SUM(quantity * unit_price * (1-discount))::numeric,2) as sum_chek_per_order
       FROM cte
       GROUP BY order_id),
median as(SELECT *
            ,(SELECT percentile_cont(0.5) within group(order by diff_delivery_date) FROM agg) as median_diff_delivery_date
          FROM agg
          where diff_delivery_date > 0)
SELECT *
  ,case
when diff_delivery_date <= median_diff_delivery_date THEN 'Before_group'
when diff_delivery_date > median_diff_delivery_date THEN 'After_group'
END as median_diff_group
FROM median

