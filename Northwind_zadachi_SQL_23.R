Бізнес на старті і на фініші: що змінилось між 1996 і 1998 роком?
  Порівняльний аналіз замовлень за ключовими показниками між двома періодами

📦 Контекст:
  Компанія Northwind працювала активно у 1996–1998 роках. Бізнес хоче зрозуміти,
чи змінилася купівельна поведінка та ефективність замовлень між першим і останнім 
роком роботи. Це дозволить оцінити напрямок розвитку, вплив інфляції, зміни у 
знижках, доставці тощо.

🎯 Мета:
  Порівняти 1996 та 1998 роки (years_group = 'year_1996' vs 'year_1998') за 
такими метриками:
  
avg_price_per_order — середня ціна товару
avg_discount_per_order — середній відсоток знижки
sum_quantity_per_order — кількість товару в замовленні
sum_chek_per_order — середній чек (без доставки)
avg_freight_per_order — витрати на доставку
avg_delivery_date_per_order — швидкість доставки

🧮 Рівень аналізу:
  Один рядок = одне замовлення, агреговане на рівні order_id
→ Порівняння відбувається між роками, на основі замовлень

