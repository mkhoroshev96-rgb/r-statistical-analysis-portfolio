Проаналізувати зміни ключових бізнес- та логістичних метрик по топ-3 товарах 
категорії Condiments (спеції), відібраних за кількістю замовлень. Усі замовлення
для кожного з трьох лідерів поділено на дві групи — до та після медіанної дати 
оформлення замовлення по кожному товару.
📌 Метрики для аналізу:
unit_price – ціна одиниці товару
quantity – кількість одиниць у замовленні
discount – розмір знижки
chek – сума замовлення (з урахуванням знижки)
freight – вартість доставки
freight_ratio – частка доставки в структурі замовлення (freight / chek)
delivery_date – тривалість доставки в днях
daily_freight_per_unit – добова логістична вартість доставки одиниці товару
🛠 Методологія:
Всі замовлення по топ-3 товарах категорії Condiments було відсортовано за 
order_date, розбито на дві рівні групи за допомогою ntile(2) — домедіанний період
та післямедіанний період — і класифіковано у відповідну змінну periods.

Рішення

WITH cte as(SELECT product_name
            ,category_name
            ,order_date
            ,p.unit_price
            ,quantity
            ,discount
            ,ROUND((p.unit_price * quantity * (1 - discount))::numeric,2) as chek
            ,freight
            ,ROUND((freight/(p.unit_price * quantity * (1 - discount)))::numeric,4) as freight_ratio
            ,(shipped_date - order_date) as delivery_date
            ,ROUND((freight/(shipped_date - order_date)/quantity)::numeric,4) as daily_freight_per_unit
            ,count(*) over (partition by product_name) as count_orders
            FROM orders
            JOIN order_details USING(order_id)
            JOIN products p USING(product_id)
            JOIN categories USING(category_id)),
ranks as(SELECT *
           ,dense_rank() over (partition by category_name order by count_orders DESC) as ranks
         FROM cte
         WHERE category_name = 'Condiments'),
median as(SELECT *
            ,ntile(2) over (order by order_date) as median
          FROM ranks
          WHERE ranks <=3)
SELECT *
  ,case when median = 1 Then 'Домедіаний період'
when median = 2 Then 'Післямедіаний період'
END as periods
FROM median

