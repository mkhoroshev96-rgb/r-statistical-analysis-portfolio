Порівняльний аналіз доставок між двома виробниками з різних країн
📦 База даних: Northwind
📊 Таблиці: orders, order_details, products, suppliers
🔗 Об’єднання: через order_id, product_id, supplier_id

🎯 Мета дослідження:
Порівняти структуру замовлень та логістичних витрат між двома виробниками з 
різних країн —
британською компанією Specialty Biscuits, Ltd. (печиво, снеки) та
американською компанією New England Seafood Cannery (морепродукти, консерви),
які мають supplier_id = 8 і supplier_id = 19 відповідно.

📌 Вибірка:
У вибірку включено всі товарні позиції з замовлень, де постачальником є один із 
двох вказаних виробників.

🧮 Метрики (на рівні товарної позиції):
unit_price — ціна одиниці товару
quantity — кількість одиниць товару
discount — знижка на товар
freight — вартість доставки всього замовлення
chek — вартість позиції з урахуванням знижки
freight_ratio — співвідношення вартості доставки до чеку
delivery_date — кількість днів доставки
daily_freight_per_unit — добова вартість доставки однієї одиниці товару

🧠 Напрямок аналізу:
Порівняй обидві компанії за кожною з метрик

Визнач, чи є статистично значущі відмінності:
  
у цінах, обсягах, знижках;
у доставці, тривалості, витратах.

⚠️ Уточнення:
Вартість доставки (freight) — одна на все замовлення, тому її розподіл на товарні
позиції є спрощенням.
supplier_id — це виробник товару, а не логіст.

Рішення

SELECT supplier_id
,company_name
,p.unit_price
,quantity
,discount
,freight
,ROUND((freight/(p.unit_price * quantity *(1-discount)))::numeric,2) as freight_ratio
,ROUND((p.unit_price * quantity *(1-discount))::numeric,2) as chek
,(shipped_date - order_date) as delivery_date
,ROUND((freight / (shipped_date - order_date) / quantity)::numeric,2) as daily_freight_per_unit
FROM orders
JOIN order_details USING(order_id)
JOIN products p USING(product_id)
JOIN suppliers USING(supplier_id)
where supplier_id in ('8','19')

