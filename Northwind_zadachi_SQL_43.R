статистиної значущості в 0,05. Це показники відношення вартості доставки до величини
чека та кількість днів доставки. Це ті метрики, які в майбутньому моржуть стати
статистично значущими, тому за ними потрібно пильно стежити. Проте, варто зазначити,
що 7 з 8 показників мають слабкий розмір ефекту, окрім ціни - вона має середній
розмір ефекту і має досить таки суттєве значення для бізнесу в прикладному плані.

