Проте медіани однакові (18 дол.) → різницю створюють викиди (окремі дорогі товари).
Розмір ефекту слабкий (d ≈ 0.18).
👉 Висновок: статистично відмінності є, але бізнес-ефект слабкий (це поодинокі 
товари, а не системна різниця).

Кількість проданих одиниць (quantity)
Тест Манна–Вітні (p ≈ 0.35) → відмінностей немає.
Середні: Callahan ≈ 25.6, Peacock ≈ 21 (різниця ~22%).
Медіани: 20 vs 18.
Ефект слабкий (≈ 0.08).
👉 Висновок: статистично і прикладно різниця несуттєва.

Знижки (discount)
Тест Манна–Вітні (p ≈ 0.34) → відмінностей немає.
Середні: 4.2% у Callahan проти 6.1% у Peacock (+45%).
Медіани = 0 в обох.
Ефект слабкий (≈ 0.09).
👉 Висновок: середні відрізняються через кілька замовлень із високою знижкою у 
Peacock, але глобально різниці немає.

Чек за позицію (chek)
Тест Манна–Вітні (p ≈ 0.36) → різниці немає.
Середні: 371 дол. (Callahan) проти 752 дол. (Peacock) → у 2 рази більше.
Медіани близькі: 276 vs 306 (+11%).
Ефект слабкий (≈ 0.08).
👉 Висновок: різниця у середніх спричинена великими угодами у Peacock; системної
різниці немає.

Вартість доставки (freight)
Тест Манна–Вітні (p ≈ 0.41) → різниці немає.
Середні: 85 (Callahan) vs 96 (Peacock) (+13%).
Медіани: 41 vs 55 (+33%).
Ефект слабкий (≈ 0.07).
