🔑 Глобальний висновок
Майже всі метрики показали: відмінності статистично незначущі та мають дуже малий
розмір ефекту.
Різниця у середніх часто виглядає великою (іноді у 2 рази), але це наслідок 
поодиноких викидів у Peacock.
За медіанами та тестами Манна–Вітні — типові замовлення обох працівників однакові.
Трансформації (лог, Yeo–Johnson) частково підтвердили це: залишки у моделях часто
залишаються ненормальними → класичні t-тести ненадійні → Манна–Вітні треба вважати
базовим.
Для бізнесу: продуктивність Callahan і Peacock практично не відрізняється, окрім
кількох «аномальних» замовлень у Peacock, які спотворюють середні.

