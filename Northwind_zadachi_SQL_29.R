За допомогою DENSE_RANK() знайдено працівника з найбільшою кількістю оброблених 
замовлень (тобто den_runk = 1).

Усі його замовлення поділяються на дві групи за допомогою NTILE(2):
  
before_group — замовлення з першої половини хронології;
after_group — замовлення з другої половини.

Одиниця аналізу:
Одне замовлення (order_id), оброблене топ-працівником за кількістю замовлень.

Мета аналізу:
З’ясувати, чи змінилися ключові показники замовлень цього працівника до та після
умовного «екватора» його роботи.

