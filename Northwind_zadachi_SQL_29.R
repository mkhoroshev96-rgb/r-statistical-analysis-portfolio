            ,ROUND(AVG(quantity)::numeric,2) as avg_quantity
            ,ROUND(AVG(discount)::numeric,2) as avg_discount
            ,ROUND(AVG(freight)::numeric,2) as avg_freight
            ,ROUND(AVG(shipped_date - order_date)::numeric,0) as avg_delivery_date
            ,ROUND(AVG(quantity * unit_price * (1-discount))::numeric,2) as avg_chek
            ,count(*) over(partition by employee_id) as count_order_customers
            FROM orders
            JOIN order_details USING(order_id)
            JOIN employees USING(employee_id)
            GROUP BY order_id,employee_id,last_name,first_name),
den_run as(SELECT *
             ,dense_rank() over(order by count_order_customers DESC) as den_runk
           FROM cte),
median as(SELECT *
            ,ntile(2) over(partition by employee_id order by order_date) as median
          FROM den_run
          where den_runk = 1)
SELECT *
  ,case
when median = 1 then 'before_group'
else 'after_group'
end as median_group
FROM median

