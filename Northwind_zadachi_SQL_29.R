Задача 136

A/B тест до та після медіанної дати для топ-3 клієнтів
База даних:
  Northwind — містить таблиці orders, order_details та customers, об’єднані через
order_id та customer_id.

Мета:
Проаналізувати, чи змінилася поведінка трьох найбільш активних клієнтів (за 
кількістю замовлень) після певного моменту часу, а саме — після медіанної дати 
їхніх індивідуальних замовлень.

Алгоритм відбору:
Для кожного замовлення (order_id) обчислюються метрики:
  
avg_price, avg_quantity, avg_discount, avg_freight, avg_delivery_date, avg_chek.
За допомогою віконної функції COUNT(*) OVER (PARTITION BY customer_id) визначено
загальну кількість замовлень для кожного клієнта.
Відібрані топ-3 клієнти з найбільшою кількістю замовлень (DENSE_RANK() за 
count_order_customers у спадному порядку).
Для кожного з трьох клієнтів їхні замовлення сортуються за датою (order_date) і 
діляться на дві групи:
  
before_group — перша половина замовлень (до медіани),
after_group — друга половина замовлень (після медіани), за допомогою NTILE(2).

Одиниця аналізу:
Одне замовлення (order_id) клієнта з топ-3.

Метрики для аналізу (усереднені по замовленню):
avg_price — середня ціна товарів;
avg_quantity — середня кількість товарів;
avg_discount — середня знижка;
avg_freight — вартість доставки;
avg_delivery_date — час доставки у днях;
avg_chek — середній чек (сума з урахуванням знижок).

Рішення

WITH cte as(SELECT order_id
            ,customer_id
            ,order_date
            ,shipped_date
            ,ROUND(AVG(unit_price)::numeric,2) as avg_price
            ,ROUND(AVG(quantity)::numeric,2) as avg_quantity
            ,ROUND(AVG(discount)::numeric,2) as avg_discount
            ,ROUND(AVG(freight)::numeric,2) as avg_freight
            ,ROUND(AVG(shipped_date - order_date)::numeric,0) as avg_delivery_date
            ,ROUND(AVG(quantity * unit_price * (1-discount))::numeric,2) as avg_chek
            ,count(*) over(partition by customer_id) as count_order_customers
            FROM orders
            JOIN order_details USING(order_id)
            JOIN customers Using(customer_id)
            group by order_id,customer_id),
den_rnk as(SELECT *
             ,dense_rank() over(order by count_order_customers DESC) as dens_rnk
           FROM cte),
med as(SELECT *
         ,ntile(2) over(partition by customer_id order by order_date) as median
       FROM den_rnk
       where dens_rnk <=3)
SELECT *
  ,case
when median = 1 then 'before_group'
else 'after_group'
END as median_group
FROM med

