Псевдоповторення для freight-метрик. freight — на рівні замовлення, а аналіз —
на рівні позицій → дублювання значень усередині order_id. Це занижує SE та 
спотворює p-value для freight, freight_ratio, daily_freight_per_unit. Краще: 
агрегувати до рівня замовлення або модель з кластерно-робастними SE / random 
intercept по order_id.
Levene має бути на тій самій шкалі. Для показників, де t-тест робиться після 
трансформації (напр. log_ratio), гомогенність дисперсій перевіряй саме на 
трансформованій змінній.
Ретрансформація після Box–Cox. Повернення “середніх у $” через просте інвертування 
оцінок лінійної моделі дає зміщення (ігнорується сміринг/Дуан). Для інтерпретації 
рівнів — або сміринг, або різниці залишати в трансформованій шкалі.
Нулі та невалідні значення.
freight_ratio: лог без зсуву падає на нулях — потрібна перевірка/зсув.
delivery_date: можливі ≤0 → у daily_freight_per_unit ділення на 0/NA. Потрібна 
явна фільтрація.
Консистентність формулювань. Подекуди після “нормальності немає — не використовуємо
t-тест” далі все ж іде t-тест без чіткого пояснення, що він виконаний після 
трансформації — варто це підкреслювати.
Ефекти vs практична значущість. Для більшості метрик δ дуже малі — це правильно
зазначено; добре б систематично відокремлювати статистичну/практичну значущість
у підсумку для кожної метрики.
Висновок щодо коректності
Загальна логіка аналізу вірна, головні висновки (вища ціна й чек у Sweden; решта
— без суттєвих відмінностей) підтримані. Найбільший методологічний ризик — залежність
спостережень через спільний freight в межах замовлення; саме через це висновки по
логістичних метриках потенційно нестійкі. Усунь (кластеризація/агрегація) + узгодь
шкали для Levene та ретрансформації — і кейс буде коректним на 100%.
