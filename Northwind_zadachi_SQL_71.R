то “частка поясненої дисперсії” приблизно r²≈3.4%, а не 18.4%. Тож формулюй: 
“малий ефект (r=0.184), практично незначущий для бізнесу”.
Observed power ≠ доказ.
Ти обчислюєш потужність із вибіркового d — це “observed power”, яка по суті 
монотонна функція p‑value і малоінформативна. Краще:
показувати CI для ефекту (r або g/Hedges) і/або
MDE/SESOI: “яку мінімальну різницю бізнес вважає суттєвою, чи виключає її CI?”.
