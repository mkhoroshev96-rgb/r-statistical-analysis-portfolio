Висновок 

За результатами аналізу восьми ключових метрик для замовлень категорії Beverages,
оброблених працівниками Peacock та Leverling, статистично значущих відмінностей 
між групами не виявлено (усі p-value > 0,05).
Розміри ефекту (wilcox_effsize) для всіх показників — слабкі (0,0098–0,161), що
свідчить про дуже малий практичний вплив фактора «хто обробляв замовлення» на 
різницю в показниках.
Середні та медіанні значення за всіма метриками близькі між собою, а відносні 
відмінності переважно не перевищують 15%–20%, за винятком деяких співвідношень,
що мають великі відсоткові різниці, але при цьому супроводжуються відсутністю
статистичної значущості та слабким ефектом.
Візуальний аналіз (boxplot) не виявив системних зрушень розподілів між групами — 
інтерквартильні розмахи та медіани здебільшого збігаються, кількість викидів 
приблизно симетрична.
📌 Практичний підсумок: фактор «обробник замовлення» (Peacock vs Leverling) не 
впливає суттєво на цінову, кількісну та логістичну структуру замовлень категорії
Beverages. Для бізнесу це означає, що продуктивність та характеристики оброблених
замовлень у цій категорії стабільні та не залежать від конкретного працівника.

