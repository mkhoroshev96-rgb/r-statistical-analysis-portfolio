p=0.0673 по delivery_days після корекції точно не стане значущим — це зміцнить 
висновок “різниць немає”.
Ефекти — по всіх метриках.
Навіть коли p‑value великі, коротко подай ефект + 95% CI (rank-biserial r для MW
або Cliff’s delta; для порівняння — Hedges’ g). Це дає бізнесу відчуття масштабу,
а не лише “(не)значуще”.
Робастність (швидкі додатки):
Log‑трансформації для сильно скошених (freight, chek) → Welch t на логах із 
