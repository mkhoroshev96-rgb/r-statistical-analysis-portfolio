Порівняння замовлень клієнтів з Німеччини, оброблених працівниками Leverling та
Davolio
Дані: Northwind
Відбір: customers.country = 'Germany' та employees.last_name IN ('Leverling', 'Davolio').
Одиниця аналізу: одне замовлення (order_id).
Метрики (на рівні замовлення):
avg_unit_price — середня ціна товару в замовленні
items — кількість проданих одиниць товару
avg_discount — середній розмір знижки
order_value (chek) = unit_price * quantity * (1 - discount)
freight — вартість доставки
freight_ratio = freight / order_value
delivery_days = shipped_date - order_date (у днях)
daily_freight_per_unit = freight / (delivery_days * items)
Якість даних: виключити замовлення з порожніми датами, delivery_days <= 0, items
<= 0, order_value <= 0.
Завдання:
Порівняти Leverling та Davolio за кожною метрикою (описова статистика + візуалізації),
перевірити статистичні відмінності (t-test або Mann–Whitney залежно від припущень),
навести розміри ефекту та зробити підсумковий бізнес-висновок про відмінності у 
структурі замовлень та логістиці для німецьких клієнтів.
Рішення
SELECT c.country
,e.last_name
,p.unit_price
,quantity
,discount
,ROUND((p.unit_price * quantity * (1 - discount))::numeric,2) as chek
,freight
,ROUND((freight/(p.unit_price * quantity * (1 - discount)))::numeric,4) as freight_ratio
,(shipped_date - order_date) as delivery_date
,ROUND((freight/(shipped_date - order_date)/quantity)::numeric,4) as daily_freight_per_unit
FROM orders
JOIN order_details USING (order_id)
JOIN products p USING (product_id)
JOIN customers c USING (customer_id)
JOIN employees e USING (employee_id)
WHERE c.country = 'Germany' AND last_name IN ('Leverling', 'Davolio')
