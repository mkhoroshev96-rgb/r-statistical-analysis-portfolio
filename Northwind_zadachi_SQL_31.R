Задача 142
Порівняльний аналіз структури замовлень клієнтів із США та Бразилії

База даних:
  Northwind — таблиці orders та order_details, з’єднані через order_id.

Мета:
  Визначити, чи існують статистично значущі відмінності між клієнтами з США та 
Бразилії за такими метриками:
  
  avg_price — середня ціна одиниці товару;

avg_quantity — середня кількість одиниць товару;

avg_discount — середній розмір знижки;

avg_freight — середня вартість доставки;

avg_delivery_date — середній час доставки;

avg_chek — середній чек із урахуванням знижки.

Одиниця аналізу:
  Одне замовлення (order_id).

Фільтр:
  ship_country IN ('USA', 'Brazil')

Подальші дії:
  Перевірка гіпотез про відмінності між країнами для кожної метрики за допомогою 
Shapiro-Wilk, Wilcoxon або t-тесту (за потреби), побудова boxplot’ів та оцінка ефекту.

Рішення

SELECT  order_id
,ship_country
,order_date
,shipped_date
,ROUND(AVG(unit_price)::numeric,2) as avg_price
,ROUND(AVG(quantity)::numeric,2) as avg_quantity
,ROUND(AVG(discount)::numeric,2) as avg_discount
,ROUND(AVG(freight)::numeric,2) as avg_freight
,ROUND(AVG(shipped_date - order_date)::numeric,0) as avg_delivery_date
,ROUND(AVG(quantity * unit_price * (1-discount))::numeric,2) as avg_chek
FROM orders
JOIN order_details USING(order_id)
where ship_country in ('USA', 'Brazil')
GROUP by order_id,ship_country

