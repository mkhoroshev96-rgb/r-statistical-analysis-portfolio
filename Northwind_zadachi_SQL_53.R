2. 📏 Відсутність графіків
Ти посилаєшся на hist() та boxplot(), але не додаєш зображень. У візуальній 
аналітиці це мінус.
Це може бути технічна межа формату, але якщо звіт іде в PDF / презентацію — 
обов’язково додавати графіки.
✅ Рекомендація: експортуй графіки з R (ggplot2, ggsave) і візуально коментуй 
2–3 ключові.

3. 📊 Використання централізованих метрик
chek, freight, unit_price, quantity — аналізуєш добре, але не враховуєш можливу
внутрішню кореляцію (наприклад, freight та quantity).
