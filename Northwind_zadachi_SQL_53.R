📌 Методологія та метрики
Відібрати всі товарні позиції, замовлені у постачальника “Gai pâturage” (французька 
компанія). Для кожної позиції розрахувати наступні показники:
  
unit_price — ціна одиниці товару
quantity — кількість проданого товару
discount — надана знижка
chek — сума позиції з урахуванням знижки
freight — витрати на доставку
freight_ratio — частка доставки у вартості (freight / chek)
delivery_date — тривалість доставки (днів від дати замовлення до дати відвантаження)
daily_freight_per_units — добова вартість доставки одиниці товару 
(freight / days / quantity)

