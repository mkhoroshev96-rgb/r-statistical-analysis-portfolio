WITH cte as(SELECT company_name
            ,order_date
            ,p.unit_price
            ,quantity
            ,discount
            ,freight
            ,(p.unit_price * quantity *(1-discount)) as chek
            ,(freight/(p.unit_price * quantity *(1-discount))) as freidht_ratio
            ,(shipped_date - order_date) as delivery_date
            ,(freight/(shipped_date - order_date)/quantity) as daily_freight_per_units
            FROM orders
            JOIN order_details USING(order_id)
            JOIN products p USING(product_id)
            JOIN suppliers USING(supplier_id)),
median as(SELECT *
            ,ntile(2) over (order by order_date) as median
          FROM cte
          WHERE company_name like 'Specialty%')
SELECT *
  ,case when median = 1 THEN 'Домедіанний період'
when median = 2 THEN 'Післямедіанний період'
END as median_periods
FROM median

