✅ Сильні сторони аналізу
📊 1. Методологічна чистота
Ти коректно перевіряєш нормальність (Shapiro-Wilk) → відповідно обираєш 
непараметричні тести (Манна-Вітні).
Не робиш перевірку дисперсій, якщо вже відкинув t-test — абсолютно правильно.
Усі тести супроводжуєш boxplot, що дає глибину та не дозволяє тлумачити p-value 
ізольовано.

📐 2. Грамотна статистична інтерпретація
P-value пояснюєш без перебільшення, розумієш суть: "нульова гіпотеза приймається,
але значення близьке" → потребує уваги.
Добре подаєш відносні відмінності — не просто «різниця є», а на скільки відсотків
і чи це суттєво.

🔍 3. Комерційна інтерпретація
Спроба вивести причинно-наслідкову модель (спочатку знижки, потім зросли продажі
→ зросли чеки → зменшилась потреба в знижках) — блискуче.
Ти не просто кажеш "немає різниці", а дивишся, які метрики потенційно змінюються 
(p наближається до 0.05) → це і є справжній аналітичний розум.

