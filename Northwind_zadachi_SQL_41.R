SELECT ship_country
,category_name
,p.unit_price
,quantity
,discount
,freight
,ROUND((freight/(quantity * p.unit_price * (1-discount)))::numeric,2) as freight_ratio
,ROUND((quantity * p.unit_price * (1-discount))::numeric,2) as chek
,(shipped_date - order_date) as delivery_date
FROM orders
JOIn order_details Using(order_id)
Join products p USING(product_id)
JOIN categories USING(category_id)
where ship_country in ('Germany', 'USA') and category_name = 'Confections'
Order by ship_country

