🧮 Метрики для аналізу:
unit_price — ціна товару
quantity — кількість одиниць
discount — розмір знижки
chek — вартість позиції з урахуванням знижки
freight — загальна вартість доставки
freight_ratio — частка доставки у чеку
delivery_date — тривалість доставки (днів)
daily_freight_per_unit — вартість доставки за одиницю товару на день

🧪 Групування:
Група A: замовлення, оброблені Peacock
Група B: замовлення, оброблені Davolio

Рішення

SELECT e.last_name
,c.country
,p.unit_price
,quantity
,discount
,ROUND((p.unit_price * quantity * (1 - discount))::numeric,2) as chek
,freight
,ROUND((freight/(p.unit_price * quantity * (1 - discount)))::numeric,4) as freight_ratio
,(shipped_date - order_date) as delivery_date
,ROUND((freight/(shipped_date - order_date)/quantity)::numeric,4) as daily_freight_per_unit
FROM orders
JOIN order_details USING(order_id)
JOIN products p USING (product_id)
JOIN customers c USING (customer_id)
JOIN employees e USING (employee_id)
WHERE c.country = 'USA' AND e.last_name IN ('Davolio', 'Peacock')

