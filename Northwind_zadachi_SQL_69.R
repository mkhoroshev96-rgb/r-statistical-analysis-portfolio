Ключові зауваження (це важливо)
Подвійний облік змінних рівня “замовлення”.
Ти береш рядки order_details, але підтягуєш з orders змінні freight і delivery_date
та порівнюєш їх на рівні рядка позиції. Тоді кожен “freight” повторюється стільки 
разів, скільки рядків у замовленні, що спотворює розподіли і p‑значення (у кого
більше позицій на замовлення — той “отримає” завищений freight/дні).
Як правильно:
або перейти на рівень замовлення (1 рядок = 1 order) і порівнювати freight, 
delivery_date, freight_ratio для замовлення;
або розподіляти freight пропорційно до вартості позиції:
freight_alloc = freight * (line_value / sum(line_value by order_id)).
Ефект‑сайз інтерпретовано невірно.
wilcox_effsize у rstatix повертає r (кореляційний коефіцієнт), а не “% варіації, 
що пояснюється”.
Якщо r = 0.124, то “пояснена дисперсія” ≈ r² = 0.015, тобто 1.5%, а не 12.4%.
Те саме для інших метрик.
