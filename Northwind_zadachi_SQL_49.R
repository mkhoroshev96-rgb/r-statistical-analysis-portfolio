Аналіз категорії "Confections" до та після медіанної дати
📦 База даних: Northwind
📊 Таблиці: orders, order_details, products, categories
🔗 Об’єднання: через order_id, product_id, category_id

🎯 Мета дослідження
Дослідити, чи відбулися суттєві зміни в структурі замовлень товарів категорії 
"Confections" (солодощі, цукерки) до та після медіанної дати оформлення замовлень.

🛠 Алгоритм формування вибірки
Відібрані лише ті товарні позиції, що належать до категорії "Confections".

Для кожної позиції розраховано наступні метрики:
unit_price — ціна одиниці товару;
quantity — кількість одиниць;
discount — розмір знижки;
freight — вартість доставки (на рівні замовлення);
chek — підсумкова сума товару з урахуванням знижки;
freight_ratio — частка вартості доставки у складі чеку;
delivery_date — тривалість доставки (у днях);
daily_freight_per_unit — добова вартість доставки однієї одиниці товару.

Усі позиції поділено на 2 групи за принципом медіани дати оформлення:
Домедіанна група — позиції, оформлені до медіанної дати.
Післямедіанна група — позиції, оформлені після медіанної дати.

📊 Завдання для аналітики
Для кожної метрики провести порівняльний аналіз між двома групами:
Оцінити розподіл:
Використати тест Шапіро-Вілка для перевірки нормальності.
Візуалізувати розподіл (гістограми, boxplot).
Порівняти групи:
Якщо розподіл нормальний → t-тест;
Якщо ні → Манна-Вітні (wilcox.test).

Оцінити ефект:
  
Розрахувати ефект (Cohen’s d або rank-biserial);
Порахувати % зміну, порівняти середні та медіани.

📈 Мета аналізу
Виявити, чи могли мати місце зміни в ціновій політиці, обсягах, логістиці чи 
знижках для солодощів, що можуть бути пов’язані з:
  
Сезонними коливаннями (свята, попит),
Зміною постачальників,
Змінами у витратах на доставку.

