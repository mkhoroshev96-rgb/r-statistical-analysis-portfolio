Задача 109

ANOVA по країнам: чи впливає країна на ключові бізнес-показники?
  
📦 Контекст:
Компанія хоче перевірити, чи відрізняється поведінка клієнтів у різних країнах. 
Це може допомогти адаптувати маркетингові стратегії, оцінити локальні переваги,
виявити "золоті" ринки.

🎯 Мета:
Проаналізувати, чи країна клієнта (country) впливає на такі метрики:
  
Середню ціну (unit_price)
Середню знижку (discount)
Середню кількість товарів (quantity)
Середній чек (unit_price * quantity)
Середній прибуток (unit_price * quantity * (1 - discount - 0.4))

🔧 Методологія:  
Об’єднай таблиці orders, order_details, products, customers.
Для кожної пари order_id × country порахуй середні метрики.

Для кожної метрики:
Перевір нормальність (Shapiro-Wilk)
Перевір гомогенність дисперсій (Levene’s test)
Проведи ANOVA або Kruskal–Wallis, залежно від виконання припущень.
Якщо p < 0.05 — зроби post-hoc:
Tukey HSD для ANOVA
Dunn’s test для Kruskal

Оціни розмір ефекту:
  
Eta² або Partial Eta² (для ANOVA)
Kruskal η² (для Kruskal)

📊 Додатково (опційно):
  
Побудуй boxplot по кожній метриці
Таблицю середніх значень по країнах
Таблицю значущих пар з post-hoc

Рішення

WITH cte as(SELECT order_id
            ,ship_country
            ,order_date
            ,shipped_date
            ,unit_price
            ,quantity
            ,discount
            ,freight
            FROM orders
            Join order_details USING(order_id))
SELECT order_id
,ship_country
,ROUND(AVG(shipped_date - order_date)::numeric,0) as avg_delivery_date_per_order
,ROUND(AVG(unit_price)::numeric,2) as avg_price_per_order
,ROUND(SUM(quantity)::numeric,2) as sum_quantity_per_order
,ROUND(AVG(discount)::numeric,4) as avg_discount_per_order
,ROUND(AVG(freight)::numeric,2) as avg_freight_per_order
,ROUND(SUM(quantity * unit_price * (1-discount))::numeric,2) as sum_chek_per_order
,ROUND(sum((unit_price * quantity * (1 - discount)) - (unit_price * quantity * 0.6))::numeric,2) as sum_profit_per_order
FROM cte
GROUP by order_id, ship_country

