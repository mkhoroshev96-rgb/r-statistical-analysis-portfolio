що показник р був вищий за 0,05. Далі, кількість товарів в замовленні - компанія
постачальник не може сама виявляти, скільки їй чого потрібно поставити реалізатору,
тут сам реалізатор виявляє, скільки йому потрібно того чи іншого товару від конкретного
постачальника. Далі у нас знижка. Розмір дисконту установлює компанія-реалізатор,
саме вона розуміє, скільки якому постачальнику вона може встановити дисконт, тому
й не дивно, що розмір знижки не залежить від постачальника. В мене все).

