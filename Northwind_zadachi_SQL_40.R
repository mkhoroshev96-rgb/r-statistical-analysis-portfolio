Альтернативна гіпотеза:швидкіть перевезення куплених товарів залежить від того, 
яка це категорія товару - напої чи морепродукти

d_125$delivery_date <- as.numeric(as.character(d_125$delivery_date))

Перейдемо до перевірки гіпотези нормальності розподілу даних за Шапіро тестом

d_125 %>% group_by(category_name) %>% shapiro_test(delivery_date)

Шапіро тест вказує на те, що наші дані розподілені не нормально. Перейдемо до 
додаткового аналізу даного факту на гістограмі

hist(d_125$delivery_date)

Дані мають праве зміщення, тест Шапіро Вілка є вірним, тому надалі ми не можемо
користуватись т-тестом. З цього випливає те, що подальша перевірка гіпотези рівності
дисперсій не має сенсу - будемо користуватись тестом Мана-Вітні

wilcox_test(delivery_date~category_name, data = d_125)

Показник р становить 0,0853, що говорить про де-юре принняття нами нульової гіпотези,
котра звучить так - швидкіть перевезення куплених товарів не залежить від того, яка
це категорія товару - напої чи морепродукти. Де факто ж показник є надзвичайно
близький до рівня статистисної значущості в 0,05. Тому надалі ми повинні моніторити
дану метрику, бо вона може стати статистично значущою. Перейдемо до візуального
аналізу даної метрики на боксплоті

boxplot(delivery_date~category_name, data = d_125)

Так. інтерквартильний розмах та розмах між вусами боксплотів є дещо ширшим по групі
напої, ніж по групі морепродукти. Що стосується екстремальних значеннь, то їх 
приблизно однакова кількість в обох групах і розмах приблизно рівний. В той час
медіани по обом групам знаходяться приблизно на одному рівні. Перейдемо до аналізу
конкретних середніх та медіанних значеннь кількості днів доставки по данним групам.

d_125_clean <- d_125 %>% filter(!is.na(delivery_date))

d_125_clean %>% group_by(category_name) %>% 
  summarise(mean = mean(delivery_date), median = median(delivery_date))

так, середня кількість днів доставки по групам напої та морепродукти становить
8,58 та 7,86 днів. Тобто, в абсолютних значеннях різниця між середніми становить
0,72 дні, в той час як в відносних показниках різниця становить 9,16%. Тобто,
в середньому товари по групі напої доставляються на 9,16% повільніше, ніж по групі
морепродукти. По медіанним значенням у нас паритет - доставка триває 7 днів.
Перейдемо до розрахунку розміру ефекту

wilcox_effsize(delivery_date~category_name, data = d_125)

Коефіцієнт становить 0,0648, що говорить нам про слабкий розмір ефекту. Тобто. всього
6,48% варіацій днів доставки пояснюються різницями між групами. В прикладному ж 
плані даний показник не є значущим для бізнесу.

Отже, в ході нашого аналізу двох груп - морепродукти та напої ми виявили, що 
лише 1 з 7 ключових показхників має статистично значущу різницю між групами.
Це ціни на куплені товари. Як це пояснити? Досить просто. В середньому ціни
на напої дуже сильно вищі, ніж по морепродуктам, проте так як ми використовували
тест Мана-Вітні, бо дані розподілені не нормально, то медіанні ціни були вже
вищими по групі морепродукти. Є ще один показник, який в майбутньому може стати
статистично значущим. Це швидкість доставки товарів. Проте поки що він є не 
статистично значущим. В прикладному ж сенсі дані метрики є не значущими для бізнесу,
бо мають слабкий розмір ефекту.

