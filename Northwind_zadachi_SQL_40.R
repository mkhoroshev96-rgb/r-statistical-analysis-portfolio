Shapiro + histogram для кожної метрики — дуже правильно.

Консистентне застосування Wilcoxon (Mann–Whitney) — обґрунтовано через порушення
нормальності.
Використання wilcox_effsize — супер. Дуже корисно для прикладної оцінки ефекту.
Порівняння середніх та медіан з аналітичним контекстом — круто, це сильно підсилює
висновки.
Узагальнення в кінці — чітко відбито результат: лише одна значуща відмінність 
(ціна), і навіть вона не важлива для бізнесу через малий ефект.

