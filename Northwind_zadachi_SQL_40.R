# Симуляція потужності Mann–Whitney тесту
sim_power_mw <- function(n1, n2, d, reps = 1000, alpha = 0.05) {
  count <- 0
  for (i in 1:reps) {
    x <- rnorm(n1, mean = 0, sd = 1)
    y <- rnorm(n2, mean = d, sd = 1)  # зміщуємо середнє другої групи
    pval <- wilcox.test(x, y)$p.value
    if (pval < alpha) count <- count + 1
  }
  return(round(count / reps, 4))
}

