Таблиці: orders, order_details, products, categories, suppliers
Зв’язки: orders ↔ order_details ↔ products ↔ categories ↔ suppliers
Вибірка: category_name = "Beverages"; постачальники з USA та Germany.
Одиниця аналізу: рядок замовлення (товарна позиція).

Метрики (рівень позиції):
unit_price — ціна;
quantity — кількість;
discount — знижка (0–1);
chek = unit_price × quantity × (1 − discount) — вартість позиції;
freight — вартість доставки (рівень замовлення);
freight_ratio = freight / chek — частка доставки в чеку;
delivery_date = shipped_date − order_date (дні);
daily_freight_per_unit = freight / (delivery_date × quantity).

Завдання:
Порівняти вказані метрики між USA і Germany у категорії Beverages; перевірити 
нормальність (Shapiro–Wilk) і гомогенність дисперсій (Levene); застосувати 
відповідний тест (Welch t-test або Mann–Whitney); за потреби — трансформації 
(Box–Cox / Yeo–Johnson / log). Подати розміри ефектів (Cohen’s d / Cliff’s δ) і 
коротку бізнес-інтерпретацію.

Рішення

SELECT category_name
,s.country
,p.unit_price
,quantity
,discount
,ROUND((p.unit_price * quantity * (1-discount))::numeric,2) AS chek
,freight
,ROUND((freight/(p.unit_price * quantity * (1-discount)))::numeric,4) as freight_ratio
,(shipped_date - order_date) as delivery_date
,ROUND((freight/(shipped_date - order_date)/quantity)::numeric,4) as daily_freight_per_unit
FROM orders 
JOIN order_details USING(order_id)
JOIN products p USING(product_id)
JOIN categories USING(category_id)
JOIN suppliers s USING(supplier_id)
WHERE category_name = 'Beverages' AND s.country IN ('USA', 'Germany')

