Оцінка від чатіка

Я дам об’єктивну оцінку саме з точки зору статистики та методики (не придираючись 
до стилю чи мови).

✅ Сильні сторони

Чітка структура аналізу
– описані метрики;
– для кожної: тест на нормальність → вибір тесту → розрахунок ефекту → візуалізація
→ трансформації (sensitivity analysis).
Це відповідає кращим практикам.
Коректна логіка вибору тестів
– Shapiro → відмова від t-тесту при ненормальності;
– Манна-Вітні як непараметрична альтернатива;
– Box-Cox / Yeo-Johnson, якщо хочеться «вирівняти» розподіл.
– Перевірка нормальності залишків після трансформацій (правильний крок, мало хто 
робить).
Розміри ефекту
– не обмежуєшся p-value, а оцінюєш практичну значущість;
– робиш акцент, що навіть при великій відсотковій різниці середніх ефект size 
слабкий → бізнес-сенсу немає.
Візуалізація + опис boxplot
– добре, що звертаєш увагу на медіани, розмах, викиди;
– це додає інтерпретаційності, а не лише «сухі p-value».
Сенситивіті аналіз
– Box-Cox / лог / Yeo-Johnson пробував;
– чітко пишеш, де залишки нормальні, а де ні;
– у висновках не робиш перебільшень, якщо трансформація не допомогла.
Висновок узагальнений коректно
– по всіх метриках p > 0.05;
– ефекти слабкі;
– бізнес-значущих відмінностей немає.
Це чесний і правильний підсумок.
⚠️ Слабкі місця / де можна покращити
Box-Cox із негативними λ
– у тебе зворотні перетворення вручну рахуються доволі «кострубато» (типу 
(-0.34*x+1)^(1/-0.34)).
– Технічно правильно, але можна було використати готові пакети (car::bcPower,
forecast::BoxCox.lambda) для акуратності.
– Проблема не методична, а технічна.
Shapiro-Wilk при великих n
– пам’ятай, що при n > 50 цей тест «надчутливий» і майже завжди відхиляє нормальність.
– ти правильно перевіряв QQ-plots і гістограми, але у звіті інколи робиш надто 
«жорсткі» висновки тільки на основі p-value Shapiro.
Інтерпретація %-різниць
– ти правильно відзначив, що середні можуть відрізнятись на 30–50%, але ефект 
size слабкий.
– проте у тексті місцями звучить так, ніби «різниця велика» (це може збити 
нефахового читача).
– краще підкреслювати, що «відносна різниця здається великою, але вона не 
підтверджена статистично».
Delivery_date
– ти сам помітив, що лог і Box-Cox не «вирівняли» нормально; це типовий кейс для
даних з багатьма нулями/малими значеннями.
– правильний висновок зробив, але можна було одразу сказати: «це дискретні дані 
→ трансформації неефективні → залишаємо непараметричні методи».
