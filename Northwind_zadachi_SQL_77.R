База: Northwind
Таблиці: orders, order_details, products, categories, employees
Зв’язки: за order_id, product_id, category_id, employee_id
Вибірка: category_name = 'Beverages', last_name IN ('Davolio','Fuller')
Одиниця аналізу: товарна позиція (рядок замовлення)

Метрики:
unit_price — ціна товару;
quantity — кількість одиниць;
discount — знижка;
chek = unit_price * quantity * (1 - discount) — вартість позиції з урахуванням 
знижки;
freight — вартість доставки (на рівні замовлення, віднесена до позиції як у вибірці);
freight_ratio = freight / chek — частка доставки у чеку;
delivery_date = shipped_date − order_date (дні);
daily_freight_per_unit = freight / (delivery_date * quantity) — щоденні витрати 
доставки на одиницю.
Мета: перевірити, чи існують статистично значущі відмінності між Davolio та Fuller
за кожною метрикою.
Алгоритм:
Перевірка якості даних: вилучити рядки з NULL датами, delivery_date <= 0, quantity
<= 0 (щоби не ділити на нуль у похідних метриках).
Для кожної метрики:
тест на нормальність (Shapiro по групах + огляд QQ/гістограм);
вибір тесту: t‑тест (за нормальності/наближення) або Манна–Вітні;
оцінка розміру ефекту (Hedges g/Cohen’s d або r для Манна–Вітні);
візуалізація (boxplot).
Sensitivity analysis: якщо розподіли далеко від нормальних — спробувати Box‑Cox
(або Yeo‑Johnson, якщо є нулі) для цільової метрики; повторити тест на трансформовани
х даних і перевірити залишки.
Порог значущості: α = 0.05. Обов’язково інтерпретувати практичну значущість 
(навіть за великої відносної різниці середніх).
Очікуваний вихід: таблиця з p‑value, effect size та короткими висновками по кожні
й метриці; стислий executive‑summary (1 абзац) про наявність/відсутність бізнес
‑значущих відмінностей.

Рішення

SELECT category_name
,last_name
,p.unit_price
,quantity
,discount
,ROUND((p.unit_price * quantity * (1-discount))::numeric,2) as chek
,freight
,ROUND((freight/(p.unit_price * quantity * (1-discount)))::numeric,4) as freight_ratio
,shipped_date - order_date as delivery_date
,ROUND((freight/(shipped_date - order_date)/quantity)::numeric,4) as daily_freight_per_unit
FROM orders
JOIN order_details USING(order_id)
JOIN products p USING(product_id)
JOIN categories USING(category_id)
JOIN employees USING(employee_id)
WHERE category_name = 'Beverages' AND last_name IN ('Davolio', 'Fuller')

