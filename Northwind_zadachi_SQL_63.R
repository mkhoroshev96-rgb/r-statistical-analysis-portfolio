📌 Альтернативна бізнес-інтерпретація
1998 рік – період структурної перебудови. Компанія, ймовірно, змінила асортимент 
(додала дешевші товари або інші SKU), але не знизила кількість продажів.
Проте логістика стала дорожчою у відносних величинах. Це означає, що частка дрібних
або географічно розкиданих замовлень зросла, і компанія втратила ефект “оптової”
економії.
Ціни виглядають підозріло, і я б тут не робив глибоких висновків, доки не перевірю
таблицю products (чи ті самі товари в 1997 і 1998). Є великий шанс, що ми
порівнюємо “яблука з апельсинами”.
Фінансові показники просіли через логістику, а не попит. Продажі стабільні, але 
логістика з’їла маржу. Це типова проблема, коли бізнес розширює географію без 
перегляду тарифів і логістичних схем.

✅ Що б я зробив далі
Перевірив структуру асортименту:
чи не з’явилися дешевші SKU у 1998,
чи однаковий розподіл продуктів у обох роках.
Подивився географію доставок:
чи не стало більше далеких напрямків,
чи не змінився перевізник (ship_via).
Перерахував метрики без екстремальних замовлень, щоб зрозуміти, чи це не 2–3 
аномальних кейси перекрутили середні/медіани.
Оцінив середній розмір замовлення – можливо, клієнти почали робити менші партії, 
що автоматично робить доставку дорожчою в розрахунку на 1 товар.

🧩 Мій короткий альтернативний висновок
Компанія Refrescos у 1998 році не втратила попит, але змінила структуру продажів
(можливо, додавши дешевші товари), що призвело до падіння середнього чеку. При 
цьому логістика подорожчала як у відносних метриках, так і в абсолюті, ймовірно,
через зміну маршрутизації або тарифів перевізників. Загальний бізнес не “просів”,
але став менш маржинальним. Перш ніж робити остаточні висновки, потрібно перевірити
структуру продуктів та географію доставок.