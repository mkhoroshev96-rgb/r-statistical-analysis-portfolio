🎯 Мета дослідження
Проаналізувати, чи змінилася структура замовлень по категорії "Condiments" 
(приправи) до та після медіанної дати оформлення замовлень.

🛠 Алгоритм вибірки
Відібрано всі товарні позиції, що належать до категорії Condiments.
Для кожної позиції розраховано:
  
unit_price — ціна одиниці товару;
quantity — кількість одиниць товару;
discount — розмір знижки;
freight — вартість доставки;
freight_ratio — відношення доставки до сумарної вартості замовлення з урахуванням
знижки;
chek — сумарна вартість замовлення з урахуванням знижки;
delivery_date — кількість днів доставки;
daily_freight_per_unit — вартість доставки за одиницю на день.

Усі замовлення категорії Condiments розділено на дві групи:
  
Домедіанна група — оформлені до медіанної дати;
Післямедіанна група — оформлені після медіанної дати.

