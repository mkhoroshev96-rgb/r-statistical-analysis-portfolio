🎯 Порівняльний аналіз логістичних і фінансових характеристик між категоріями 
“Dairy Products” та “Grains/Cereals”

📦 База даних: Northwind
📊 Таблиці: orders, order_details, products, categories
🔗 Об’єднання: через order_id, product_id, category_id

🎯 Мета дослідження:
  Визначити, чи існують статистично значущі відмінності між молочними продуктами
та зерновими/крупами за логістичними й фінансовими показниками.

🍶🥣 Порівнювані категорії:
Dairy Products — молочна продукція
Grains/Cereals — зернові та крупи

📐 Метрики для аналізу (на рівні товарної позиції в замовленні):
unit_price — ціна одиниці товару
quantity — кількість куплених одиниць
discount — знижка
freight — витрати на доставку
chek — сума позиції зі знижкою
freight_ratio — частка доставки у сумі (freight / chek)
delivery_date — тривалість доставки (у днях)
daily_freight_per_unit — щоденна вартість доставки однієї одиниці товару 
(freight / delivery_date / quantity)

