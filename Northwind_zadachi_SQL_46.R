Рішення

WITH cte as(SELECT category_name
            ,order_date
            ,p.unit_price
            ,quantity
            ,discount
            ,freight
            ,ROUND((freight/(p.unit_price * quantity *(1-discount)))::numeric,2) as freight_ratio
            ,ROUND((p.unit_price * quantity *(1-discount))::numeric,2) as chek
            ,(shipped_date - order_date) as delivery_date
            ,ROUND((freight / (shipped_date - order_date) / quantity)::numeric,2) as daily_freight_per_unit
            FROM orders
            JOIN order_details USING(order_id)
            JOIN products p USING(product_id)
            JOIN categories USING (category_id)),
median as(SELECT *
            ,ntile(2) over(order by order_date) as median
          FROM cte
          WHERE category_name = 'Condiments')
SELECT *
  ,case when median = 1 THEN 'Домедіанна група'
when median = 2 THEN 'Післямедіанна група'
END as category_groups
FROM median

