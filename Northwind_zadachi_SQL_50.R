Аналіз категорії "Beverages" до та після медіанної дати
📦 База даних: Northwind
🧾 Таблиці: orders, order_details, products, categories
🔗 Об’єднання: через order_id, product_id, category_id

🎯 Мета дослідження
Проаналізувати, чи відбулися статистично значущі зміни у структурі замовлень 
товарів категорії "Beverages" (напої) до та після медіанної дати оформлення 
замовлення.

📐 Метрики для аналізу
(розраховані на рівні кожної товарної позиції з order_details):
  
unit_price — ціна одиниці товару
quantity — кількість одиниць товару
discount — знижка (%)
chek — підсумкова сума позиції з урахуванням знижки
freight — загальна вартість доставки (на рівні всього замовлення)
freight_ratio — частка доставки у чеку (freight / chek)
delivery_date — тривалість доставки (у днях)
daily_freight_per_unit — добова вартість доставки одиниці товару
(freight / (shipped_date - order_date) / quantity)

🛠 Алгоритм вибірки
Відібрані усі товарні позиції категорії "Beverages".
Для кожної позиції обчислені вищевказані метрики.
Усі записи поділено на 2 групи за медіанною датою замовлення:
  
Домедіанний період — order_date < медіани;
Післямедіанний період — order_date ≥ медіани.

