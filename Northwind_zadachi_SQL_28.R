Задача 135

Порівняльний аналіз клієнтської поведінки в Австрії та Швеції
База даних:
  Northwind — містить таблиці orders та order_details, з’єднані через order_id.

Мета:
  Порівняти структуру замовлень між двома європейськими країнами — Австрією та 
Швецією, щоб з’ясувати, чи існують статистично значущі відмінності в основних 
показниках клієнтської поведінки.

Одиниця аналізу:
  Одне замовлення (order_id).

Методи відбору:
  Враховуються лише ті замовлення, у яких значення ship_country дорівнює 'Austria'
або 'Sweden'.

Метрики для аналізу (усереднені на рівні замовлення):
  avg_price — середня ціна одиниці товару в межах одного замовлення;

avg_quantity — середня кількість товару в межах одного замовлення;

avg_discount — середній рівень знижки;

avg_freight — середня вартість доставки;

avg_delivery_date — середній час доставки у днях (shipped_date - order_date);

avg_chek — середній чек за замовленням (з урахуванням знижки):
  quantity * unit_price * (1 - discount).

Рішення

SELECT ship_country
,order_id
,order_date
,shipped_date
,ROUND(AVG(unit_price)::numeric,2) as avg_price
,ROUND(AVG(quantity)::numeric,2) as avg_quantity
,ROUND(AVG(discount)::numeric,2) as avg_discount
,ROUND(AVG(freight)::numeric,2) as avg_freight
,ROUND(AVG(shipped_date - order_date)::numeric,0) as avg_delivery_date
,ROUND(AVG(quantity * unit_price * (1-discount))::numeric,2) as avg_chek
From orders
Join order_details USING(order_id)
where ship_country in ('Austria', 'Sweden')
GROUP by ship_country,order_id

