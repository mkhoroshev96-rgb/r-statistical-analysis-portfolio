Задача 133
A/B тестування по країнах з урахуванням обсягу замовлень
База даних:
Northwind — містить таблиці orders і order_details, об’єднані по order_id.

Мета:
Перевірити, чи існують статистично значущі відмінності в ключових метриках замовлень
між країнами з високим і низьким обсягом замовлень (на основі кількості замовлень
по країні), незалежно від конкретної країни.

Метод розподілу груп:
  
Для кожної країни обчислюється загальна кількість замовлень (count_order_country).

Обчислюється медіана кількості замовлень серед усіх країн.

Країни, де кількість замовлень менше або дорівнює медіані, потрапляють у групу A (before).

Країни, де кількість замовлень більше медіани, потрапляють у групу B (after).

Одиниця аналізу:
Одне замовлення (order_id).

Метрики для аналізу (розраховані на рівні замовлення):
  
avg_price — середня ціна товару;
avg_quantity — середня кількість одиниць;
avg_discount — середній розмір знижки;
avg_freight — вартість доставки;
avg_delivery_date — час доставки (у днях);
avg_chek — середній чек (обсяг замовлення з урахуванням знижок)

Рішення

with cte as(SELECT ship_country
            ,order_id
            ,order_date
            ,shipped_date
            ,ROUND(AVG(unit_price)::numeric,2) as avg_price
            ,ROUND(AVG(quantity)::numeric,2) as avg_quantity
            ,ROUND(AVG(discount)::numeric,2) as avg_discount
            ,ROUND(AVG(freight)::numeric,2) as avg_freight
            ,ROUND(AVG(shipped_date - order_date)::numeric,0) as avg_delivery_date
            ,ROUND(AVG(quantity * unit_price * (1-discount))::numeric,2) as avg_chek
            FROm orders
            Join order_details USING (order_id)
            Group by ship_country,order_id),
count_and_runk as(SELECT *
                    ,row_number() over(partition by ship_country order by order_date) as within_runk
                  ,count(*) over(partition by ship_country) as count_order_country
                  FROM cte),
mediana as(SELECT *
             ,(SELECT percentile_cont(0.5) within group(order by count_order_country) FROM count_and_runk) as mediana
           FROM count_and_runk)
SELECT *
  ,case
when count_order_country <= mediana Then 'before'
when count_order_country > mediana Then 'After'
END as median_group
FROM mediana
Order by median_group

