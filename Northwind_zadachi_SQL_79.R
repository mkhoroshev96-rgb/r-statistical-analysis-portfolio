– Є чіткий науковий pipeline: нормальність → вибір тесту → ефект-сайз → візуалізація
→ сенситивіті аналіз → інтерпретація.
Правильне використання статистики
– Не залишився на рівні p-value, а перевірив ефекти (Cohen’s d, rank-biserial, 
Cliff’s δ).
– Врахував чутливість Шапіро-тесту на великих вибірках і не робив «сліпих» 
висновків.
– Додав Box-Cox / Yeo-Johnson / log, показав, як трансформації змінюють розподіл,
але не змінюють кінцевий висновок. Це дуже грамотний «sensitivity check».
Глибина бізнес-інтерпретації
– Ти не просто написав «р > 0.05», а пояснив, що різниця «номінальна, але статистично
незначуща».
– Показав у відносних величинах (наприклад, +10% ціни у Peacock), але зазначив, 
що для бізнесу це не суттєво.
– Фінальний висновок чіткий: немає підстав перерозподіляти замовлення між Leverling
та Peacock.
Методологічна чистота
– Ти не змішав висновки з Box-Cox/логом із основними тестами, а чесно написав, 
що вони – додатковий сенситивіті аналіз.
– Кожного разу перевірив залишки у моделях (Shapiro + QQplot). Це дуже високий 
рівень уважності.

⚠️ Моменти для покращення
Перевантаженість тексту
– Дуже багато деталей (кожен Shapiro, кожна гістограма). Це круто для «навчального
конспекту», але для «презентації результатів» бізнесу воно виглядає надто довгим.
– У підсумковому варіанті можна було б винести все у таблицю (метрика – тест – p
– ефект – висновок) і короткі 2–3 абзаци коментаря.
Зворотні перетворення (Box-Cox)
– Ти правильно зробив обчислення «назад», але місцями доволі складно інтерпретувати.
У бізнес-звіті краще давати вже готові середні в оригінальних одиницях, без показу 
«(-0.26*... +1)^(1/-0.26)».
Yeo-Johnson на знижках
– Тут воно виглядало трохи «зайво», бо різниці не було видно навіть до трансформацій.
Тобто, сенситивіті аналіз варто було обмежити на 2–3 ключових метриках (ціна, чек,
delivery_date).
