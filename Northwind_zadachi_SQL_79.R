🧩 Одиниця аналізу: товарна позиція (рядок замовлення)

📌 Метрики (на рівні позиції):
unit_price — ціна товару;
quantity — кількість одиниць;
discount — знижка (0–1);
chek = unit_price * quantity * (1 − discount) — вартість позиції із знижкою;
freight — вартість доставки (з рівня замовлення, віднесена до позиції як у вибірці);
freight_ratio = freight / chek — частка доставки в чеку;
delivery_date = shipped_date − order_date (днів) — тривалість доставки;
daily_freight_per_unit = freight / (delivery_date * quantity) — добові витрати
доставки на одиницю.
🧹 Якість даних:
виключити позиції з відсутніми датами;
delivery_date > 0;
chek > 0, quantity > 0;
для часток/ділень уникати ділення на нуль.
🧪 Гіпотези (для кожної метрики окремо):
H0: розподіли метрики однакові у Peacock та Leverling.
H1: відрізняються.
🔬 План аналізу:
Описова статистика по групах (mean, median, IQR, n).
Перевірка нормальності в кожній групі (Shapiro-Wilk) → вибір тесту:
нормальність/однорідність дисперсій: t‑тест (Welch при неоднорідних);
інакше — Mann–Whitney (wilcox_test).
Розмір ефекту:
Cohen’s d (t‑тест) або rank-biserial / Cliff’s δ (для U‑тесту).
Сенситивіті та нормалізація (за потреби):Box–Cox для додатних метрик (unit_price,
quantity, chek, freight, freight_ratio, daily_freight_per_unit);
Yeo–Johnson — якщо можливі нулі;
перевірка залишків (Shapiro, QQplot) у лінійних моделей на трансформованих даних.
Візуалізації: boxplot/violin + медіани; для лог‑шкал — вказати це на графіку.
Інтерпретація у бізнес-логіці: якщо різниця в ціні/знижках не супроводжується 
змінами у chek, freight_ratio чи delivery_date — зазначити низьку прикладну 
значущість.
