Задача 144

A/B тест до та після медіанної дати для замовлень, оброблених конкретним працівником

База даних:
Northwind — таблиці orders, order_details, employees, пов’язані через order_id та
employee_id.

Мета дослідження:
Перевірити, чи змінилася структура замовлень, оброблених працівником Davolio, 
після медіанної дати оформлення замовлень, які він обробляв.

Алгоритм:
Для кожного замовлення (order_id), обробленого Davolio, розраховано такі метрики:
  
avg_price — середня ціна одиниці товару;
avg_quantity — середня кількість товару;
avg_discount — середній відсоток знижки;
avg_freight — середня вартість доставки;
median_order_value — медіанна вартість товарних позицій;
avg_chek — середній чек (сума з урахуванням знижки);
avg_delivery_date — середній час доставки (у днях).

Всі замовлення сортуються за order_date, і за допомогою NTILE(2) розділяються на
дві групи:
  
before_group — перша половина замовлень (до медіанної дати),
after_group — друга половина замовлень (після медіанної дати).

Одиниця аналізу:
Одне замовлення (order_id), оброблене працівником Davolio.

Ціль аналізу:
Порівняти групи before_group та after_group за ключовими метриками, визначити 
наявність статистично значущих змін у поведінці замовлень із часом.

