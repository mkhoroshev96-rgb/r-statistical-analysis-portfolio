📊 Метрики для порівняння:
unit_price — ціна одиниці товару;
quantity — кількість купленого товару;
discount — розмір знижки;
freight — вартість доставки (на рівні замовлення);
avg_chek — вартість товарної позиції з урахуванням знижки;
avg_delivery_date — тривалість доставки (у днях).

Рішення

WITH cte as(SELECT  category_name
            ,p.unit_price
            ,quantity
            ,discount
            ,freight
            ,ROUND((quantity * p.unit_price * (1-discount))::numeric,2) as avg_chek
            ,(shipped_date - order_date) as avg_delivery_date
            ,count(*) over(partition by category_name) as count_products_in_category
            FROM orders
            JOIN order_details USING(order_id)
            JOIN products p USING (product_id)
            JOIN categories USING(category_id)),
den_ran as(SELECT *
             , dense_rank() over(order by count_products_in_category DESC) as dens_rank
           FROm cte)
SELECT *
  FROM den_ran
where category_name in ('Seafood', 'Confections')

