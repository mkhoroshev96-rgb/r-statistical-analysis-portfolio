with cte as(SELECT  ship_via
            ,category_name
            ,p.unit_price
            ,quantity
            ,discount
            ,freight
            ,ROUND((quantity * p.unit_price * (1-discount))::numeric,2) as chek
            ,(shipped_date - order_date) as delivery_date
            FROM orders
            JOIN order_details USING(order_id)
            JOIN products p USING(product_id)
            JOIN categories USING(category_id))
SELECT *
  ,case
when ship_via = 1 THEN 'first_shipper'
when ship_via = 2 THEN 'second_shipper'
when ship_via = 3 THEN 'third_shipper'
END as shipper_group
FROM cte
where category_name = 'Dairy Products'
Order by shipper_group

