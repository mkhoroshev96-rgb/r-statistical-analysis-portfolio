  avg_price — середня ціна одиниці товару в межах одного замовлення;

avg_quantity — середня кількість одиниць товару;

avg_discount — середній рівень знижки;

avg_freight — середня вартість доставки;

avg_delivery_date — середня тривалість доставки в днях (shipped_date - order_date);

avg_chek — підсумкова вартість замовлення з урахуванням знижки:
  quantity * unit_price * (1 - discount)

Рішення

SELECT ship_country
,order_id
,order_date
,shipped_date
,ROUND(AVG(unit_price)::numeric,2) as avg_price
,ROUND(AVG(quantity)::numeric,2) as avg_quantity
,ROUND(AVG(discount)::numeric,2) as avg_discount
,ROUND(AVG(freight)::numeric,2) as avg_freight
,ROUND(AVG(shipped_date - order_date)::numeric,0) as avg_delivery_date
,ROUND(AVG(quantity * unit_price * (1-discount))::numeric,2) as avg_chek
FROm orders
Join order_details USING (order_id)
where ship_country in ('Germany', 'USA')
GROUP BY ship_country,order_id
Order by ship_country

