Задача 130. 
A/B тестування поведінки клієнтів з Аргентини та Бразилії

База даних: Northwind — містить інформацію про замовлення (orders) та деталі 
замовлень (order_details).
Мета: визначити, чи існують статистично значущі відмінності у поведінці клієнтів
з Аргентини (група A) та Бразилії (група B) щодо замовлень.
Умови експерименту (розбиття на групи):
  
Група A: усі замовлення, доставлені до Argentina
Група B: усі замовлення, доставлені до Brazil

Одиниця аналізу: одне замовлення (order_id)

Метрики для порівняння (з обчисленням середніх значень по замовленню):
  
avg_price: середня ціна одиниці товару в замовленні
avg_quantity: середня кількість товару
avg_discount: середній рівень знижки
avg_freight: вартість доставки
avg_delivery_date: тривалість доставки (днів)
avg_chek: підсумкова вартість замовлення з урахуванням знижки

Рішення

Нарешті ми прийшли до цієї точки - А/В тестування. Будемо аналізувати поки що
досить схожі країни між собою по ключовим показникам. Почнемо).

SELECT ship_country
,order_id
,order_date
,shipped_date
,ROUND(AVG(unit_price)::numeric,2) as avg_price
,ROUND(AVG(quantity)::numeric,2) as avg_quantity
,ROUND(AVG(discount)::numeric,2) as avg_discount
,ROUND(AVG(freight)::numeric,2) as avg_freight
,ROUND(AVG(shipped_date - order_date)::numeric,0) as avg_delivery_date
,ROUND(AVG(quantity * unit_price * (1-discount))::numeric,2) as avg_chek
FROm orders
Join order_details USING (order_id)
where ship_country in ('Argentina', 'Brazil')
GROUP BY ship_country,order_id

