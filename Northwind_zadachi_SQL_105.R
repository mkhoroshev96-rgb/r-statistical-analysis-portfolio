Коефіцієнт дорівнює 0,13, що значить - ефект є дуже малим. Тобто, (0.13+1)/2=
0,565 - в 56,5% парних порівняннь дана метрика є вищою по Канаді і відповідно в
43,5% - по Великій Британії. Довірчі інтервали включають в себе 0, що значить,
що ефект не є стійким. Тобто, добова вартість доставки одиниці товару по суті 
не є вищою по жодній з країн, а тому й різниця між ними не має практичного сенсу

Нормалізація даних та сенситивіті аналіз

boxcox(lm(daily_freight_per_units~country, data = d_223))

Отже, наша оптимальна лямбда є зміщеною дещо вправо від 0, в той час як довірчі
інтервали включають в себе 0. Тобто, це значить, що ми можемо скористатися обома
способами перетворення даних, але ми все ж оберемо логарифмування. Перейдемо
до створення нової змінної

d_223$log_daily <- log(d_223$daily_freight_per_units)

Отже, дана нова змінна покликана виправити асиметрію та стабілізувати дисперсію.
Чи так це? На перший пункт нам дасть відповідь Шапіро-тест

shapiro.test(d_223$log_daily)

Показник р становить 0,08404, що значить, що наші дані розподілені нормально
після трансформації. На другий пункт нам дасть відповідь перевірка гіпотези
рівності дисперсій

levene_test(log_daily~country, data = d_223)

Отже, показник р дорівнює 0,136, що вказує на те, що дисперсії є рівними. Тобто,
ми будемо використовувати т-тест, але без поправки на нерівність дисперсій.
Перейдемо до побудови моделі

m8 <- lm(log_daily~country, data = d_223)
summary(m8)

Середня лог нормалізована добова вартість доставки одиниці товару по Канаді
дорівнює -0,7036, що в зворотній трансформації дорівнює exp(-0.7036)=0.495. В
той же час, середня лог нормалізована різниця в добових вартостях доставки одиниці
товару між Канадою та Великою Британією дорівнює -0,3192, що в зворотній трансформації
становить exp(-0.3192)=0.7267. Тобто, середня нормалізована добова вартість доставки
одиниці товару по Великій Британії дорівнює 0.495 * 0.7267=0.360. В той же час,
в відносних величинах дана середня нормалізована метрика є вищою по Канаді на
37,5%. Показник р становить 0,2258, що вказує нам на те, що немає статистично
значущої різниці між цими країнами по цій метриці. Розрахуємо розмір ефекту

cohens_d(log_daily~country, data = d_223)

Наш коефіцієнт становить 0,21, що говорить про те, що наш ефект є малим. Довірчі
інтервали включають в себе 0, що значить, що розмір ефекту не є стійким. Таким
чином, дана нормалізована метрика не має суттєвого прикладного значення

shapiro.test(residuals(m8))

Показник р становить 0,192, що значить, що наші залишки розподілені нормально
після трансформації. Перейдемо до візуального розподілу залишків на лінійній
моделі

qqnorm(residuals(m8)); qqline(residuals(m8))

Отже, наші залишки розподілені нормально на даній моделі. Крім того, відхилення
присутні та зосередженні в хвостах. Але так як вони є не суттєвими та не численними
вони не ламають нашої нормальності. Тобто, обидва тести вказують нам на один і той
же ключовий висновок, а значить - вони є релнвантними по цій метриці

Висновок

📊 Ключові результати

Суттєво значущі відмінності спостерігаються лише у трьох метриках:
Unit price — Канада має набагато вищі ціни.
Check (вартість замовлення) — Канада має середній чек у 3–4 рази вищий.
Freight ratio — Велика Британія має значно більшу частку доставки у чеку.
Інші показники (quantity, discount, freight, delivery_date, daily_freight_per_units)
не відрізняються статистично.

💡 Бізнес-інсайти
Канада: працює у преміальному сегменті — високі ціни та чеки, помірна логістика. 
Доцільно розвивати стратегію цінового лідерства (premium-pricing) і консолідувати
великі поставки.
UK: має нижчі ціни, але логістика дорожча відносно вартості замовлення, що знижує
маржу. Варто оптимізувати витрати на доставку (контракти з перевізниками, об’єднання
поставок).
Акції та знижки не є фактором конкуренції — тут паритет.
Доставка подібна за тривалістю, отже, логістична швидкість не є слабким місцем 
жодної зі сторін.

⚙️ Аналітична оцінка
Найвищі p-value (> 0.2) — стабільні відсутності ефектів.
Сильні ефекти (|d| ≥ 0.8 / |δ| ≥ 0.5) — лише за ціною та чеком.
Висновки узгоджуються між непараметричними та параметричними тестами після 
трансформацій (Box–Cox, log).
Усі моделі мають нормально розподілені залишки.

