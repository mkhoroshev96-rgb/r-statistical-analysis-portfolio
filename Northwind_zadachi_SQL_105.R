Порівняння UK vs Canada у категорії Confections (Northwind)

R-пакети: car, rstatix, dplyr, effectsize, MASS.
Джерело даних: orders ↔ order_details ↔ products ↔ categories ↔ suppliers.
SQL-вибірка: category_name = 'Confections', постачальники з UK та Canada
(див. наданий запит; одиниця аналізу — рядок замовлення / товарна позиція).

Метрики (на рівні позиції)
unit_price, quantity, discount
chek = unit_price × quantity × (1 − discount)
freight
freight_ratio = freight / chek
delivery_date = shipped_date − order_date (у днях)
daily_freight_per_units = freight / (delivery_date × quantity)

Мета
Порівняти всі метрики між UK та Canada з коректним вибором статистичних тестів,
трансформацій та оцінкою розміру ефекту. Надати підсумкову таблицю й короткі
бізнес-висновки/рекомендації.

Перевірки припущень
Нормальність у групах: Shapiro–Wilk.
Гомогенність дисперсій між групами: Levene.

Вибір тесту
Є нормальність і гомогенність → класичний t-test.
Є нормальність, але дисперсії різні → Welch t-test.
Інакше → Mann–Whitney (Wilcoxon rank-sum).
За констант/відсутності варіації — тест не застосовувати, зробити описовий 
висновок.

Трансформації (за потреби)
Box–Cox / Yeo–Johnson / log.
Для discount дозволений малий shift при нулях.
Після трансформацій повторити Shapiro–Wilk і Levene; для моделей — перевірити
залишки (QQ-плот + Shapiro).

Розмір ефекту
Для t-тестів: Cohen’s d з 95% CI.
Для непараметричних: Cliff’s δ з 95% CI; додатково (за бажанням) Hodges–Lehmann
(різниця медіан) з CI.

Вивід (зведена таблиця)
метрика | Canada mean/median | UK mean/median | тест | p-value | ефект
Бізнес-висновки та рекомендації
Інтерпретувати різниці в ціні/знижках/чеку/логістиці (рівні, частки, швидкість).
Дати короткі actionable поради для UK та Canada (ціноутворення, акції, логістика,
пороги, консолідація тощо).

Крайові випадки та обробка даних
Константні unit_price у групі → тест не застосовується; робимо описовий висновок
і фіксуємо обмеження.
Нульові discount → використовувати лог-трансформацію лише зі shift.
Відсутні або нульові delivery_date → виключити з розрахунку daily_freight_per_units
і freight_ratio з обґрунтуванням (уникнути ділення на нуль/NA).
Аутлаєри: залишити, але відобразити їх вплив через робастні тести/медіани; за 
потреби — чутливість аналізу без екстремальних значень (додаток).
Результатом має бути табличний підсумок, короткі бізнес-інсайти та точкові 
рекомендації для обох країн.

Рішення

SELECT category_name
,s.country
,p.unit_price
,quantity
,discount
,ROUND((p.unit_price * quantity * (1-discount))::numeric,2) as chek
,freight
,ROUND((freight/(p.unit_price * quantity * (1-discount)))::numeric,4) as freight_ratio
,(shipped_date - order_date) as delivery_date
,ROUND((freight/(shipped_date - order_date)/quantity)::numeric,4) as daily_freight_per_units
FROM orders
JOIN order_details USING(order_id)
JOIN products p USING(product_id)
JOIN categories USING (category_id)
JOIN suppliers s USING(supplier_id)
Where category_name = 'Confections' AND s.country IN ('UK', 'Canada')

