Висновки

У результаті аналізу ключових бізнес- та логістичних метрик замовлень категорії
«Confections» для постачальників з Germany та UK виявлено такі підсумки.

Статистично значущі відмінності
Ціна (unit_price): істотно вища у Germany (MW p ≪ 0.001; Cliff’s δ ≈ 0.75 → 
дуже великий ефект; ≈87.5% парних порівнянь на користь Germany).
Чек позиції (chek): вищий у Germany (MW p ≈ 4.5e-05; δ ≈ 0.37 → середній ефект).
Лог-модель підтвердила різницю (~удвічі більші геометричні середні у Germany).
Частка доставки у чеку (freight_ratio): вища у UK (MW p ≈ 0.0035; δ ≈ -0.27 → 
малий ефект). Box–Cox-модель дала узгоджений результат.
Відмінностей не виявлено (p > 0.05; ефекти малі/нульові)
Кількість (quantity), знижка (discount), вартість доставки (freight),
тривалість доставки (delivery_date), добові витрати на одиницю (daily_freight_per_unit)
— різниці немає; ефекти дуже малі та нестійкі.

Робастність і трансформації
Для асиметричних метрик застосовано Box–Cox/лог; нормальність залишків здебільшого 
покращувалась і не змінювала ключових висновків. Там, де нормальність/гомоскедастичність
сумнівні, висновки підтверджені непараметричними тестами.

Бізнес-інтерпретація
Germany: вищий ціновий рівень і більший чек на позицію.
UK: вищий відносний тягар логістики (freight_ratio), попри схожі абсолютні витрати
та швидкість доставки.

Операційно: обсяги, знижки, абсолютний фрахт і швидкість — еквівалентні між країнами.
Висновок: для «Confections» країна постачальника впливає насамперед на ціну і чек
(вище в Germany) та на відносну вагу доставки в чеку (вище в UK). Інші метрики 
прикладної різниці не демонструють. Це підказує стратегію: утримувати/розвивати 
преміальніший мікс у Germany; в UK — працювати над зниженням відносного фрахту 
або підвищенням середнього чеку за рахунок міксу/ціни.

