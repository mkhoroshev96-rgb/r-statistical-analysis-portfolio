Що зробити: або розподілити фрахт на позицію пропорційно вартості/кількості 
(як ви робили в інших задачах), або моделювати з кластеризацією (mixed-effects: 
random intercept по order_id / робастні SE з кластеризацією).
Мікс товарів як конфаундер
Ціна/чек різні між країнами можуть бути наслідком іншої структури продуктів у 
Confections (різні product_id).
Що зробити: контроль за product_id (fixed effects), або matching по продукту, 
або аналіз різниць всередині одного продукту (paired/blocked дизайн), або WLS з 
вагою частки продукту.
Multiple testing
7 метрик × кілька підходів → імовірність хибнопозитивних зростає.
Додай корекцію (BH/FDR) для головної батареї тестів; збережи фокус на 2–3 первинні
метрики (pre-spec).

Cliff’s δ і CІ
Є логічна неузгодженість: місцями пишеш «довірчі інтервали не включають 0, отже 
ефект не значущий». Якщо CI не включає 0 — це, навпаки, значущо. Перевір опис.
Box–Cox/лог: бек-трансформація середніх
Зворотне перетворення середнього на трансформованій шкалі не дорівнює середньому
на оригінальній ( Jensen’s inequality ). Для лог-моделі коректніше давати 
геометричні середні або застосовувати smearing estimator.
Shapiro–Wilk: де перевіряти нормальність
Для t/LM нормальність важлива для залишків моделі, а не для сирих даних. Ти це 
місцями робиш правильно (перевіряєш residuals), але подекуди робиш висновки вже
після глобального shapiro по змінній. Тримайся перевірки residuals by model.
Ratios та діління
freight_ratio = freight/chek та daily_freight_per_unit = freight / (delivery_date×quantity)
— нестабільні при малих знаменниках та чутливі до викидів (0-знижки/малі чеки/малі дні).
Рішення: winsorization/trimmed mean, аналіз на лог-масштабі (log-ratio), або 
моделювання компонентів окремо.
Нулі та трансформації
Для змінних із нулями — Yeo–Johnson краще, ніж Box–Cox+мікрошіфт (менше арбітрарності;
стабільніше).
Вага та SE
Для позицій має сенс зважувати на вартість позиції при агрегуванні або використовувати
робастні (HC/clustered) SE.
Дрібні технічні моменти
У частині з freight_ratio спершу MW p≈0.0035 (значуще), а потім у тексті про CI/δ 
звучить, ніби «не значущо» — підчистити формулювання.
Для описової статистики поряд із «mean/median» додай IQR і MAD — посилить робастність.
Перевір єдність позначень країни в сирих даних ('UK' vs 'United Kingdom') — у 
Northwind зазвичай 'UK', але краще уніфікувати на етапі ETL.

Висновок по якості кейсу
Високий рівень: логіка аналізу, охоплення метрик, вміння працювати з трансформаціями
й ефектами.
Головний ризик валідності — кластеризація на рівні замовлення та конфаундинг 
міксом продуктів. Якщо додати control по product_id/random effects по order_id 
і зробити FDR-корекцію, результат буде публікабельним і методично бездоганним.
Бізнес-висновки виглядають стійко: DE дорожче, UK — більша частка доставки; інше
— без суттєвих різниць.

