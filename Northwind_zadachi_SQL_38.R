A/B тестування — вплив економічного статусу країни на структуру замовлень
📂 База даних:
Northwind — використовуються таблиці orders, order_details.

🎯 Мета дослідження:
Перевірити, чи відрізняються характеристики замовлень між двома групами європейських
країн залежно від економічного статусу:
  
top-country — країни з Великої сімки (G7), що представлені у базі: 'France', 'UK',
'Germany', 'Italy';
other — решта європейських країн: 'Spain', 'Switzerland', 'Belgium', 'Norway', 
'Sweden', 'Austria', 'Poland', 'Ireland', 'Denmark', 'Portugal', 'Finland'.

🔍 Одиниця аналізу:
Окреме замовлення (order_id), яке має країну доставки в Європі.

📊 Метрики для порівняння (на рівні замовлення):
  
avg_price — середня ціна одиниці товару;
avg_quantity — середня кількість товару;
avg_discount — середній розмір знижки;
avg_freight — середня вартість доставки;
median_order_value — медіанна вартість позицій у замовленні (з урахуванням знижки);
avg_chek — середній чек;
avg_delivery_date — середня тривалість доставки у днях.

Рішення

WITH cte as(SELECT order_id
            ,ship_country
            ,ROUND(AVG(unit_price)::numeric,2) as avg_price
            ,ROUND(AVG(quantity)::numeric,2) as avg_quantity
            ,ROUND(AVG(discount)::numeric,2) as avg_discount
            ,ROUND(AVG(freight)::numeric,2) as avg_freight
            ,ROUND(PERCENTILE_CONT(0.5) WITHIN GROUP (ORDER BY quantity * unit_price * (1 - discount))::numeric,2) AS median_order_value
            ,ROUND(AVG(quantity * unit_price * (1-discount))::numeric,2) as avg_chek
            ,ROUND(AVG(shipped_date - order_date)::numeric,0) as avg_delivery_date
            FROM orders
            JOIN order_details USING(order_id)
            where ship_country in ('Spain', 'Switzerland', 'Italy', 'Belgium', 'Norway', 'Sweden',
                                   'France', 'Austria', 'Poland', 'UK', 'Ireland', 'Germany',
                                   'Denmark', 'Portugal', 'Finland')
            GROUP BY order_id, ship_country)
SELECT *
  ,case
when ship_country in ('France', 'UK', 'Italy', 'Germany') then 'top-country'
else 'other'
END as countries_group
FROM cte
ORDER by countries_group

