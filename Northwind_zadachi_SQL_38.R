категорій "Grains/Cereals" та "Condiments" за шістьма ключовими метриками, 
пов’язаними із замовленнями.

📂 Дані:
Використовується база Northwind, таблиці:
categories, products, order_details, orders — обєднані через category_id, 
product_id, order_id.

🔍 Критерій вибірки:
До аналізу включаються лише товарні позиції з категорій:

('Grains/Cereals', 'Condiments')
📊 Метрики для аналізу (по кожній товарній позиції):

unit_price — ціна одиниці товару
quantity — кількість одиниць у замовленні
discount — застосована знижка
freight — вартість доставки (на рівні замовлення)
chek — вартість товару з урахуванням знижки
delivery_date — тривалість доставки в днях (shipped_date - order_date)

Рішення

SELECT category_name
,p.unit_price
,quantity
,discount
,freight
,ROUND((p.unit_price * quantity * (1 -discount))::numeric,2) as chek
,(shipped_date - order_date) as delivery_date
FROM categories
Join products p USING(category_id)
JOIN order_details USING(product_id)
JOIN orders USING (order_id)
WHERE category_name in ('Grains/Cereals', 'Condiments')

