Задача 162

Порівняння замовлень між клієнтами з Німеччини та Франції
База даних: Northwind
Таблиці: orders, order_details (обєднані через order_id)
Одиниця аналізу: одне замовлення (order_id)
Вибірка: лише замовлення, доставлені до 'Germany' або 'France' (ship_country)

🎯 Мета дослідження:
Перевірити, чи існують статистично значущі відмінності між клієнтами з Німеччини
та Франції за такими ключовими метриками:
avg_price — середня ціна товару в замовленні
avg_quantity — середня кількість одиниць товару в замовленні
avg_discount — середній розмір знижки
avg_freight — середня вартість доставки
median_order_value — медіанна вартість позицій в замовленні (з урахуванням знижки)
avg_chek — середній чек на замовлення (сума з урахуванням знижки)
avg_delivery_date — середня тривалість доставки (у днях)

📊 Методи аналізу:
Shapiro-Wilk — перевірка нормальності для кожної змінної у двох групах.
Levene Test — перевірка рівності дисперсій.
t-тест або Mann-Whitney U — залежно від результатів попередніх тестів.
Effect Size — обовязково розрахувати (Cohen’s d або Rank-biserial).
Boxplot — для візуального порівняння кожного показника.
                                
Рішення

SELECT order_id
,ship_country
,ROUND(avg(unit_price)::numeric,2) as avg_price
,ROUND(avg(quantity)::numeric,2) as avg_quantity
,ROUND(avg(discount)::numeric,4) as avg_discount
,ROUND(avg(freight)::numeric,2) as avg_freight
,ROUND(percentile_cont(0.5) within group(order by (unit_price * quantity * (1-discount)))::numeric,2) as median_order_value
,ROUND(avg(unit_price * quantity * (1-discount))::numeric,2) as avg_chek
,ROUND(avg(shipped_date - order_date)::numeric,0) as avg_delivery_date
FROM orders
JOIN order_details USING(order_id)
where ship_country in ('Germany', 'France')
GROUP by order_id,ship_country

