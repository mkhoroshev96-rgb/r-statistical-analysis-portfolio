📦 Контекст:
Компанія Northwind має невелику команду працівників (employee_id), які обробляють 
замовлення.Кожен з них — або чоловік, або жінка (gender). Бізнес хоче зрозуміти:
  
Чи впливає стать працівника на ключові бізнес-показники?
Чи є статистично значущі відмінності у продуктивності?
Чи є сенс впроваджувати додаткові навчання або переглядати політику мотивації?
  
🎯 Мета дослідження: Порівняти чоловіків і жінок серед співробітників за наступними
метриками на рівні замовлень:
  
delivery_time — середня кількість днів від оформлення до відвантаження
sum_quantity — сумарна кількість товарів у замовленні
avg_price — середня ціна товарів у замовленні
avg_discount — середня знижка на замовленні
avg_freight — середні витрати на доставку
sum_chek — загальна вартість замовлення після знижки
sum_profit — прибуток компанії з замовлення

