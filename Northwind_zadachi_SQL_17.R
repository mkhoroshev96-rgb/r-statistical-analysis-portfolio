WITH cte as(SELECT country
            ,last_name
            ,first_name
            ,employee_id
            ,order_id
            ,order_date
            ,shipped_date
            ,unit_price
            ,quantity
            ,discount
            ,freight
            FROM orders
            JOIN order_details USING(order_id)
            Join employees USING(employee_id)),
agg AS(SELECT country
       ,last_name
       ,first_name
       ,employee_id
       ,order_id
       ,ROUND(AVG(shipped_date - order_date)::numeric,0) as delivery_date
       ,ROUND(AVG(unit_price)::numeric,2) as avg_price
       ,ROUND(SUM(quantity)::numeric,2) as sum_quantity
       ,ROUND(AVG(discount)::numeric,4) as avg_discount
       ,ROUND(AVG(freight)::numeric,2) as avg_freight
       ,ROUND(SUM(quantity * unit_price * (1-discount))::numeric,2) as sum_chek
       ,ROUND(sum((unit_price * quantity * (1 - discount)) - (unit_price * quantity * 0.6))::numeric,2) as sum_profit
       from cte
       Group by  last_name,first_name,employee_id,order_id,country
       Order by last_name, first_name)

SELECT *
  ,case
when country = 'USA' THEN 'US_employer'
when country = 'UK' THEN 'UK_employer'
END as country_group
FROM agg

