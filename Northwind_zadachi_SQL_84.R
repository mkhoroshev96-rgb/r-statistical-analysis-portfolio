Підсумок по суті висновків
Сигнали «надійні» і бізнесово релевантні:
USA вище за ціною та чеком — це, ймовірно, прайс/мікс ефект (потребує контролю 
за продуктом).
Brazil вище за freight_ratio — логістика «кусається» відносно чеку; навіть після
трансформацій результат тримається.
Інші метрики (quantity, discount, freight, delivery_date, daily_freight_per_unit) 
— різниці немає або ефект мізерний. Це узгоджується з очікуванням, але обов’язково
прибери псевдореплікацію для остаточної версії.

