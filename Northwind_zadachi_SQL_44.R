📦 База даних: Northwind
📊 Таблиці: orders, order_details, products, categories
🔗 Обєднання: через order_id, product_id, category_id

🎯 Мета дослідження
Визначити, чи існують статистично значущі відмінності у структурі замовлень, цінах
та логістичних витратах між двома категоріями товарів:

🥤 Beverages (напої)
🥦 Produce (овочі та фрукти)

📐 Метрики для аналізу (на рівні товарної позиції в замовленні):
unit_price — ціна одиниці товару
quantity — кількість одиниць товару
discount — розмір знижки
freight — загальна вартість доставки
chek — вартість позиції з урахуванням знижки
freight_ratio — частка доставки у вартості товару
ivery_date — час доставки у днях
daily_freight_per_unit — добова вартість доставки однієї одиниці товару (freight 
/ дні доставки / quantity)

