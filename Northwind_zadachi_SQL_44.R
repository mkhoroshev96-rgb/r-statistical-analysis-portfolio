даними категоріями. Це ціни, відношення вартості доставки до чеків на куплені товари,
чеки, кількість днів доставки та добова вартість доставки одиниці купленого товару.
Як це все пояснити? Почнемо з самого початку - ціни на овочі та фрукти є кратно
вищими, ніж на напої. В цілому так воно і є, але ж ми оцінювали по всім рокам, тому
в не сезон деякі овочі та фрукти коштують дуже дорого, до того ж вони є дуже
корисними від цього така й вартість навідміну від напоїв, які є в цілому протилежними
за властивостями, особливо алкогольні вироби. Проте в категорії напої не тільки
алкогольні вироби, а й вода проста та солодка, тому і розмах цін є досить широким
по даній категорії, проте все ж овочі беруть більш стабільно високими цінами, тому
й ми маємо статистично значущу різницю. Далі, відношення вартості доставки до
величини чеку. Тут досить просто - по категорії Напої вартість доставки є майже
ідентичною за величиною, що і поОвочам/фруктам, проте чеки є вищими саме по 
категорії Овочі фрукти, тому й дана метрика виходить вищою саме по категорії 
Напої, бо ділиться вартість доставки, нехай така як і по Овочам фруктам, проте
чеки є значно нижчими. тому й ми маємо статистично значущу різницю. Далі, як ми
вже сказали чеки є суттєво вищими по категорії овочі/фрукти. Так як дана метрика
є похідною, то дуже велике значення відіграла ціна, яка кратно більша по категорії
Овочі/фрукти. Далі кількість днів доставки. Свіжі овочі та фрукти - продукт дуже
швидкопортящийся, тому й логічно, що його доставка є якомога швидшою на відміну
від напоїв, в яких термін придатності від 90-180 днів і вище при чому умови зберігання
нормальні, а от для овочів та фруктів потрібгний холод. Це все впливає на швидкість
доставки і як висновок - маємо статистично значиму різницю. І наостанок - добова 
вартість доставки одиниці товару є значно вищою по категорії Овочі/фрукти. Чому 
так? Бо вартість доставки ділиться на нижчу кількість днів доставки, ніж по категорії
Напої і як наслідок маємо різницю між групами. Крім цих 5 показників є ще одна метрика
показник р якої є дуже близьким до рівня 0,05. Це знижки, з показником р в 0,0993.
Тобто дана метрика в близькій перспективі може стати статистично значимою. І 
наостанок - 7 з 8 показників мають слабкий розмір ефекту, тому є не значущими в
прикладному плані для бізнесу. Але от по цінам розмір ефекту є сильним, тому це
єдина метрика яка має дуже суттєве значення в прикладному сенсу для бізнесу.

