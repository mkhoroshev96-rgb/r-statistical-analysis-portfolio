WITH cte as(SELECT category_name
            ,order_date
            ,extract(year from order_date) as year
            ,p.unit_price
            ,quantity
            ,discount
            ,freight
            ,ROUND((p.unit_price * quantity * (1-discount))::numeric,2) as chek
            ,ROUND((freight/(p.unit_price * quantity * (1-discount)))::numeric,4) as freight_ratio
            ,(shipped_date - order_date) as delivery_date
            ,ROUND((freight/(shipped_date - order_date)/quantity)::numeric,4) as daily_freight_per_unit
            FROm orders 
            Join order_details USING(order_id)
            JOIN products p USING(product_id)
            JOIN categories USING(category_id)),
median as(select *
            ,ntile(2) over (order by order_date) as median
          FROM cte
          WHERE category_name = 'Beverages' AND year = 1997)
SELECT *
  ,case when median = 1 THEN 'Домедіанний період'
when median = 2 THEN 'Післямедіанний період'
END as median_periods
FROM median

